<h1>The Fear of Life</h1>
<p>The fear of life is the first cause of all our psychological troubles.</p>
<p> The fear of life is a silent and false assumption that life is untrustworthy and dangerous that runs in the background of all experience.</p>
<p> The fear of life comes upon almost all of us accidentally at birth, when the shock and violence of our arrival sets the context within which our entire psychology -- all of our understandings, all of our bedrock assumptions, our likes and dislikes, and our sense of identity -- will take form. The fear of life is the cause of all human aggression and self-destructive behavior.</p>
<p> The fear of life is a psychological autoimmune disease. It seeks to protect us from the danger of being alive by holding life itself at arms' length lest we fall in and perish. It warps the lens of personal psychology through which we perceive the meaning, validity, and the likely effect of everything that happens to us, within us, and around us. It creates and maintains the delusion that life is not safe, that life is not to be trusted. It poisons our minds and our relationships with one another and with the earth itself.</p>
<p>All of our self-hatred and hatred of others, all of our self-defeating and aggressive habits of behavior and relationship, and all of our misery and disappointment in ourselves and in our lives spring from one simple cause, and that is the fear of life, which drives us insane.
  </p>

