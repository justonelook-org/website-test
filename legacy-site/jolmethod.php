   <p><strong>Great little instruction booklet on being human</strong><br>
</p>
   <p><em> I enjoyed reading this very practical and pleasantly short guide on a practice that has the potential to transform anyone’s life. John and Carla's take on self-enquiry is unique and legit. Their use of a meditation technique to aid focus and daily attention is also useful.</em></p>
   <p><strong>Can change your life by taking away your suffering</strong></p>
   <p><em>I've been involved in the seeking and spiritual healing fields all my life. I've experienced very few things that make a practical difference in one&rsquo;s day to day life. For a number of years I was involved with the healer Bruno Groening and one of this main groups devoted to his teaching. Bruno was the most prolific healer I know of in recorded history. I feel the JOL teaching is an evolution of sorts to his teaching. But all spiritual belief paradigms are stripped away with JOL, and you just get a very pure teaching which can actually reduce your suffering in your life. The idea is that you can lose what John and Carla call 'the fear of life' and you can get to the point where you are not suffering no matter what happens. That doesn't mean you don't feel pain, or cry when you lose a loved one, etc. There is a simple (but not easy!) exercise called the 'Self-Directed Attention Exercise' which is the same vehicle as Buddhist anapanasati meditation, but with a different intention&#8212;and, imo, this makes all the difference. (If you are a meditator, check out John Sherman's conversation 9 on youtube where he explains the difference between the two). I've never experienced any method/teaching that is making such a practical difference in my life in terms of reducing any suffering that arises. I've been practicing this teaching for around 10 months now. I didn't want to write a review until I felt I was starting to experience consistent results in my life on a day to day basis. Now this is starting to happen for me. I highly recommend checking out John and Carla's website at justonelook.org and checking out all the videos of talks given by John Sherman on youtube. There is also a forum on the justonelook website where people discuss their experiences using this teaching, and I am keeping a record on there of any significant changes I experience as I continue to use this teaching.</em></p>
   <p><strong>Everyone should read this book!</strong></p>
   <p><em>If you're tired of the constant frustrations of life, if you've been searching for years in religion, spiritual teachings, psychology, or self-help books, but haven&rsquo;t found any lasting answers, this little book is what you need. You won't need another one. John and Carla Sherman offer a detailed description of their Just one Look Method, and a clear explanation of the fundamental, underlying cause of human suffering. The solution almost seems too simple to be true, but I can vouch for the fact that it works. Although I've only been practicing this method for around nine months, I have seen significant changes in my life. My old, neurotic thinking patterns are constantly being overhauled, and I feel as though I'm finally approaching a level of maturity and sanity that was unknown to me before. I&rsquo;d advise everyone to buy this book and try the Just one Look method. You've got nothing to lose, and more to gain than you can imagine.</em></p>
