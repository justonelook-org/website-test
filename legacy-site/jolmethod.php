   <p><em>I was ridden by deep depression and anxiety all my life. After applying your method in 2011, the symptoms started to leave. No endless ruminations anymore. Hopelessness and anxiety are pretty much gone. I'm still lacking in drive and energy, but more interested in things to enjoy and in practical matters rather than worrying about it. I just used the core method, the Looking, though. The Self-Directed Attention Exercise had proved too hard for me yet. Both techniques are described in the book in great detail, clearly referring to a lot of real-world experience working with people. There's no beating around the bush. You get to know what to do right off the bat. Every line brims with dedication, spirit of research and enthusiasm about this work. It touched my heart.</em></p>
   <p><strong>This has worked when nothing else has!</strong></p>
