<h1> Lookers Tell Their Stories</h1>
<p>&quot;I was ridden by deep depression and anxiety all my life. After applying your method in 2011, the symptoms started to leave. No endless ruminations anymore. Hopelessness and anxiety are pretty much gone. I'm still lacking in drive and energy, but more interested in things to enjoy and in practical matters rather than worrying about it.&quot;</p>
<p>&quot;I&rsquo;ve been  trying to put my finger on the changes that have happened more than six years  out from the first look at myself. I now  somewhat understand John&rsquo;s loss for words when trying to describe a life  without fear. I have a taste of this life. The neurosis and anxiety have calmed  down for some time now. As the  filters and barriers come down I am less and less in the deadened inner world  of the habits of fear. The world around me is enlivened and endlessly  fascinating. There is a feeling of rightness, even when there is something  wrong externally, and of course much is externally wrong with the world right  now, as ever. This feeling of rightness is deep and pervasive from the surface  to the bones of me.&quot;</p>
<p>&quot;I came across Just One Look in June of this year. I tried the looking and, to be honest, I didn’t experience anything at all. But John had said in a video and in writing that that doesn’t matter. Whenever I felt inclined I did the looking again, until that stopped. Now I have started carrying out the Self-Directed Attention practice. John and Carla are so helpful. No fluffy fantasy promises. A simple practice that leads us to being fully functioning mature self-reliant humans.&quot;</p>
<p>&quot;I had a funny image of how The Just One Look Method operates. It’s like when you cut off a chicken’s head, the chicken still runs around for awhile until it finally gives up and drops dead. Kind of macabre, but amusing nonetheless!&quot;</p>
<p>&quot;Pre-looking 6 years ago, I suffered with depression and anxiety for 30+ years and although I know it is predominantly psychological in nature it also notoriously causes physical symptoms in the body, 2 years into the looking this has left me and has never returned. This has also been the case with a condition I suffered with called fibromyalgia, tendinitis, psoriasis, which is an autoimmune disease, IBS, sciatica, chronic sinus problems, urine infections repeatedly, etc. etc. The list goes on... All gone. Some ailments still remain, but even they are better than they were, and that's okay because my attitude towards health and illness is so much saner than it ever was. I do what needs to be done in the moment. I have tapered off 4 prescription drugs as I don't need them anymore, and am currently coming off my last one and then I'm done. The way I have come to see it is that the fear-based negative thoughts affect and may even cause illness/disease to become chronic, the fearful thoughts constantly comment on how terrible this is, how much worse its getting, I can't cope blah blah blah. I think a diseased mind riddled with fear affects our body at a cellular level, how can it not? Looking at it from a scientific point of view, there's plenty of evidence out there that confirms this.. Mind and body are connected. My point is that once the fear starts to fall away, some illness/disease can disappear, although I agree its not always the case by any means, and that's important to realise, but it is possible!! I think once the looking is done the possibilities are endless...&quot;</p>
<p>"I&rsquo;ve had many physical ailments fall away in the last seven years, especially anxiety and depression-related ailments. The biggest thing is that I stopped being hyper focused on my health, mental and physical, and this allowed many things to naturally clear up. The uncovered intelligence of the body/mind also allows for better lifestyle choices that change health profile over the long run."</p>
<p> &quot;Clarity, like so many things I've discovered on this path, isn't what I had always thought it would be–it's better! I must add that doing some 15 months of regular, twice-daily self-directed attention gave me the clarity to try cutting out all sweeteners and processed foods. The worst of a lifetime of anxiety and depression disappeared within the first few days. Anything left, which I put down to my remaining shortcomings in controlling my attention, is, with that renewed effort, gradually improving, so I'm totally fine with it being a maturation process rather than a miracle cure.&quot;</p>

