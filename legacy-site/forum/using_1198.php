<p>Hello Nikos,</p><p>I've read Tolle's Power of Now and A New Earth, and those books are very dear to me. They have taught me a lot about how the mind operates, and why it is so good at fooling itself. I've had some wonderful insights and experiences when I read them but the thing about those wonderful insights is that sooner or later, they lose their wonder. Back then, I was happy to learn that I wasn't the story about me that lived in my mind, but that never helped me to see who I was. I had my share of ego-less states that were filled with bliss but what they really did was go away after a while and leave a reference point. Before those beautiful flashes, my life was crap. But after these flashes, my life was still crap, but now I had the knowledge that it didn't have to be crap, which obviously made the experience of everyday life even worse than it was before.</p><p>But that's the whole point - life can be a very harsh experience, your story of the car accident is a very clear example of that. But it is only when you have looked at yourself, and have seen that you are still the same you that experienced the crash, and experienced every other moment of your life, without being harmed or changed, that life is fine the way it is. That, for me, was the biggest surprise after the first time I looked at myself - that all this anxiety, and at the same time all this search for happiness, were all born from the same source : that I didn't want life to be what I thought it was. Back then, life was a painful place to be. Now it doesn't matter so much how I feel or think life should or shouldn't be, these things usually get in the way of living.</p><p>I hope you find something useful in what I wrote. It's good to see you have found your way here.</p><p>Wouter</p>
