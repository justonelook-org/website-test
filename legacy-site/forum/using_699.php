<p>Hello,</p><p>It's high time I've written something about the looking--in hopes that my message confirms what John and Carla are tirelessly attempting to communicate and also to encourage other people who are looking and may have hit some tough spots.</p><p>I've been at it for about 2 years now.</p><p>My life has gotten difficult lately and I'm ok with that--not because I choose to be--I just am. I think that I've seen the glaring fact that I am unaffected by any of lives vicissitudes enough times that the belief pattern has simply eroded which has made it possible to relax into the turmoil. I can clearly see that the turmoil, anxiety or anger are just labeling constructs and they actually mean nothing--on a sensate level they feel intense, even overwhelming but without the belief in the accompanying inner audio track they fade very quickly. None of this has anything to do with me--it happens on its own. I never choose to relax or "see through" this stuff. The looking does its thing in a magical way.</p><p>I feel and see everything that comes through me in a much more intensified way now--I have no choice in this. All I can do is look and though my attention seems to have a stronger relationship with patterns that in the past I've identified as negative or troublesome I can sense that the days these patterns hold my interest are numbered and knowing that at such a core level brings me to a place of almost feeling endeared to these old habits as if they are gone already and I miss them, they never hurt me. It was just a misunderstanding--play fighting.</p><p>As these habits lose momentum the looking is revealing itself to be what I am--the 'old me' was just a story, a certain perspective and stance, some opinions, physical sensations, these all used to be the 'axis' of my existence--now they just seem to be the beautiful and intense show that I am.</p><p>Thank you John and Carla.</p><p>Richard</p><p>"And if you listen very hard,</p><p>the tune will come to you at last,</p><p>when all are one and one is all, yeah</p><p>to be a rock and not to roll"</p><p>Zeppelin</p>
