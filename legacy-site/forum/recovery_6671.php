<p>In addition to what John has replied, this can be an interesting read: <a href="http://www.fastcompany.com/3059634/your-most-productive-self/your-brain-has-a-delete-button-heres-how-to-use-it">http://www.fastcompany.com/3059634/your-most-productive-self/your-brain-has-a-delete-button-heres-how-to-use-it</a></p><p>In short: the article discusses that connections in the brain that get used less get marked by a protein. When that mark is detected by 'glial cells', they destroy the pathway. Which is actually literally what John is saying: if you decline to use these neural pathways, they will starve.</p><p>I guess not interacting with people is not a good way to starve these pathways.. you are looking for healthy interaction instead of no interaction.</p><p>Also if I understand the problem might not be in the interaction, but in the social comparison.. which still might occur in forced social settings or in isolation.</p><p>Stop the social comparison and the pattern will starve.. probably easier said than done, but i guess it's as simple as that.</p><p>NB: Of course this article might be the easily consumable mass-media version of the actual story, it might only apply to very specific regions, pathways or animals even. There is probably no proof this applies to behaviour patterns, but that doesn't make it less interesting and relevant.</p>
