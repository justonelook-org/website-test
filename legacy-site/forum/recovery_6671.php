<p>It's been 3 and a half years since I first looked and I am certain what needed to happen, happened. Something to do with my perception has definitely changed but it is hard to describe. I suppose it is just the case that I am going sane and that what is happening right now, moment to moment, appeals to me more than worrying about an uncertain future, which makes life seem more interesting. Like there is so much going on. Like I say, it's hard to describe.</p><p>What has not changed for me is the self consciousness that has been my biggest enemy for a long time. I don't understand why it is hanging on and I wonder if using focused attention exercises is the only way I can get past it. It is basically social anxiety and I always feel it when around people. I am more worried about my facial expression than of other people when I talk to them. My smile is sometimes feigned, like I have to force it. It sounds ridiculous but it has become like an obsession. I know this is big detail but i don't mind sharing and wonder if others can relate? At times when there is no anxiety I can obviously relax and enjoy company but usually it is very frustrating.</p><p>Anyway, this isn't a major deal I know, so i'm not looking for any life saving advice. Just thought I'd post something. Forums are a bit quiet these days eh...</p><p>Jim</p>
