<p>What we as a group have in common is an interest in the looking.  Our interest may be spurred by different desires (mine:  to be rid of misery and suffering), but we're interested in looking at ourselves all the same.</p><p>My purpose in writing about us as a group is this:  first, I was struck this morning with the possibility that we could be like one of those groups which has had an impact or made a mark in history, like the cafe people of the Left Bank, or the Greenwich Village beatniks, or the Hudson Valley artists, etc.  My guess is that most of us don't do much joining, and we appreciate the absence of entanglement in being involved with people we can't see or touch or even talk to.  Yet we have commonality of interest, and the significance of that interest cannot be overestimated.  I mean, let's face it, eradicating misery and suffering in our experience of life or the fear of life that spawns it is a pretty big deal.</p><p>Second, by having the sense of community in mind when we post, we can avoid the flattery and self-congratulatory or self-serving comments that may bog us down and turn off those of us who have the tendency to feel excluded.  There can be no nucleus to a group like us.  We are all so different, and the looking has proved that if nothing else.  I would guess that our "leaders" set up the private message format to limit the personal and reduce the chaff.</p><p>Third, there seems to be a fair number of people who tune in to the Forum but don't post.  I was very reluctant initially.  Now you can't shut me up.  I promise those of you who were like me that there will be a 10-fold benefit against the risk and trouble of posting "I am here."  And if you don't choose to post, there will be equal or greater value in asking, "who is not posting?" (but no value whatsoever in asking, "why do I choose not to post?").  trimpi</p>
