<p>Dear johninar,</p><p>First of all, let me say that I am not in your place. I am trying to be useful to you, but I cannot put myself in your place completely and cannot understand how you feel fully.</p><p>This is what I can say and, again, I hope you find it useful somehow. The pain is a part of it. Excruciating pain, fear, loss, death, lack of ability to cope with things, these experiences are all part of it. They are all part of life. That's not to say that it's up to you to hold it all in your embrace without any resistance or impact. If it hurts, it hurts. If it's too much, it's too much. If it's overwhelming, it's overwhelming. If it comes to you that you can do something about it, you will. And if there's nothing to be done at all, nothing will be done. The miracle of being you is that nothing is excluded. Not even what is hugely painful. Nothing is resisted, nothing is thrown out. You can hold everything, even the experience of not being able to hold things. Now some situations are so powerful, so intense, that they can put kind of a veil between us and ourselves. It's a temporary thing. No matter what happens, you will always find yourself again, when things settle down or even before that. You are always here, never absent and you know that well. The rest is just life living itself. I hope this is useful to you.</p>
