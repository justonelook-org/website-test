<p>Hi John,</p><p>I encourage you to stay with the looking and my guess is you will notice that, over time, your responses to life's challenges starts to become less conflicted.  There are some very challenging situations in my family. For me, these are the most loaded. They can trigger huge emotions in me. Prior to the looking, my responses were highly constrained by fear. While I knew how I felt, I couldn't bring myself to communicate those feelings and opinions directly in a clean , clear manner. Of course   it is still part of who I am, I have noticed, however, that it is becoming easier to speak my truth. There also seems to be less emotional charge to my comments and responses so family members are more likely to respond to the content of my words rather than the huge emotion  that was lurking behind the words in the past.. I am by no means home-free on this one. It is a work in progress but the looking, over time, has made a huge difference in not  feeling "on the line" in these situations. I wish you well with all of this. It is not easy.  There is something about family dynamics that seems to bring up "primal" kinds of feelings and responses. Maybe the best thing we can do is what you are doing. Just doing one"s  best and  trying to take responsibility for  ourselves and our own linitations  and the  looking certainly helps with that.</p><p>Best</p><p>Paul</p>
