<p>Greetings,</p><p>I found this forum through the interview of David Parrish on batgap.com. I also listened to John Sherman's interview there. When I did the looking exercise I had an incredible experience. When I intended to turn my attention inward to see what it felt like to be me, it was like a floodlight illuminated what it truly did feel like to be me beneath the facade and layers of the projected me that I was most familiar with. What I experienced for that brief moment was absolute stark terror. I saw that I was terrified all of the time. Then I experienced compassion for my scared self because I felt sorry for myself that I lived in such fear all of the time and didnt even know it. I was really blown away. I had no idea that so much fear was hiding there.</p><p>I have done the exercise many times since then and I have not been able to duplicate the same piercing recognition of what it feels like to be me that occurred the first time. So my question is, Is that normal?  If the fear is gone I cant tell and I dont get a sense of it the way I did the first time.</p><p>When I watched the JSherman intervew he said that the first look was the most important so I am hoping that the initial look did create the catalyst for the resulting recovery process that he spoke of. I am interested in the experiencees of others to see if there are similarities and to see how it has unfolded for others.</p><p>Thank you!</p>
