<div class="forum-post-template-post"><p>Hi Laurel</p><p>I just wanted to let you know that I had a very similar experience as you. The first time I noticed myself, my anxiety went through the roof. It was as if my perspective became vastly enlarged. This was, actually, before I was aware of John's work. I had been reading Greg Goode's book, Standing as Awareness when this happened. I was very worried and alarmed by this reaction. I wondered if I was dissociating. I actually contacted Greg for reassurance. When I started John's looking my anxiety continued to spike when I engaged in the looking. I still don't know exactly what that is about but I wonder if it is a the terror in seeing something so incredibly huge coupled with the fact that we initially see it as being external to us (therefore threatening). That is how it felt to me but it has settled, overtime, I think because my experience in continuing to do the looking has shown me that I don't have to be afraid of me. Even though I am ever present, fundamental to everything etc. I am essentially, as John says, simple beyond belief; like discovering that I have a big brother only to find out that it is just me.</p><p>With kind regards,</p><p>Paul</p>
