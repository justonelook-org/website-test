<p>I'm still wondering how it is that this insignificant act took away all my motivations and aspirations, all ambition and desire for achievement, it's really strange to see. I mean I'm okay with that at the moment, in fact I enjoy it but something in me still nagging: how you gonna survive like that...where you gonna be in 10 years if you continue like that.</p><p>I wonder about this thing called fear, the mother of all these wonders of human achievement. It took us to the moon and let us see the naked surface of Mars, hidden beneath its dusty cover dancing along with the stars, it made us dominate the earth surpassing the great dinosaur and the tiger in power and skill, made civilizations with all its scientific inventions, administrative play and complex views of how we see the world and ourselves. It made us fight wars, then conquer one another, colonization and slavery as he whose fear is greater is the winner and the winner takes it all.</p><p>It has made our minds speed up, overwhelmed by gigantic floods of information vomited out of the mouths of giant oracles called google and tell-lie-vision that where shaped by our minds yet shaping our minds in our craving for more, bigger and better life. Yet what is the price?</p><p>As we pay attention to the daily soap called "The Fear of Life" the currency that slips through our hands is and has always been -- Our Life.</p><p>Like the bull in the arena who has been chasing this damn red fabric over and over again, each time getting a spear in his neck not realizing he will never win - that's the rule of the game.</p><p>As we're bankrupting ourselves in the stock market of our minds, speculating on what idea is gonna be the next jackpot, we here have decided to stop speculating and invested our last coins on nothing and no one but ourselves - Bullseye!</p>
