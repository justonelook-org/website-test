<p>The one dose of 'fear gone' has killed some motivation.  It seems like I can find just as much enjoyment in what I would have called doing nothing in the past, when I let myself.</p><p>I still have a nagging fear that I need to keep inventing game changing systems and solutions.  There's also a sense that the fear driving that is losing ground.</p><p>I was a beach bum during my early adult years and I didn't 'straighten up' until I was almost 30 and became a father.  Since then I've been playing career catch-up.  Killing myself would be a more honest opinion.</p><p>I'll take loss of fake motivation over mindless fear.  But I agree, it's beyond strange.  One of the clues that the looking works for me is that it doesn't roll out according to my mind's terms.  The mind is great at pointing out faults in everything it sees but it cannot gain any ground on this.  Like trying to climb a greased pole.  It marshals in emotions and stored memories in an attempt to pull me back into the trance of fear because it fears anything different.  Wow, drag me back into fear because that's all I've ever known.  Oh well, algorithms can only work with known variables.  The good news is that these algorithmic flare ups can't seem to persist through the night.  1 day venues at best.  Now the dreams are getting funky but I'm viewing all this change as a good thing.  Stiring up the mess is part of the work, it appears.</p>
