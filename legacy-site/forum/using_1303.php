<div class="forum-post-quote"><p class="forum-post-quoted-name">Cytex</p><p style="margin-top:0;">Hi, since I did the looking, which is only two weeks ago, I realise the craziness of my past life. And this makes me feel sad, and even ashamed given the futility of my attempts.</p><p>On the other hand, I can also appreciate the creative energy that I invested to run away from me, to fix my life, to become fulfilled. I have explored new worlds, always on the look for the truth. And I learned so much. Aren't many of human achievements in art and science fuelled by that hole inside, that something is missing? Aren't we moved by art because it shows us the gap between what is and the intuitive knowing how life could be? Is this all gone now, will it be obsolete, once we live the lives without anxieties and longings?</p></div><p>Dear Cytex,</p><p>Your post caught my attention because I have been experiencing my own capacity for creativity in a way that was never possible before. Like you, and many other people I've met, I had an instinct for truth, for the purity of individual expression. I especially had a love of philosophy, a philosophy of freedom. I did everything I could to emulate what other people had done, some of whom I think might have lost the fear of life (Thoreau for instance).  But my perspective on all that has changed. The only thing I have lost is the subtle feeling of quiet desperation, which clouded my perspective and built a self supporting web of behaviors that kept me feeling safe, well, as safe as I could feel at that time. But lo and behold, now that the fear is gone, I still have "anxieties and longings." Difference is, I don't care as much about the anxieties, and the natural longing to learn has increased.</p><p>Now I find myself immersed in creative writing endeavors. I just finished a short book of letters and am working on a novel. The experience of creative writing is, well, as Steven King describes it, like "telepathy" or "magic." In fact, for lack of better words, that is how life is for me now. Creative energy is everywhere. I get to play in it, or watch, either way, what a gift!</p><p>Not that it matters much, but I wrote a short story about a family 300 years in the future, a future where the fear of life had been eradicated from all of humanity. A close friend and confident of mine wants me to turn that into my second novel. I am amazed to see my characters come to life in on paper in a way that was absolutely not possible when I was still inflicted. Of course, any good story has to have its challenges and hardships, as does natural human life. But the way I see it, the yearning I have to learn and explore can and is being expressed in a way that is by far, more natural and honest and expressive. And, funny thing is, if there is a moral to anything I write, it always comes around to "look at your self." Though I'm doing an end run with it, I can't help but put the suggestion in there.</p>
