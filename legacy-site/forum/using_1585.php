<p>Hi George!</p><p>In my view the looking is enough. I haven't used any mental exercise at all, during my recovery process. The new learning of what role my attention plays in my life, has come as a result of having looked at myself. But that has taken time. And I am still planing too start to focus attention as a exercise too strengthen my attention. But I am not sure if I ever will. And I think that is a combination of old habits and a feeling that it is OK anyway. Old stuff is falling away no matter what I do.</p><p>But in general I think focusing attention is a very good help on the way. It is very clear too me that my own suffering and confusion is decreasing as my attention gets stronger. Attention and suffering seems too have a strong connection. So for those who feel like it and can manege to get it done, I really think it is good idea to try it.</p><p>Niklas</p>
