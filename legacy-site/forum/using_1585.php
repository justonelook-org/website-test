<p>in my experience, what matters is if you consciously move attention. that means that you feel the physical sensation of moving attention and that you know it as you feel it what it feels like to move attention inside your mind.</p><p>what does not matter is what name you give this action. mindfulness, meditation, mindlessness,looking, not looking etc. are all welcome names if the movement of attention is actually accomplished.</p><p>also, my personal opinion is that if one thinks the objective is to kill fear, then it causes difficulty. that might be the motivation. but the objective is to move attention and to know that one has moved it.</p><p>the reason is that if I think that I want to kill fear to get freedom, my mind creates thoughts that imagine this fear and the killing of it, and subsequent freedom. these are only images and cause movement away from myself (irrelevant distraction). so its kind of self defeating. but if i acknowledge that is the motivation, but see that what is within my control is to move attention, it reduces the heartache associated.just a simple recalibration of expectations helps.</p>
