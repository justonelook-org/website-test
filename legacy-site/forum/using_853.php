<p>And I'm 67 years old. And in the midst of this day (usually one of my favorites, b/c it's a day when I feel more awake, more alive than usual)--I've been experiencing two states: one, feelings of extreme discomfort and bodily distress (ongoing allergies, headache, joint pain and an overall sadness for feeling sick on one of my favorite days) and two, being present in the midst of the magical quality of this day: it's warm and bright, with sun, sky, breeze, birds chirping--a feeling that the world is awake, even if I'm not.</p><p>So I seem to be standing on a fence between the way I used to "make" a day wonderful--and the way a day is wonderful without any effort from me. (I'm <span class="forum-post-italic">standing </span>instead of <span class="forum-post-italic">sitting </span>on the fence, b/c that's just the way this personality rolls.)</p><p>I hate feeling crummy. I hate not knowing why. I hate fighting with my life. So then I just let it be there. I just look at what is just the case. (I'm doing it now...) And there is the hum of the air conditioning, the sound of cars passing on the street--the light from the car windows that makes a sparkle of light dance around the room, the heaviness in my eyes, the sound of silence in my ears, my stomach grumbling, breath moving, eyes burning--and now, this room with me sitting here typing to you becomes a temple.</p><p>I hear the words, "forgive me for I have sinned" and I wonder at them...I see the sin is the separation from me, and I feel gratitude for this life, just as it is surround and support me. Yes, it's all OK.</p><p>Thank you all for receiving this message and for receiving me, just as I am.</p><p>I am grateful beyond measure,</p><p>With love,</p><p>Dawn</p>
