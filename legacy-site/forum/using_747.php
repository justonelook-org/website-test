<p>What you are looking for is not a revelation or new belief.  When you say you see that your existence will end, this is an interpretation or conclusion you have drawn.  That's fine, but who is interpreting or concluding?  We are so habituated to interpreting and concluding that it's difficult to rest in the looking.  I've given up any expectation of finding, and it's easier now for me to rest in the looking.  Sometimes, but not all the time, I get what I perceive to be the naked apperception of who or what I am.  Even that apperception boils down to an interpretation or conclusion, so I don't make anything out of it, except for maybe a little "aha."  Then I go about attending to whatever my attention is attracted to and come back to the looking when it occurs to me.  I think the only way you will find the "existence that will always be there" is to know that it can only be known in the present moment.  That realization will go, but it's always available to be known and realized again and again in the present moment.  If you know that's where it is to be seen, you will trust your senses and have a little "aha" reflection of your own.  The inquiry is the perfect vehicle for carrying you on the road of looking.  Trimpi</p>
