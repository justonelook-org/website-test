<p>Welcome Emma.</p><p>I've been looking at myself for about four years.  I'll see if I can be of help. If we think of existence as a noun, it IS very confusing because it drags along all you have ever heard about existence. Try thinking of it as a verb, as in "How am I certain I exist?" As in: If I said to you, You do not exist; and you would say, Yes, I do! Also, try saying to yourself, "I'm here, right?" and notice that sense of you that is always present no matter what's going on in the mind, body or world. I'm here and change is happening continuously in my mind, body and world and I notice it. But this sense of being here is the same as it has always been, the same as when I was three years old. I find Looking at Myself works regardless of understanding or belief.  It seems to bypass all that. As for before birth or after death, I just don't know. Really, the only thing I know is that I'm here. And that is totally miraculous.</p><p>About life getting harder with the looking: Looking back I find that my life certainly got stirred up. For example, I became aware of a substrate of fear I had been denying.  Over time I have become aware that no matter what I do, even Look at Myself, life seems to have its own wave rhythm. I may get a wave of disturbance followed by a wave of peace followed by a wave of disturbance, etc. With continued looking, I have realized that these waves are not me so I let them be.</p> <p>LeraJane</p>
