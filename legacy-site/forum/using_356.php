<p>Dear John and Carla,</p><p>I've been doing the vichara for no more than 5 or 6 months. I came to your work through listening to a YouTube video of Gangaji regarding what her teacher said about Stopping the search. It was at the time what I needed as I had been practising Transcendental Meditation regularly for about 15 years. I had no experience of anything they talked about. No Transcendental Consciousness, Cosmic Consciousness, whatever. While everybody was "transcending" all over the place I only felt frustration and totally out of it. Eventually I quit the movement, thinking I was hopeless but mostly feeling I had been sold a bill of goods. So Gangaji's little video came as a great relief. Just stop the whole nonsense. I had experiences of unstoppable laughter, tears but that all passed eventually and never came back so I got to you and listened to all your retreat videos, podcasts, thought it had something to do with what she was saying but not really.</p><p>As the vichara progresses I can see very clearly how I have been contracted, how I was protecting myself from what you call the wildness of life, it's unpredictable and certainly anarchistic nature, wanting to be safe, uninvolved in my life. And through all the craziness of my current situation however I continue the practice. I will say though some of this, getting nearer to the life is scaring the living heck out of me, I have invested that much in "staying away" at all cost but underneath is a sense of excitement, promise of what I have no idea.</p><p>When I do the vichara I am looking at something I know. Perhaps the analogy that works for me best is the one of finding a childhood memory. It's clear that whatever it is has always been and and has remained untouched. But it really seems like nothing very much at all. Just THAT. Really..nothing. It's certainly encouraging to hear you say there's no way you can do this wrong! I don't know where all this is taking me, but it's certainly one heck of a ride. (excuse the non-spiritual phrasing. I'm really not much of a spiritual person anyway.)</p><p>Appreciate your work, the way your presentation of this has evolved and wish you and Carla all the very best. Hope we may meet some day.</p><p>Antony</p>
