<p>Hi John, thanks again for speaking with me.  It seems now like a lifetime ago.  I felt your compassion for me during our conversation and it was such a welcoming feeling.  But as you know only I can be the one to "look" at myself and rid myself of the feeling of being separated from my own life.  I've had some good days and some very bad days.  Randy and I are still separated and that is the hardest thing for me.  The what I'll call "little" things of life (job, flat tire, etc.) those don't seem to be a burden any more.  The uncertainty of my future has been devastating to me both in terms of my relationship with my husband, my financial situation, my view of what "family" means to me.  They are all cloudy and murky and I feel totally out of control.  The river is in control and I am not enjoying the ride.  When I hear from my husband or get any sign that things are improving I feel "good" and then when he changes his mind or perhaps his words are interpreted wrongly my me I feel bad.  It's easy to believe that you are looking at yourself when good, satisfying feelings are present (maybe it's just a lie that you are looking during these times) but it's been impossible for me to feel that I am in my life when the feelings of fear, abandonment, loss, grief are present.  They are physically painful, so painful.  And all I want to do is run from them, protect myself from them, go to sleep and forget them.  I can understand why people want emptiness, lack of feeling, because of the pain. How do you live with uncertainty?  How do you live the ups and downs?  I keep hoping that they will go away but it doesn't seem like it will happen.  I recently had a very bad 24 hours of horrible, horrible feelings.  I thought I was going crazy and I probably was.  I finally just let them all in, waves and waves of them, I could barely breath.  And they did lessen and stop for awhile.  They are there today but at a much lower level.  I still continue to try to look and have been listening to the podcasts.  I was wrong below about only those who have found it being on these podcasts.  I'm working my way backwards and see that there are many seekers just like me out there struggling.</p><p>Thanks again for speaking with me and doing this work.</p>
