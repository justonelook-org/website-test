<p>John, I wrote to you via your email info link earlier this week.  Not sure if you received the message in that format.  My husband and I are in a crisis of sorts, 30 years of living together, many years of anger, fear, hurt, resentment.  He recently had a spiritual awakening and wrote to you about the fever breaking.  We both seemed to have reached a crisis point at the same time.  I realized that I had many things to work through that were not a direct cause of my relationship with him.  He felt some great release, fever breaking, etc but couldn't share that with me and was probably afraid of my reaction (I'm not exactly sure why he couldn't).  He needed to get away from me and the kids and went about it the wrong way as least it seemed this way at the time.  Maybe it was the right way, who knows.  Anyway it resulted in very intense feelings on my part (and not the good kind).  But the interesting part was that we both listen to your podcasts (he more than me) and he had been using my Ipod and got his fixed before he left.  I picked up my ipod and decided to listen to you while walking.  That's when I heard his email discussed on your Open House (I've confirmed that this is true with him).  That was the first time I knew specifically about it and I was sad, angry, etc.  Mostly sad that he had apparently "found it" and couldn't tell me for fear of my reaction.  I was jealous, I want to feel that way too.  But I didn't and I still don't.  I don't know how to do it.  I've tried, maybe tried too hard or not hard enough.  I know you talk about being in the wilderness but it seems that all your callers, emailers are not in the wilderness, they have "looked", they have "found".  I know this is suppose to be inspiring to those of us still looking but just once I'd like to hear from someone that is truly "not there yet", is still in the 10 years in the wilderness.  Maybe that would be discouraging to you and your listeners.</p><p>Anyway, I just wanted to share this with you.  I'll keep trying and keep exploring and maybe even come to your retreat in November.  Your message is a good one, you try so hard within the limits of our world.  Thanks for listening.</p>
