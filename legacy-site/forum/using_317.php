<p>The pain and angst that is expressed through your words is so moving. Though my circumstances are quite different, I am no stranger to the kind of fear and emotional suffering of which you speak. I wish there was something helpful I could say, but I fear there is nothing I can offer. I know exactly what you mean when you voice your frustration that it seems like those we hear in the podcasts have 'looked', they have 'found'. Well, rest assured that I feel very much that I am "not there yet." For a while now I have had an increasing sense of feeling horribly adrift, lost, a ship at sea with no compass, despite increasing efforts to find the truth, to find my own awareness and being. I try to look, and sometimes I feel like I have really seen what I am trying to find, and other times I can't tell left from right, up from down. I am always seeking a way to neutralize the fear, whenever fear strikes me and I feel I am dangling helplessly by the throat, being strangled by the hideous beast called Fear. I have yet to find anything consistent, although some part of me refuses to stop searching.</p><p>Anyway, I cannot give you any tips on how to look at yourself because I haven't the foggiest idea. But I can offer my sympathy and assurance that I too am struggling really badly. I can only hope you don't feel as hopeless and useless as I sometimes do. If I ever figure my issues out, I'll let you know how I did it.</p>
