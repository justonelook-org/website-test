<p>The fever broke and I seem to have found the fear and guilt have gone. Of course, I still have doubts, maybe I'm just in an "up" time of the season. Maybe things are clicking along nicely bio-chemically, and I'm experiencing this kind of high of a new boldness and audacity and lack of dis-ease and the feeling that someone is watching me and I'm doing something wrong and am always explaining myself in my head.  That's gone.</p><p>My question is: is it a common side effect of this to doubt your sanity, want to have your head examined, and to furiously seek an appointment with a psychotherapist? That's what I did. Met with therapist and wanted to know if I'm crazy. The therapist seems to think I'm fine, but hasn't ruled out the possibility that it could be some weird form of depression or bio-chemical change of life.</p><p>Are these doubts I'm having common? Should I hold off making any big important life decisions while I'm still under the influence of this "high" of not being afraid or fearing that I'm doing something wrong?</p><p>It all seems to be working out well, my nature feels like it's pulling me to it, and it's my personal, unique nature, absolutely unique to me, and it's sort of "calling" to me. I am watching this unfold more than taking any actions, but am proceeding very cautiously, with that doubt that maybe I'm just making this story go the way that I want the story to go.</p><p>Your thoughts?</p>
