n uncontrollable influences or patterns that come and go, in the same way that weather and physical ailments come and go. I get through them as skillfully as I can, like taking an umbrella in the rain.  I am still learning not to judge my states; that judging one state as bad only adds to my discomfort like pouring hot water on a burn; and judging another state as good sets me up for disappointment when it recedes. (Disappointment has been a major reaction in my life. I suspect it will be the last healing to occur.)</p><p>We are all doing the best we can all the time.  I hope this has been helpful.</p><p>Lera</p>
