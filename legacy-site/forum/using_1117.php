<p>Hello John,</p><p>I composed a post several weeks ago ( mid Sept) but failed to click send. After several weeks of waiting, I began to wonder why it hadn't been approved. After allot of drama being attended to by my mind about John, the looking, the stupidity of it all... it finally came to me that it might just be that I failed to summit it correctly! Turns out that was the case, but it didn't matter because I was deep into the melodrama of having spent all of August in a state of bubbly bliss with an underlying sense of life nourishing me that left such satisfaction that I was sure I had found the pot of gold at the end of the rainbow. When everything seemed to flip was when I composed the post asking for help/feedback/something! I talked about my experience with the mindfulness practice that went crazy. My only tool had turned into a neurotic thought form looping around my mind with no end in site. I finally had a little mental meltdown. But in the melting down, my attention was moved away from the stuck voice inside my head telling me repeatedly to move my attention to my breath or to what's in front of me... when that was what I was trying to get away from because it wasn't working. My attention was drawn to an old familiar emotional wave that in the past had brought with it a state of hopelessness etc., etc... I watched the wave loose energy and turn into nothing I was afraid of. My reaction to that was ...so what! My default state, which is a varying degrees of pissed off-ness, had already kicked in.</p><p>I felt sure that I had turned a corner in August, when everything started coming together. I recognized the bubbly bliss as a state but I couldn't believe the sense of being nourished was a part of that same state. It wasn't here in July, it was here in August but wasn't here in Sept! And I was pissed! It felt like I was right back where I started.</p><p>This aspect of the fear (if that is what it is) stayed with me in one degree or another through today. I had a moment of clarity earlier today but for the life of me I can't remember what was so clear! There are tidbits of things I have heard John say over the last 2 yrs. just floating through ...the feeling of not being done and the feeling of being done is two sides to the same coin...you don't get what you want with this work, you get what you need. So far it is not completely clear to me what I need...</p><p>Any feedback is welcome,</p><p>LaQuita</p>
