<div class="forum-post-template-post"><p>Hello to all who have posted to "Houston, we have a Problem". I have mostly been a lurker but am compelled to post now. Yay! I started "looking at myself" maybe about 1 1/2 years ago, not sure. About 6 months ago I did what John suggested about remembering yourself from childhood and then looking to see if the same sense of me was there now. Voila! In an instant everything fell away and I was astounded. I'm sure I made some sort of sound and then thought "wow, nothing ever REALLY happened", at least to the "permanent me". (my pet name for me and the looking). The next day I was still a little "high". I tried to read something by the mystics, a practice which I have been doing for many many years, and to do my regular 30 minute morning meditation. Whoa! It all seemed, well, unneccesary and irrelevant. Long story short, I felt a bit disoriented and confused. I decided a few weeks later to try my meditation again (watching the breath) and discovered it was VERY relaxing, enjoyable, free of tension and having a sense of "nothing is at stake" so I do believe that the daily meditation is helpful as moving attention training.</p><p>I recently re-joined a group of local practitioners who study the mystics and carry on different kinds of non-dual type self inquiry. I have a 2-year history with this group and it was the studying I did with them that led me to John via internet research.</p><p>I can't remember where I read it in one of John's talks but I discovered that while present in the group I feel like I am in a sort of insane asylum! Everything said sounds really odd. I proceeded to come home and chastise myself for being so judgmental. Luckily, this thread came up and John's words brought me back to myself.</p><p>"Seems to me that the best we can do is decide an experience is worth attention or not, and if it is not, move our focus elsewhere. And judgement of states already present strikes me as an excellent example of something that cries out to be ignored."</p><p>WOW. I hadn't noticed that I was operating on the premise that "judgmental" thoughts were bad and could/should  be gotten rid of! What a relief came over me. YES! I was judgmental and didn't need to give it any attention. Maybe in time judgmental thoughts will decrease but the main learning fro me was that I am still hooked into seeing these states as "me" and that they need attention and/or "getting rid of".</p><p>Once again, thank you, John and LeraJane and LaQuita.</p><p>Annie</p>
