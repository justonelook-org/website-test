<p>Hi, John and Carla, and all you guys, I thought I'd write and tell you about the way I spread the idea of looking around. Although this is my first write on the grass roots, I am trying very hard to spread the word around. I am a writer of songs and Poetry, and I have joined different poetry sites on which I send my Ballade style poetry around. Both myself and my wife have written articles for a site called poetry Soup. I have written very many poems and narratives about the looking and John and Carla's work. At first I was getting very few comments for these poems, although I did place in a few poetry contests with couple of these writes.</p><p>Now that I am becoming more popular on the site I have a few people actually take an interest in the looking. I am getting quite a few views for these poems now, which means people are actually reading about the looking. So who knows how many will either try it or come to this site. I notice that as I read the stuff that others put on the poetry site, that there are more unhappy people on the site than happy ones, so maybe they will get the message and do something about it.</p><p>So all you people who know how to write could do this too, they tell me that every little bit helps. I have also helped a couple of people through the looking. my cousin in England has Parkinson's disease, and the looking has been a lot of help to her, and my niece had bad troubles with fear and was under a shrink, she found the looking was very helpful too. I will do my best wherever I go to spread the word around. Anyhow this is all I want to say. I wish you all well. Peter</p>
