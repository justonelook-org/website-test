<p>About nine years ago I encountered John's work as I was looking into different approaches to self-inquiry, and I began practicing the looking very intently.  I felt entirely sure that I was seeing myself, and it came naturally.  I continued to attend "spiritual" retreats, but while others were practicing meditation, I would do the looking instead.  A couple years later, triggered by a divorce and the death of my mother, my life seemed to fall apart completely and I fell into an extended period of alcohol abuse, accompanied by intense depression and anxiety.  During this very long period of time I completely forgot to look.</p><p>Only recently, as I began to enter a different kind of recovery (the AA kind), I remembered to look again.  Now when I'm in meetings, and others pray to a higher power, I do the looking instead.  Once again it comes naturally and I've been doing it often.</p><p>I am wondering if forgetting the looking for so long is something others have experienced also.  I can't help thinking that my seven year stretch of "amnesia" might mean I never got it right the first time, and perhaps I don't have it right now.  On the other hand, I feel I am doing exactly what John has described.  Grateful for any responses.</p>
