<p>Hi Delmogazi, Jackx, John, Carla, and everyone here!</p><p>This thread really spoke to me because I experienced something very similar. I first came across John and the looking before he started calling it that, about 10 years ago. At this point I feel certain that I did the looking, and I continued to follow John as his way of describing this act developed away from the world of spiritual seeking into the much more practical thing that it is now. Everything John said about what it's like to be sane, and what it's like to experience life without the craziness generated by the fear of life, felt intuitively true and desirable to me. But even though I had done the looking repeatedly, I still felt miserable. I believe John has said the looking will cure you of the disease, but it takes a while for the symptoms to go away... Well, I felt anything *but* cured, and to be honest felt very little difference compared to before I had done the looking.</p><p>After that, I did not experience addiction myself but got into an intense relationship with an alcoholic and became deeply codependent, and like others forgot all about the looking. I also went through (a different) recovery in the Al-Anon 12 step program and my formerly actively alcoholic partner is now sober and doing great in recovery himself, but I have remained miserable in the same way as I was before the looking, even though my life circumstances have improved greatly in many ways. Recently the misery got to be such a problem that I've started flailing around for anything that might help, and came back to see what John was up to lately. A few days ago I listened to his podcast on self-directed attention, and I am starting to think this may have been the missing piece for me. I've been practicing the exercise of focusing attention on the breath, and outside of that exercise I am practicing becoming aware of what I am attending to and determining if that is useful to me or not, and at times (this is a hugely different experience for me) actually being able to decline to attend to thoughts/feelings/internal monologues that I see to be harmful.</p><p>I will be honest--for a while I felt rather disappointed and disillusioned about the looking. I always felt John and Carla sincerely believed in its effectiveness, but that for some reason it just didn't work for me. I am excited to give this approach another try with this tool to help me through as I (hopefully!) "go sane." I am very grateful to John and Carla for continuing to do this work, and for the support of this community. I hope to stick around this time, and to have good things to report.</p>
