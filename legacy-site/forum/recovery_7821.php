<p>Hi everybody, a lot has happened since my last update here. I had done the looking last April, and quickly went into recovery, without any "honeymoon." Since then it's been a long struggle trying to let go of old spiritual beliefs that I now believe not only didn't help me, but actually hurt me over the years. I have been dealing with chronic physical problems for many years, which I have suspected are psychosomatic, and since I hadn't found any relief, I got into a lot of psychological stuff digging, too, which messed me up even more. At the time it seemed that JOL and SDA couldn't possibly be enough to resolve these complicated mental and emotional traumas and difficulties, but now I finally get what John has said about there being no need to delve into all of that, because the root cause is the fear of life. So it always gets back to that one basic thing. I'm glad that during all of my periods of vacillating and doubting, I kept up pretty well with the SDA. I discovered that if I tried to stop I'd feel even worse, so that was motive enough to keep going! <img src="smilies/smile.png" alt="smily"></p><p>Anyway, I feel now that I've made a definite shift. I've finally dropped all my fruitless searching for ways to "fix" myself, and I'm just living my life, observing what comes up during recovery, learning a lot, becoming more self-reliant and focused, and actually starting to feel some genuine satisfaction with my life, even though I'm still somewhat disabled and basically housebound. I can feel myself improving, though, without any special effort on my part. I'm able to do more things than I could a couple of months ago, which is a good feeling. But the important thing is that I'm not bothered by my symptoms and limitations anymore... they seemed to have moved into the background.</p><p>I'd love to tell everybody just to give JOL a try, but I hold my peace about it. If somebody asks out of curiosity (I put a JOL banner up on my Facebook profile), I'll be happy to say a few words and then steer them towards John and Carla's website.</p>
