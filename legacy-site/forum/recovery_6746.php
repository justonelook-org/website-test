<div class="forum-post-quote"><p class="forum-post-quoted-name">delmogazi</p><p style="margin-top:0;">I do have a lingering thought that I hope could be addressed at some point. I understand that the purpose of "self-directed attention" is to help me move thoughts from one place to another. But for me there's a missing link in the logic. I think I've actually got self-directed attention down pretty well now. But then the thought comes in...what am I supposed to attend to? From watching John's videos, I've gathered that his advice is that it's just whatever is going on in the moment...life...doing the dishes...etc. But part of me rebels against that in the sense that there are an infinite number of things I could attend to in any moment, and how do I know what's right? Plus, I have a big rebelliousness since childhood against "doing the dishes", lol! I guess what I'm asking is, even if I have the power to direct my attention, how do I guide it??</p></div><p>Actually the purpose of the self-directed attention exercise has nothing to do with moving thoughts. The sole purpose of the exercise is to gain supple control over your own attention.</p><p>If you are still having trouble with the exercise and are unable to get to the count of ten reliably, you should do nothing with the thoughts that have hijacked your control, no matter whether they are worthwhile or worthless. Instead, every time you find yourself distracted, merely move your attention away from ALL thoughts and back to the sensation of the breath in your nostrils, and start counting again from 1.</p><p>On the other hand, if you have gained reliable control over your attention, with steady practice you will learn to recognize the worth or worthlessness of the individual thought forms present in your mind. The thoughts you decline to attend to will die of starvation and fall away on their own; those you deliberately feed with your attention will flourish and evolve to give more depth and detail to your understanding.</p><p>Your skill in declining to give attention to the worthless thoughts will increase over time and this will hasten their demise. And you will learn to make the most of the thought forms that are sane and self-reliant.</p><p>And when you understand  the practice clearly you will see that it is all much simpler than it seems.</p>
