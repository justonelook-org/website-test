<p>If I go skateboarding, the only thing I need to attend to is my balance and the visual aspect of riding my board. If I play the guitar, all I need to do is concentrate on the chords I'm playing and the melody I am searching for in my head. It's the same when you do the dishes, or take a walk, or are driving.</p><p>You might have thoughts come to you when you do the dishes, about how tomorrow is going to be really hard or maybe an analyst on something 'bad' that happened yesterday. These thoughts are unnecessary to attend to and come from 'the fear'. So, in that moment you can either put your attention back to the feel of your hands washing the dishes or to the sensation of your feet (one I find helpful), or to your breath. Then you have moved attention away from those neurotic, suffering thoughts, to something which is neutral and 'in the moment'.</p><p>The idea that there is a right thing to put attention on is another fearful thought, as there isn't any right thing' we should be doing or attending to.</p><p>I hope this helps.</p><p>I am also starting to practice the movement of attention exercises.</p><p>Enjoy <img src="smilies/smile.png" alt="smily"></p>
