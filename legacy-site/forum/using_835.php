<p>Fiona:</p><p>Thank you for your post.  As a psychologist I have worked with people and brought the act of looking into the process.  In some cases there have been disturbing and upsetting experiences that followed.  The risk is that these experiences are misinterpreted to be "dangerous", that they mean something, that something is happening that will hurt you, or that you will lose control, become overwhelmed, impulsively take action to escape.  This is why it is so important to have support and be related to others for whom the "fever has broken", others who can assure you that you are never effected and have never been effected by what has come into to your experience. It is clear to me that as the fear departs and that which arises in thoughts, emotions, physical sensations begin to appear as just that which is "happening", rather than a threat that a state of well being, the natural state, a direct experience of life is occurring.  The natural state is the experience of being a context of "openness".  When we are experiencing uncomfortable thoughts, feelings, or sensations as the looking does it work there is no concern and these experiences are simply part of the array of life.  In the end all there is...is life...and it is clear that we are that and that is us.  If you have looked the end is certain.  Notice the thoughts and notice they are just thoughts, rather than you thinking, this provides some space to relax.  I have found that as the recovery occurs that it is useful to do whatever will support you in just "relaxing" and allowing yourself to let your experience occur.</p><p>Stay in touch please and let us know how your doing.</p><p>Love.</p>
