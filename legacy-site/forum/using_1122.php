<p>Hi George,</p><p>First I congratulate you on finding your way here at such a young age. From an older perspective, as someone who tried a myriad of different paths before finding John, I can suggest that you may find more philosophically distracting avenues and perhaps more devotionally stimulating roads, but you will not find a straighter way to a more fulfilling and satisfying life than looking at yourself.</p><p>If you followed (not just read) John's instructions to <span class="forum-post-italic">Look at Yourself,</span> then you have not wasted your time. From here you can choose to do this or that, and also probably find that you will think to look at yourself from time to time. This process is very uncomplicated and is not dependant on what you think or feel or do after looking, not in the common way of thinking we have to follow some practice to obtain some goal.</p><p>I may not be qualified to respond to your question since I knew right away, after sampling so many others, that John was onto something true. I started following John's podcasts almost 5 years ago and I had many doubts about whether I was looking correctly. I am satisfied with the results even though I have had a long recovery period which I am still involved with on some issues. I have full confidence that those issues will resolve successfully as has so many others.</p><p>John has suggested, and I concur, that those of us who came on board before he had refined his message seem to have had a longer recovery period. Also those of us who have had more learning and practice in the realm of spirituality seem to take longer. At your age, you have an advantage on both these points.</p><p>People starting to look at themselves now also have another advantage. Lately, John has been offering insights to make the recovery period more effective: training of attention, moving of attention, and using skillful ways of dealing with life during the recovery period to reduce suffering. Do what you can to ameliorate your suffering and speed healing during the recovery period, just like you would if you had a physical injury. Stay with us, George, so you will be able to tell others yourself that, Yes. It is worth it.    Lera</p>
