<p>Hello, everybody.</p><p>Today I'd like to make a statement of joy here. For nearly 2 years I've been trying to accomplish the act that John suggests all of us to do. During all of that time I tried methods that produced in me different kind of lasting states of peace. Each time I moved on to the next approach to the looking I thought I've got it. The fear of life was snuffed out again and again. I made false reports of success on a spiritual forum. And yet I aways returned to misery and despair. But I couldn't stop.</p><p>A few weeks ago I spontaneously tried it once again. I remembered a moment from my childhood. I saw that today I am the same. I became curious of this little strange fact. I didn't believe anything already. I knew nothing will save me from Hell. I was just interested in it. So I tried this looking at this feeling of me being me a few times more. And something changed. Something I couldn't even imagine. That's why I say that this report is definite. Problems dissappeared. The war is still on but it is not about me. The simple everyday life is a marvel. There is no desire for anything to be any different than it is. When pleasurable things go away there is no desire to hold onto them. Spirituality is just spirituality. You can be spiritual. You can be non-spiritual. Life is just life.</p><p>All I want today is to share this, to give it to other people. This is... salvation, the promised land. All I want is for humanity to be happy and sane.</p><p>John, my big hug to you.</p><p>You saved my life.</p>
