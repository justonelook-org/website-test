<p>Dear Nick the Dude,</p><p>I am extremely happy to hear your success and joy. I relate completely with your history of thinking that you had it and then losing it and then being free and then being in chains again. As heart-wrenching as that melodrama is, I think it's actually very common among us folk. People are so eager to be free that they tend to get excited and prematurely proclaim a new state of being or a newfound peace or triumph. But it just doesn't work that way, sadly. Having been through this, I see more clearly the difference between how people would like to believe things are, and how they actually are.</p><p>Which leads me to my warning:</p><p>Despite how skeptical I like to be, I still want to try to offer help. Although I wish very much that this current hope and love stays with you, I think it very likely that it will wane again. But nevertheless you shouldn't lose heart even if you do fall into familiar slumps again, because from what I've been hearing you saying across the forums, I am very much convinced that you have successfully looked at yourself. Which is to say that, if there is any fruit whatsoever to be reaped from John's promise, you are <span class="forum-post-bold"><span class="forum-post-italic">guaranteed</span></span> to get it.</p><p>I cannot promise that this whole business really will save us. Few have more doubts than I. But I can reassure you that, if it does work, you're on a one-way train to awesomeness. At this point, you couldn't reverse it if you wanted to.</p><p>I hope to hear more as your journey progresses.</p>
