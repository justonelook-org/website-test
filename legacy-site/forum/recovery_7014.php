<p>Hi Roger,</p><p>I'm very sorry to hear about your wife's serious illness. I'm not a cancer survivor nor a sufferer of serious illness, but I've been looking into lifestyle choices and health recently, and came across researchers and studies about diet and cancer at YouTube. I don't know if you know this already, but there have been studies made on the effect of fasting before chemotherapy. A few days' water fast before chemo seems to mitigate the symptoms that the toxins cause in the body, and make the cancer cells more vulnerable to the therapy. Fasting selectively weakens the cancer cells' ability to cope with the toxins and strengthens the healthy cells. In this state chemotherapy seems to hit the cancer harder.</p><p>What comes to our common illness of fear of life, it seems to me that all kinds of things in life are seen as serious threats to our well being resulting in the superficial suffering you mentioned. They seem as threatening as cancer and we are alarmed all time and on defense. Hence the metaphor of "psychological auto immune disease". This is a serious condition, as we know. We also know the cure but it acts out until completely gone. Most of those things become meaningless when faced with death. But absent of imminent threat of death, other things occupy our minds. I was affected quite a lot when guy of my age I see daily, under fifty, faced the same threat from cancer. It makes you think.</p><p>I wish your wife strength and success fighting cancer, and peace of mind for you, too, Roger.</p>
