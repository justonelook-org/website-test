<p>I have been thinking about this post for quite awhile and recently have felt some things have gelled enough for me to respond.  First, I am sorry for your situation.  I have experienced serial grief in my life, most recently when my mother died within 2 weeks of my mother in laws' death, but nothing this intimate.  On the occasion of tending my mother and her death, which was perfectly natural for a 93 year old, I found that my grief was clean, or at least cleaner than in the past, more efficient.   By this I mean that the looking, over time, seems to have made my emotional states more efficient and productive.  I have less meta-emotions about my emotions.   Grief is just grief, sadness is just sadness, without reactive meta-anxiety about the grief that spirals the grief and sadness into a whole new realm.  I still get anxious, but I'm not anxious about my anxiety.  I still get sad, but I'm not depressed about my sadness, thereby avoiding a self perpetuating pattern that goes on and on.</p><p>Our mothers died two years ago, three years after I looked.   Now I feel I have some perspective on what was happening then by observing the increasing efficiency of my emotional states now.  The absence of fear allows us to respond naturally to painful events without the emotional drama of the past. It still hurts, no question, but we also have the space to process the meaning of the event and see it more wholistically, as well as be present for those we love.</p><p>I hope all is well.</p>
