<p>The problem of human misery has nothing to do with ego, its motives, its stupidity, its brilliance, or its persistence, and the solution has nothing to do with gaining anything. The actual problem is a deeply hidden false belief about our nature and our relationship to life, that is dissolved when the beam of conscious attention is brought to touch our actual nature by looking inward and striving to get a momentary taste of what it is to be just you. It is, so far as I can tell, actually not possible to <span class="forum-post-italic">sit </span>there, and any peace that might appear while in the act of looking is entirely coincidental.</p><p>The belief that what must happen for me to be free of the feeling of being trapped in my life is for some new <span class="forum-post-italic">experience</span> of realization or freedom to happen is a big part of what makes it so difficult to see what is so clearly in our face at all times. The idea that there is something wrong with my life, with the content of my conscious mind, with my motives or my understanding, with my emotions, my attractions, my aversions, etc., is just wrong. This life, with all of its incomprehensible wild wonder, turns out actually to <span class="forum-post-italic">already be</span> the self-realization we seek.</p><p>But there is no need for you to believe this, or anything else I may say about these matters. If you will just try with all your heart to get just a split second of contact with what it actually feels like to be just <span class="forum-post-bold"><span class="forum-post-italic">you</span></span>, all else will be revealed to you naturally, over time.</p><p>Please keep in touch. Forgive me for taking so long to respond.</p><p>in love,</p><p>John</p>
