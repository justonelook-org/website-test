<p>Dear John and Carla,</p><p>Hi both, hope you are well, and my sins are I am a taxi driver/gambler. I started spiritual practice and self enquiry just over a year ago including Holosync meditation and reading a number of different books including Maharaj and Maharshi and various Advaita related material.  Of course in the early months I guess it was all, When am I going to get something from this?  and the expectation of some results which as we know is just the 'ego' doing what it does, and the million questions that arise such as, Should I try to find a Guru? Am I doing this right? Am I changing? etc, etc.</p><p>Watching your 'The Last Resort' on YouTube a few nights ago I really felt like I had taken a quantum leap in understanding because you put it across so clearly and I thought to myself, Of course I Am here. I have always been here. All the pain, all the pleasure, every experience my whole life I AM HERE and it seemed so obvious. In the 3 or 4 days since then I have just tried to sit in that 'me-ness' whenever I can and I feel peace. My meditations are so much more enjoyable as I can just sit in that presence. Of course the ego is still comes back in every chance it gets.</p><p>I realise also that it's the ego that is also feeling like it has found something, some new joy or pleasure, and it is staking claim to it somewhat, I guess I should just let the ego do as it will and ignore it? So John, I guess what I want to ask is, What now? (bloody ego again!) Will this sense of 'me-ness' continue, become all consuming until total realisation? I guess I should just let things be.</p><p>Anyway thank you John so much for your message and I hope you can reply and we can meet someday.</p><p>Love and Respect,</p><p>J.S. (UK)</p>
