<p>That which we call the fear of life is the base context within which the personal psychology arises. So far as we can tell it is silent, apart from its effects, and can neither be felt nor related to in any way other than through its effects.</p><p>It may or may not produce a palpable experience of being afraid of life, but I suspect such experience would be stifled by the operation of the psychology anyway. I never had any sense at all that I was afraid of life itself, only of its events.</p><p>We arrived at this theory by the normal course of observation and consideration of our personal experience and of what we see in others. For more, you might want to read the ebook The Radical Act of Inward Looking, which was written by three mental health professionals, and is availble free of charge on our website:</p><p><a href="http://www.riverganga.org/go/radicalactpage">www.riverganga.org/go/radicalactpage</a></p>
