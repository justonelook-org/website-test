<p>Hello!</p><p>I am musing, and since we don't have a few comfy chairs and a coffee pot on the go and all the interesting people on these boards to share that with, thought I would put it here for conversation...thinking out loud.</p><p>My personal sense of the fear of life is that of constant vigilance. I have been aware of this in my life since I was very young. It manifested as anxiety about punctuality, appearance, dutifulness. Now it is constant vigilance that myself and my kids, animals, partner are well-fed, happy, warm, growing, safe etc...So I am always busy and always watchful. I see the fear of life manifest differently in others. In doing the looking I notice two things - a compassion for us (humanity), and a great sadness. Sometimes this sadness is very painful to me. I imagine this is part of the recovery, this poignant recognition of how we suffer and inflict suffering on other living things. And yet, I know, our reality is untouched by this.</p><p>OK, so back to the looking...</p><p>Best wishes,</p><p>Emma ~</p>
