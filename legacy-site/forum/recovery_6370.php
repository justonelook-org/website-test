<p>Re: Recovery</p><p>I've been in this work for 6 years now. The first 5 years seem like a cakewalk compared to the last year. My truck died, I lost both of my jobs, I went broke, I lost my physical possessions, I went homeless and I fell in love. Despite these perceived complications my day-to-day functioning level is very high. I've found long distance running and IPA's useful for alleviating the nearly constant barrage of uncomfortable emotional states I've been experiencing. My skill level with attention has reached an acute level, which has made the situation both better and worse. Better because I'm able to easily drop or look past useless thoughts, and worse because I now FEEL my feelings without any form of buffering. I often wish for some hiding place from this body: a distraction or identification but looking away is no longer available. Through this work, I've directly accessed a vastly increased level of capability in adapting to, and making use of, adverse conditions.</p><p>It's actually been a wonderful year.</p><p>Below is an excerpt from a letter I wrote to share with the people close to me that have looked.</p><p><span class="forum-post-italic">I found that as my habitual reactions to emotional states disappeared I began to enter into a new, raw, and much more immediate emotional experience. In this place I am utterly naked; the thoughts that I previously engaged with as tools for navigating, understanding and labeling my emotions have either been rendered transparent or have departed entirely. What I'm left with is an ocean of raw, unmitigated emotional experience. This work has profoundly affected the sensitivity of my body; I often feel that it [the body] is processing an incredible strain; that it's the theater for an ordeal that could shatter it completely, yet I feel an unusual sense of love and joy permeating the discomfort. </span></p><p>- Richard</p>
