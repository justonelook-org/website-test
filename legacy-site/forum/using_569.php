<p>Lately...</p><p>Spirituality is dropping away. It is just a hobby, though one that I thought could save me from unhappiness. I thought that if I try to be like these guys, try to see life like them, understand life like them, that I'd be happy. And I can't say that nothing has been happening, cos it has. I've been changing. Though in some ways I'm more miserable than I was before, I think it is in the realising that 'nah, this stuff ain't doing it for me.'</p><p>A big, I think, crutch in all this is that I have found something that I think is working, though in some strange hardly seen way. It comes from the suggestion of a couple named John and Carla Sherman. they too, were in the spiritual business. I think I am now experiencing an escape from the spiritual ghetto - ghetto in the terms that it is a unique faction in life trying to tackle the same dis-satisfaction that is felt in our lives by taking up a certain perspective, or approach. But what I've found is that it doesn't work, or at least, if it does actually work, it is by chance, and for very few.</p><p>What John speaks about, and suggests, is something somehow different from all that (If you are interested, read here thefearoflife.org or riverganga.org). His asking of me to look at myself is something that doesn't require anything, any understandings, or any idea of 'spirituality'. I do not call looking at myself 'spiritual'. Or if I do I would not like to call it that. Because it isn't. It doesn't require any understandings. It doesn't require me to change at all...And furthermore...besides that..I think it is actually doing something...the personness of me...the personality that is me..that's what i look at.</p><p>How do i think it's effecting me? I don't know, but i think it's related...somehow,,in some what seems like a distant way, i am beginning to feel a subtle resonance with my life. as if, in some way that i don't think i am able to understand, that i am liking and living my life. it sounds weird maybe. In some way that i can't describe my life is becoming quite intimate in me. that kinda says it... Anyway.. i thought I'd tell you..cos I've been going through some rough times lately...k...love Tys</p>
