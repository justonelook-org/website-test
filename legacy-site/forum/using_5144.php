<p>This business of the relationship between the brain and the mind is very interesting, and I have been following the developments in that area as closely as time permits.</p><p>I am certain that there is a deeply symbiotic connection between the mind and the brain. First, it is obvious that the amazing power and complexity of the human mind has arisen only because of the sheer size and complexity of the brain upon which it depends for sensation and computational power.</p><p>I have seen reports from recent research, mostly overshadowed by the current fascination with matters material, that have demonstrated that activity of the mind often also gives rise to physical changes in the brain.</p><p>See this article from the Wall Street Journal for some validation of this claim.</p><p><a href="http://online.wsj.com/articles/SB116915058061980596">http://online.wsj.com/articles/SB116915058061980596</a></p><p>(I have seen much more about this, and if I were writing a scholarly paper here, I would have to dig into my saved research to provide more authority for this claim, but I hope you will take my word for it. If not, please at least consult with the great god google.)</p><p>But back to your question...  I know that there have been genetic markers discovered that seem to be associated with OCD, and this is great news since it may well lead to material therapy for this disease. But, no matter whether it does or not, it is certain that a person afflicted with it is also afflicted with the fear of life, which can only exacerbate the misery and cripple all effort on the part of the afflicted person to have a satisfactory relationship with the life they have been given.</p><p>Conversely, it is also clear that freeing the mind from the fear of life would allow the person to engage with the actual nature of their life and their condition -- in a manner that might well produce satisfaction.</p><p>After all, we are all stuck with the brain we have been given, no matter how it is 