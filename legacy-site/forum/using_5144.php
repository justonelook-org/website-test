<p>Hi Matt, in this area (genes - brain function), there is not much secured knowledge. My view regarding heritability of mental disorders is that there is something like a family curse, a specific way a family implements the fear of life. This represents as a thick, unconsious fog that manifests in rules and traditions, in pride and shame, and creates family secrets and crimes. Often, a new generation tries to break free from this fear based set of rules and regulations, tries to crack the heritage from the ancestry. However, what starts as a sincere attempt to become free ends as just another flavor, a variation of the family curse. (A similar view could apply to communities, societies, even nations.)</p><p>Besides family connections, there is also a genetic similarity within the family. However, how genes determines the organisation and functioning of the brain is very little understood. It is clear that textbook examples of simple monogenetic diseases (a disease that arises because of a defect in a single gene) like color blindness are not representative as a general mechanism  - as you also mention. I like your view of family-specific subceptibilty genes that become modified, genetically by mutation (rare), or epigenetically by modifying the packaging of the gene (frequently), based on the fear.</p><p>The fear of life makes the mind more rigid, as it clings to "proven and established" strategies of life, even to lies and self deception. Without the fear based context, the mind becomes more free and flexible. This must also reflect how active different genes are. The epigenetic modifications mentioned above represent the most straightforward and fast mechanism how to change gene activities. Bruno</p>
