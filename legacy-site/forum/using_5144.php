<p>Hi, it appears that the genetic predisposition for many diseases is less than previously anticipated. Huge efforts have been undertaken in so called genome wide association studies that aim finding a link between diseases and genetic variations using a large number of patients and healthy people as controls, with somewhat disappointing results. Rather, it appears that epigenetic changes that alter the expression state of genes but do not affect the sequence of the genes play an important role. This is influenced already in early pregnancy eg by experiences of mother. Such changes are reversible, which is good news, as this means for example that the brain can change to more healthy states. Bruno</p>
