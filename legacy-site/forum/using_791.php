<p>Chris, you have reached a nice plateau, and states like you have reached feel a lot better than being in the swamp.  For me, however, born and bred of the work ethic, I committed to and have practiced the looking in fair weather and foul weather, in and out of the swamp of misery and suffering.  My thinking has been that if I do it even in the worst of times, I will accomplish what I set out to do, which is to look when it occurs to me to do so.  The carrot at the end of the stick is the belief that this will cure that unseen force demanding that I pay attention because the other shoe is soon to drop.</p><p>In addition, I would report that there is an inertia with a solid effort of looking that will carry me for a period of time -- several hours if I'm lucky -- characterized by a sense of impregnability.  Whether that's self-delusional or not is beside the point, because it keeps me going with what I have thought of as an experiment.  Now I know it to be an experiment that works; however, I still like to think of it with a fresh mind and won't discard the experiment notion.  Also, it may be that we are all pioneers in an undertaking that will one day be a generally accepted practice.  I'd rather be in the vanguard than the last to know what cures misery and suffering.</p><p>As far as meditation is concerned, I think we all know how unruly the mind can be and how discipline of any kind can be of benefit to, as you put it, sharpen the beam of your attention.  I think it might pay to be aware that any practice, even the looking, may swallow the practitioner.  trimpi</p>
