<p>So I think I'm on a pretty good course now with the looking. Doing it as often as it occurs to me and it feels good, it feels like I've moved to a more simplified looking than earlier where I was frustrated with lack of "touching the nature", if you will - probably because trying to fit it in with older understandings (Nisargadatta / Ramana...).</p><p>But now the looking seems easy all of a sudden. And it feels good, really good - not 'blissed out party time', but you know, just really nice. With this, I mean the actual looking - the moment when I touch my nature with the beam of my attention without the intermediate of thought. It actually is a very nice feeling... Does this resonate with you guys or am I looking at something else?</p><p>My life still seems as it is, fear peculating here and there--but hopefully, it will wear off soon.</p><p>Also, and again - the looking feels very easy. That is, I can after a day of activity (or during) do the looking directly without any slowing down or "getting into the mode" process, which I thought was necessary before. This to me is in contrast to Mindfulness Meditation which I feel is harder, takes more effort (and dedication) - kind of like going to the gym. I know it's good for me and will produce positive effects, but it takes some determination and effort to do.</p><p>So do you think Mindfulness Meditation is necessary for the looking, or is it just a good complementary tool for sharpening the beam of attention (which I know is its purpose in combination with the looking, as mentioned by John). I feel like a can do the looking well without a set routine of Mindfulness Meditation (which I do sporadically though, mostly because I have the notion in the back of my mind that it will help the looking). Do you feel it's more of a necessity though and that an everyday routine of Mindfulness will be very beneficial, if not crucial, to getting rid of the fear when doing the looking?</p><p>Thanks a lot, and hope everyone is good!</p><p>Chris</p>
