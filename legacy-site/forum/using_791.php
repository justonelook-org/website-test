<p>Hi Chris,</p><p>You may find the mindfulness meditation helpful as a way to practice focusing your attention. It certainly has that practical use for some people however it is not necessary to look at yourself.</p><p>You mention that lately you feel good when you look at yourself. The nice feeling could be described as a side effect or something that occurs as a result of the looking. The actual act of the looking takes care of getting rid of the fear, possibly even the first time you try it with intense conviction. The feelings that arise whether they are pleasant or not are not the point of the looking.</p><p>Keep looking at yourself in whatever way it occurs to you as often as it occurs to you. How else could you do it? ;)</p><p>David</p>
