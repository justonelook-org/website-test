<div class="forum-post-template-post"><div class="forum-post-quote"><p class="forum-post-quoted-name">Kafkaesque</p><p style="margin-top:0;">So do you think Mindfulness Meditation is necessary for the looking, or is it just a good complimentary tool for sharpening the beam of attention (which I know is its purpose in combination with the looking, as mentioned by John). I feel like a can do the looking well without a set routine of Mindfulness Meditation (which I do sporadically though, mostly because I have the notion in the back of my mind that it will help the looking). Do you feel it's more of a necessity though and that an everyday routine of Mindfulness will be very beneficial, if not crucial, to getting rid of the fear when doing the looking?</p><p>Chris</p></div><p>Dear Chris:</p><p>Years ago, I started doing a type of Mindfulness Meditation. I could never get into counting my breath so I sat in meditation with the idea of watching or noticing a particular thing. It could be listening for sounds as vibration and not making a story about them. As one sound came, noticing it and then going back to listening again.  Or feeling a sensation in my body, staying with it (feeling it without thinking about it) as long as it lasted, and then feeling another sensation or part of my body. Another time, I might choose to notice the tense of thoughts that appeared. (Was the thought about remembering the past or planning for the future?) The common point was to focus on something present in my experience at the exclusion of other things. If I was listening, I would ignore thoughts, sensations, etc. I continued my practice when I found John and started looking at myself. About 9 months ago I stopped meditating except to sit with others to help them learn to meditate. How did I know when to stop meditating regularly? Hard to say. I just knew. At some point I just felt that life was an ongoing moving meditation. I still do the looking whenever I think of it.</p><p>To answer your question, I think I was in such a state of fear that this mindfulness meditation was necessary to train myself to focus my attention. I think it is great preparation for looking, but may not be necessary for everyone. My guess is that since you can do the looking well you are past the stage where focusing your attention in daily meditation practice is necessary. John says that the fear of life goes very early and the recovery is what takes time. Some people who enjoy meditation continue with it during recovery, as I did for a long time, because it aids the mind and body and makes the life more balanced.</p>
