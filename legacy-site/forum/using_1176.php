<p>Hi, everybody.</p><p>I've been missing from the forums for some time but now I'd like to make a certain conclusion to my practice of the looking. I've been "practising" for about 3 years now.</p><p>Most of that time I've been trying to accomplish this act and as I said many times before I've had my share of completely illusory ups and downs. I wanted happiness and satistfaction badly.</p><p>Well, I tried many-many times to look at myself. But happiness on a permanent basis did not come. So I decided to leave this practice completely and I now see that it was a right decision.</p><p>You see, it seems to me that I'm sick in a way. I have a very hyperactive mind. Most of the time it is almost impossible for me to turn my attention away from thoughts and just do something without trying to understand it, control it or whatever. For years I've been only thinking about this act more or less subtly. There was no conscious recognition of myself. It is plain to me now but it was not so before. The hyperactive human mind is fond of playing with itself and ultimately deceiving itself.</p><p>Well, I finally managed to get a taste of "me". I consider it a kind of good luck actually. I can't even do it again but it seems unnecessary. I won't burden you with the details here but I'd like to tell what this "seeing" did to me because it is really surprising.</p><p>It is really hard to explain what happens and I now understand why John struggled for 13 years to do it. It's really tempting to talk about psychology and metaphysics here but...</p><p>It did not make me happy and content. I think it gave me <span class="forum-post-italic">freedom of choice</span> in my life.</p><p>Before the "seeing" I felt like I was absent from my life. I was a passive, miserable, almost non-existent element in the whole thing. I felt like I was a passenger on an unstoppable train going straight into Hell. Whatever I tried to help myself only made the ride worse.</p><p>Now I feel like I can leave this train. I feel that I can actually do things and make a change in my own life. I now see that it is futile to wait for happiness. It will never come by itself.</p><p>That's my outlook on life now and I am grateful to John and Carla for that.</p><p>To sum this all up, my keywords for the result of this work are <span class="forum-post-italic">freedom of choice</span> and <span class="forum-post-italic">responsibility</span>.</p>
