<div class="forum-post-template-post"><div class="forum-post-quote"><p class="forum-post-quoted-name">ElDuderino</p><p style="margin-top:0;">The hyperactive human mind is fond of playing with itself and ultimately deceiving itself.</p></div><p>Dirty jokes aside, I too came to the conclusion that the human mind, when left to its own accord, has an immense talent for self-deception. For quite some time now the idea that the only thing that happens once you consciously look at yourself is that your break the bond between consciousness/attention and the activity of the mind. Up until that point, attention is permanently stuck on the great show that is mental activity, like a baby glued to a tv screen, eyes hopping from one interesting, moving, colorful object to the next. Once that tie is broken, and it really only takes one look as far as I know, the show just goes on in its own space, but with the option of not having to look at it. As far as freedom of choice goes, that to me seems to be the only choice we have : not going there. I can get engrossed in the mental products, but it's more an old habit than an actual response to life, and I can snap out of it with more and more ease as time goes by and the hyperactivity loses momentum.</p><div class="forum-post-quote"><p class="forum-post-quoted-name">ElDuderino</p><p style="margin-top:0;">To sum this all up, my keywords for the result of this work are freedom of choice and responsibility.</p></div><p>I already mentioned this in another post, but for me, responsibility is now a given instead of a choice. Running away from life and not taking responsibility is the old and ancient way of the mind to deal with life by not dealing with it, self-deception at its finest, the great art of denial. But it's just what we're taught as children of children, and it's this ancient art that is now slowly destroying the world. But for me, apart from having the choice not to go there, what now rules my life is rather a freedom FROM choice. I'm being more metaphysical than I'd like to be right now, but more and more it seems to me that the word choice has no meaning, life doesn't present choices, mind does, and when you live with attention freed from the ties with mind activity, there is seldom more than one "choice" to choose from. At least that is my present experience, whenever I have to choose between options it occurs to me time after time that it's the mind who's weighing pros and cons, and going into scheming mode to fool life into doing what "I" like. When I don't indulge in these trains of thought, life goes on through me, and in retrospect I can say : there was a problem, I solved it by choosing to do such and such - but always in retrospect. But enough with the meta.</p><p>Thank you for posting this, it helped me to form the words and ideas for what I just wrote. I hope this is of any help to someone.</p><p>Wouter</p>
