<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">FluoSmurf &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1362055520-->Thursday, February 28, 2013 at 04:45</span> 
