<p>Dear GeoffreyLevens,</p><p>In my experience, if possible, it's better to keep things simple as much as possible. It is impossible for me to know at what moment in your life and in your search you are, but when in doubt, just find what you call me. What happens afterwards is not important and, in fact, it's beyond our conscious control. It's like looking into your own eyes, like touching your deepest core, your personal center, you. It is at the same time the subject and the object, if you want to talk about such distinctions, but they are irrelevant. It's just like tasting a sip of tea or tasting a cupcake. You have a taste, then it goes on its merry way. Rinse and repeat, when and if needed.</p><p>For a long time, I wondered what happens or should happen after I look at myself. My personal impression is that it varies. Sometimes it will feel really good. Some other times it feels like an almighty presence everywhere. Sometimes like an all pervading veil of life. Sometimes like falling in a huge void. Sometimes like going through a dark room, not being able to find any solid point. All these experiences are ultimately not important or, as John puts it, besides the point. Hope this is helpful to you.</p>
