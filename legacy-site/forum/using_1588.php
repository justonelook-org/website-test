<p>Hi!</p><p>I don't know your situation, since you've been involved in similar things for 10 years and don't know if this answers your question at all, but here's my experience with this problem. I hope it helps.</p><p>When I was starting out I had a similar problem. I was too analytical and approached this from a philosophical/spiritual perspective. It lead to theorizing and especially at first trying to fit the looking in with other theories. A big problem was how John in older texts used to say to look for yourself and nowadays it's all about feeling the "sense of me", and I was trying to fit them together. I was trying to judge if they were the same thing or different things, because as you say a sensation is an object not the subject which I understood myself to be after reading spiritual and philosophical theories (and probably still do). However, after some time I kinda realized that this is not about the theory but rather the looking itself (also it seemed that theorizing just didn't work). Just look for yourself. There's no confusion if you don't try to find the answer by theorizing. For example I'd go about "answering" your question to myself by feeling the "sense of me" and then seeing if it feels separate from me, and if so, what am I, can I find myself. Or if I think I'm the subject, then what is it in actuality? What is it like to be the subject? Can I turn my attention on the subject? What is the relationship with the "sense of me"? And even "how do I know I am the subject and can't look at myself?" (seeing if there is something to it or is it just an empty belief absorbed from reading spiritual teachings). All in a very experimental way, not theorizing about it. And if the same question arises again, instead of thinking about it just look again. However, remember that John has said that just one look is enough. So all this extra experimental looking may be pointless, but I find it interesting anyway.</p><p>Also if you think what this all is about is that John found an act which seems to work and is trying to get others to also do it. All of the theory is just speculation over it (what seems to be the case). This isn't science. So I don't think it's a good idea to take the theory too seriously. Just do the act and hope for the best.</p><p>To John:</p><p>A question popped into my head while writing this reply: Has the reason you're trying to separate this from spirituality something to do with that maybe self-inquiry (and other traditions) had the act of looking, but it's so heavily buried under theory that people get stuck theorizing instead of looking? Maybe they once were useful but no longer are? All talk and no action?</p>
