<p>George,</p><p>I took John's words about just one look literally without any problems right there in the beginning, trusting that this is not another meditation practice. I tried the looking without any sense of success, and addressed this in one of the Open House meetings. I felt that I can con myself into thinking that I've looked when in fact I haven't. John replied that the capacity of putting oneself down at every opportunity, even with looking at oneself, is quite surprising, or something to that effect, as far I remember. I was chastised and later became convinced that I had done the deed.</p><p>These days I'm sure of it. I was very relaxed about it after the initial hick-up. I tried looking when it occurred to me, for a couple of months, perhaps, then dropped it. I felt a faint, nice sense, a very brief and pleasant feeling, a few times when looking. I hardly look anymore. I have no interest in it. I'm not concerned about me, except as an abstract philosophical question (and as a mechanical reflex of inferiority complex). It's been nearly two years. I still suffer most of the time, but I'm able to let it roll more easily. I don't get so hung about it as I used to. The mindfulness practice of attention mastering is helpful. Keep at it, is my advice, for what it's worth.</p><p>Seppo</p>
