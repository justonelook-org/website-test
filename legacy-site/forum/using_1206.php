<div class="forum-post-template-post"><p>Hello George,</p><p>what you wrote got stuck in my mind and an analogy occurred to me. When an infant first learns to walk a new world opens up. There is not much understanding involved, just a lot of trying. Same thing with learning to ride a bike - I remember being very determined to learn how to ride that damn thing without training wheels and eventually succeeded to master the basics, falling quite a lot in the process. Once I was able to ride it, there was enjoyment and amazement, but also the realization that there is so much more to it than not falling. All around me there were indications that I had just begun to discover the possibilities (this was right in the middle of the BMX craze in the eighties, just saying) and that there were exceptional people who had mastered exceptional skills with a bike.</p><p>It seems to me that the same is true for seeing yourself. The me-ness is all that is important, and very ordinary because it's always there and always the same. However, how that manifests itself in your actions and thoughts and words is in my opinion an endless journey of evolution and discovery. The new understanding that arises about life in the world as seen from this new perspective, how it changes and destroys the old beliefs, and how this evolution becomes an important part of life itself, is a lesson that won't stop until my body stops working. But all that began with that simple recognition of me. Once you had a single glimpse of this simple fact, life slowly starts to have an entirely different flavor.</p><p>This is my experience at least.</p><p>Wouter</p>
