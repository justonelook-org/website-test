<p>I would just let her be now, Franco. It is not uncommon for folks to overlook the smallness of what they are looking for even though they have actually succeeded in touching it. The heart of us is such a still, small sensation that it is common not to recognize it for what it is even when you have looked directly at it (at yourself.)</p><p>Her forceful dismissal of any suggestion that it is possible to do this might be itself evidence that she has succeeded.</p><p>If she brings it up again on her own, you might either send her to <a href="www.justonelook.org">www.justonelook.org</a> and ask her to read that page, or even print it and give it to her to read on her own "“ again, only if <span class="forum-post-underline">she</span> brings it up.</p><p>Trust the simplicity of the looking.</p><p>John</p>
