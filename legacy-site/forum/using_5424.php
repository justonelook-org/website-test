<p>I tried to explain the inward looking to a close friend. I walked her through John's way of doing things, and later gave her links, which she read, but she still says she doesn't "get it." She says, "How can I put my attention on me? I'm always experiencing me, because I am me!" So I said, "Look at that painting on the wall. If you have your attention on the painting, is your attention on you, or on the painting?" She said, "It's on the painting. But I'm the one with the attention. The 'me' is always there because I'm me. It's never not there. So how can I put my attention on me, when it's always there?"</p><p>I talked with her for quite some time but gave up. I encouraged her to post a question here, but later I realized she probably wouldn't do it herself, so I thought I'd ask for her. Does anyone have any advice on how to help her "get it"?</p>
