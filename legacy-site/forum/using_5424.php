<p>I'm experiencing something similar with a significant other, who does have depression (suicidal even, sometimes). And I know it would help her, but she's in her early twenties and I'm 37. I don't know if the age thing makes a difference in being able to figure out that the looking might be more important than all the forms and sensations, and the getting of things (money, power, stability, etc) "out there" in the world, or not.</p><p>At least for me, my life experience has shown me that there's no real true sanity or "happiness" to be found out there in the world of people , forms, power, money, sex, etc. Though having some of those things are nice and beneficial, ultimately it won't "do it for me", just talking about myself here.</p><p>I've tried to introduce the Looking to my SO, and so far (kind of as expected) she did not take to it, or "get it", if you will. And all the while she's suffering from depression and other things, not realizing the potential gold I'm offering her. Hope I'm not putting my foot in my mouth too soon, because I'm relatively new to the Looking. However I will say the looking is a real thing, there is a "me" here. I know that now, and I feel that now. And it's comforting and settling to turn my attention to ME.</p><p>That said, trying to pass on this message to my SO is akin to (silly metaphore time) trying to show your cat the automatic can opener (which already has a can of tuna in it) and all the cat has to do is press the button and the automatic can opener will open the can and reveal all the yummy tuna. But the cat just sits there curiously, and looks at the can opener, then gets distracted and moves on to something else of more interest.</p><p>Yes, it's a little frustrating. It's not my goal in life to show her this Looking. I won't die if she never "gets it". Part of me doesn't care. However I want to do good for her, and I do and will continue to offer this information to her, because I care about her. From another angle, it's an interesting project (for me) to find a way to somehow get the mail pass the castle gates, and get it to the person inside there.</p><p>Only way I can see it working at this point is just leave it at their doorstep, and they'll pick it up and look at it, when the mood or need arises in them. Let them discover it, rather than you forcing it upon them.</p><p>Best Wishes</p>
