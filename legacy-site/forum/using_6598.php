<p>Again and again complaints about the unwell majority of the human community is expressed in one way or another in this forum. It seems like an air of subtle discontent with the the outside is breathed into many postings. Not that these complaints are not justified, they might as well be, and maybe some I even see as 'complaints' because that's how I interpret them. I'm pretty fed up with the fear of life myself, in me as well as in others - makes little difference really.</p><p>My solution, except to go bitch about it in the forums, is to ask <span class="forum-post-underline">everyone</span> to look. John has made it easy for us. I feel reluctant to stay in any sort of relationship for very long if my counterpart has not looked. I ask friends, family, colleagues, lovers, classmates, even first dates and other loose brief acquaintances to look at themselves left and right (anyone if I'm the mood really... I'll ask the pizza guy, no joke). I practice bringing this to others in one way or another every week, be it through writing, talking or whatever. As I do it more I get more effective at it, more tasteful, tactful and elegant at it, making it more fun and trying to reduce the intrusiveness, and to wove it in as a something natural in a normal everyday conversation, and not something magical or evangelical. Every time I do it I learn more about it and get a little bit better.</p><p>So I need to ask some other lookers, many of whom are also complaining about the state of humanity..  You guys:</p><p>How do you share the looking with others, and who do you share it with?</p><p>What is keeping you from working with the looking in others?</p><p>How are you planning on working with the looking with others in the future?</p><p>What are your experiences of sharing the looking so far?</p><p>To me these questions are the most important out of the all confused ponderings my recovering mind is throwing at me.</p><p>Do you agree this is not unimportant?</p>
