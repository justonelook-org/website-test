<p>I love your approach, Roed_.   I'm not as evangelical as you, but I have told a good number of people, 30, about the looking over the last 4 years.  I keep track because I like to check in on them over time. I work with kids with learning disabilities and many are on the autism spectrum.  I do a lot of stress reduction techniques and throw the looking in whenever I can as a way to 'reduce stress'.  I've seen some rather remarkable changes in some kids (I work with elementary age thru late teens) and see many kids who have left our school and visit later doing very well.  It's something you sort of recognize, a clearness in the eyes......I can't say the looking is responsible for all the changes, but as John has mentioned, children seem to recover faster than adults.</p><p>Most of the people I surround myself with have done the looking and it's been fun to see the changes.....although my wife thinks it's ridiculous, I still see good things happening for her.  This thing is so long that we forget and not notice the changes over the years, but it's fun to see people change over time and mark my own changes against theirs.</p><p>Thanks for the inspiration.</p>
