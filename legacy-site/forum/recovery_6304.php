<p>Thanks, roed! I agree the ability to direct attention is useful, and I apply it often. However, deciding not to attend to sensations and experiences in the body can be the perfect excuse of a fear-based mind to avoid issues that actually benefit from or need attention. So it is actually the opposite of what you say: a fear-based mind tends to avoid dealing with all life brings, that is, all sensations that arise in the body. Instead, a fear-based mind applies selection and censorship. This happens involuntarily. Therefore, I tried to do the opposite, in a self-reliant way. Instead of declining to attend to certain sensations, I embrace them all. However, importantly, I do not identify with them. I just witness and acknowledge whatever arises, whatever happens in me, like a host would welcome guests that come and go. This is how I decided to use my attention.</p><p>As a result, the limbo state that I (and apparently many others) experienced is now gone. I feel more energy. So it works for me. I will see where this lead to.</p>
