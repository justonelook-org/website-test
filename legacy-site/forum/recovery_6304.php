<p>roed, and even if there is a conflict, it is fine.  The working model seems that mechanisms in us are diseased because they evolved in the context of the fear. And that one needs to starve them, deprive them of our attention, to give room to sane mechanisms that arise. Formally, from the outside, this concept makes sense. However, it brings the tendency to sort what is in us into old-fear based, and new, sane. And this promotes suppression of shadows, and can prolong recovery. I decided to embrace everything that arises in me. There is no need to selectively deprive certain sensations of our attention, because they were born in a context of fear. After the looking, there is nothing to be feared - so why not deal with whatever is in us? Of course, I am not talking about intellectualising, or identifying with concepts, but welcome the sensation before they manifest as language or concepts.</p>
