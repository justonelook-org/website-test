<p>Hi, it has been 2 years since I first did the looking. I do see a lot of changes. Lifelong issues have been dissolved. For example, social fear is much reduced, maybe by 80%. And I am grateful for that. Ocassionally (but much less frequently), I still have strong but vague feelings of impeding doom, of having failed at something. However, when these states arise, there are initially no thought patterns associated to them. This feeling, or whatever I could name it, is present before thoughts. In such moments, directing attention to breathing helps to shorten these periods, before the mind kicks in to amplify and prolong these states. So, it seems to me that not everything happens in the mind. There is a deeper level where fear exists (in the body, in deeper levels of the brain, maybe the amygdala) before it rises to the mind. Can anyone relate to that?</p>
