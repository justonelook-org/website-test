<p>During the recovery period, lifelong mechanisms of dealing with a seemingly threatening life become meaningless because the underlying cause, the fear of life, has fallen away. However, the replacement with new authentic ways to deal with sensations takes time. During this period, it is the old mechanisms that are still available and operate despite their inefficiency. This creates stress. It is my experience that more can be done than just controlling one's attention. So I agree with your assessment that accelerating the recovery period is an important topic. Controlling the attention is certainly the core to efficiently dealing with this period. However, there is no need to abandon all other methods of healing that humans have found. What is useful depends on each individual life history and it is hard to generalize. What helps me is to establish a healthy life style, such as exercising, reducing alcohol consumption, being in nature, not engaging in other peoples drama and stay true to myself. Other things that I found during my lifelong quest for an escape prior to the looking are now easier to implement.</p><p>Regarding the origin of the fear of life, I think the view that it originates at birth is a bit vague. It is finally accepted by science that pregnancy is a crucial period that influences the health of a human. For example, the loss of a twin during pregnancy is a traumatic experience for the surviving fetus. Similarly, traumatised mothers and fathers (for example due to war, sexual abuse) cannot establish healthy relationships with babies. Such traumatic experiences (overwhelming threats) are secluded in deeper areas of the brain, not accessible by the conscious mind. After the looking, therapies aimed at releasing the traumatic energies become more efficient.</p>
