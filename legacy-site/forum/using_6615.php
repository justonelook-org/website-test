<p>'Looking' at yourself is more real than anything you see, hear, touch, think or anything else as anything perceived is going to be bogus on some level.  Noticing what you really are by 'looking' at yourself is as real as it gets.   Do the looking and let everything unravel, rewire and surprise you.  Chill out and let it happen and don't despair when life doesn't fit into your conditioned concept of nirvana.  Concepts are bogus.  Looking is a glimpse of truth.</p><p>The above is a departure from my thinking during my first 6 months of recovery.  I thought I had to work hard at this.  Bogus, I tell you.  Of course, many of you advised me of this before and I disagreed, pointing to differences in personality and other mumbo jumbo.  Fooey.  Just look.  That is all.</p>
