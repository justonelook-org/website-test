<p>I first came across John's work through a non-dual website in which he was being interviewed. I was intrigued by the clear and precise communication and absence of abstract concepts.</p><p>My reaction to the work was one of excitement because it seemed to be describing, in plain English, what I had already started to experience. The big difference seemed to be that, in the past, in working with other teachers I had been using spiritually based descriptions to describe what has turned out, as a result of the looking, to be just me. That has felt huge.</p><p>I have been involved in the looking for about 8 months. It is hard to remember what I did at first to attempt the looking. I may have been attempting to see what is always there regardless of changes in mood, body, circumstances etc. I remember trying to get the feeling of me.</p><p>The main difficulty I initially had with the looking had been my thinking that I should be seeing "something" and that something should be ongoing (resting in something). The other thing was that I experienced a huge surge in my anxiety when I first attempted the looking. It was as if my whole body was having to squint as I tried to look so as to titrate the intense stimuli. It felt overwhelming like a painful birth or like scratching on a blackboard...which has started to settle into a very simple recognition of me.</p><p>John recently mentioned that during the course of recovery, even though the wound has healed, there can still be orphaned idiosyncratic traits, impulses, self protective reactive patterns that continue. This has been my experience and it is quite challenging at times. The diminishing of the fear of life has resulted in me not having to constantly be looking over my shoulder and keeping an eye on life. Whether in specific situations at work, in my personal life or just generally being in the life... there is a growing knowing that I am safe. I also don't feel the impulse to find lofty spiritual frames in which to cast any of this... I'm here and that more than suffices.</p><p>For anyone coming to this work for the first time, I would encourage them to think of it in any terms that allow them do just do it... and keeping it as simple as possible.</p><p>There are still, what feels like, significant challenges in seeing the self protective patterns that continue to come up. At this point I feel less inclined to indulge them but at the same time I don't know what to do instead. Like my old job description is obsolete but there is not yet a new skill set in place. It is as if I am waiting for these things to just run their course.... I trust that there is a recalibrating going on... But I don't know what it will look like when it settles... it is wonderful and anxiety provoking at the same time.</p><p>It also feels right.</p><p>I feel so grateful. Thank you so much.</p>
