<p>Hi Paul,</p><p>I have found the self-reliance "happening" to me as well. Strangely enough (but maybe not very surprising), after I decided that it was OK to let the remnants of the fear run its course, I became very fearful and anxious the next day and for days to follow. The following week my husband and I went on a Thanksgiving trip to visit our siblings and elderly parents, and by the time we got back, I felt quite ill. I got into the doctor right away. It seemed that life was teaching me it's good to be self-reliant, but it's also OK to need help and to ask for help. I was willing to admit that I couldn't take care of my health all by myself, and that I could work with the doctor as a team to bring me back to health. The doctor is treating me, although it will take months for the treatments to work, and for me to feel completely healthy again (if that's even in the cards). But I still feel the same, regardless of how awful the body feels. And that it's OK to feel awful, and to need help and to ask for it. We have doctors and treatments for a good reason. As John says, we are all in this together. This illness has brought out a deeper compassion in me for the suffering of others. We are all a team together, and are all members of the "creature of humanity", as John likes to put it. I totally agree with that. Anyway, having a sick body can still demonstrate to me that I am all here, that I am not diminished in any way. I can take the steps to take care of this body, even though I have no control over the outcome. But I'm still me, nonetheless.</p><p>Incidentally, I used to be an avid Course in Miracles student. And now I find that declaring "this is all a dream, and the body doesn't exist" is a sorry form of the denial of life. In the group I was a member of, a favorite topic of conversation was whether it was OK to see a doctor or take medications and vitamins. I started feeling guilty for taking care of myself in any way. Now, I really don't care how real or not the body is, or whether this is a dream. I'm going to take steps to take care of my body and help the others in my life take care of their own bodies as best I can.</p><p>Peace and blessings to all,</p><p>Jenny</p>
