<p>We all speak a unique dialect of whatever language we use, and it is for this reason that it is impossible to confirm or deny the the <span class="forum-post-italic">naming</span>of extremely simple matters, such as just what it is we are noticing when we look at ourselves.  This is especially the case with the use of words such as "I", "awareness", "consciousness", and the use of the passive voice with a personal pronoun (<span class="forum-post-italic">the</span> I), and the like.</p><p>When you speak of the "feeling of I" as an "overall sensation of here", that rings a bell.</p><p>I think it best to stick with the simple and the ordinary when considering such matters as how to tell whether you are on the right track.  It is <span class="forum-post-italic">you</span> that you are trying to get a glimpse of--you would call that <span class="forum-post-italic">me</span> from your point of view.  Try to stay personal; use the personal pronouns whenever you can if you want to speak of what it is I want <span class="forum-post-italic">you</span> to look at, and don't concern yourself with any philosophical objections that may come.</p><p>Keep in touch,</p><p>John</p>
