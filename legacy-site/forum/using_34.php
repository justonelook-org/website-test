<div class="forum-post-template-post"><p>Dear John,</p>
<p>I recently purchased your book through Amazon and have fully benefited from it.   As well, I have read your pdf about Ramana. I lived in Tiru for a year and so   am very familiar with his teaching.  I   realize that you are very busy and do receive many emails, so it's no problem if you don't have time to answer. But I would like to ask you one question of   clarification. As you mention in the book, one should find the feeling of I somewhere in the body and hold to that. For me that is not a particular spot but a more overall sensation of here. I can easily do this if it is needed.  But, I am very familiar with I that is not body-centered and not a   feeling at all. It is not localized and in fact is not describable in the  realm of time or dimension. It is simply the I knowing itself as itself. Or, awareness aware of itself. This is a very obvious and easy noticing for me. I feel that this is the direct route to enquiry, bypassing the need to ask the question, Who am I? or even to hold to the body sense of I until I come to this direct knowing. Since I don't want to fool myself at all and my intention for coming to the full fruition of whatever is possible is absolute, I wanted to ask you if it is right to simply notice this that I have mentioned again and again and to forgo the question or body sense, or   shall I continue with a holding on to the sense of I as a sensation here in the body?  If you have time for a very simply response that will be sufficient for me and very appreciated. I thank you.</p><p>With love and warmth,</p><p>S.</p>
