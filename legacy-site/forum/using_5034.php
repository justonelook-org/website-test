<p>I wanted to add another suggestion about breathing. Before you start counting your breaths, when you have a quiet time by yourself, you can spend a few moments just watching/noticing your breathing. As John suggested, feel cool air passing through your nostrils during inbreath, and warm air going out during outbreath. After making sure that you are not forcing your breathing in any way, you can switch to counting.</p><p>This noticing of the feel of air movements in your nostrils without counting is easy to do "on the go," during walking, waiting for a bus or a train, standing in check-out line, even when you work on your computer. It doesn't require even a minute of your time or much effort, while with counting it's a bit more complex.</p><p>Counting requires more concentration, I think, and a specially dedicated time. But noticing your breath without counting might be a good supplement to it.</p>
