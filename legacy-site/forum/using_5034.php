<p>Hi John, Carla and everyone,</p><p>I read a post by anonymous who mentioned about "a breathing problem of hyperventilating". I thought I would post a suggestion on how to deal with this problem which I encountered before, too. The reason for hyperventilation might be that, instead of watching our breath, we try to manage our breathing - accelerate it or slow it down, make it kind of rhythmic, or in compliance with our counting speed, etc. That could lead (and surely led, in my case) to hyperventilation and even raising blood pressure.</p><p>To overcome this, I would suggest to try to let go of any control over your breathing. Just counting the in-breaths or out-breaths as they come, without making them regular or irregular or changing them in any way. Most often, when we start paying attention to our breathing, it gets more prominent and regular and even deeper. I think, the trick is to avoid it and try to "catch" your natural breath, as it would happen without you watching it.</p><p>Also, I found that John's suggestion of paying attention to the air flow as it goes through your nostrils - cool air goes in and warm air goes out - is helpful too. This technique (instead of paying attention to the movement of your chest or abdomen in your breathing, for example) naturally makes your breathing less deep and more shallow.</p><p>I hope it makes sense!</p>
