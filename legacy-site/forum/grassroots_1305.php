<p>Hello Jackx,</p><p>Well, it seems that I wasn't as clear with what I wanted to say in the blog posting as I thought I was. But that's not so unusual, so let me try again.</p><p>We have come to see that the idea of an organized and directed campaign to spread news of this work was a misunderstanding on our part, and we have abandoned it. But we have never doubted that people who have seen the results in their own lives will naturally and spontaneously pass it on to their family and friends. In the first place, we cannot believe that someone who has actually begun to see the results for themselves would not want to give it to friends and family. And we have seen for ourselves the growth in interest in this work that can only be accounted for by this kind of sharing.</p><p>There are nevertheless many who do see liberation to be a solitary matter, and my remarks about that were meant to call that inclination to the attention of those who are new here and hopefully nip it in the bud. It was not meant to be a blanket accusation. Please forgive me if it seemed to be that.</p><p>What we want is exactly what you are doing in your relationships with friends and family, AND what you have done here now. In bringing your experience to the community, you enrich the ongoing conversation as to the nature of the looking and its effects and, most importantly, you allow all to see the ways and means with which folks like you are naturally offering it to those around them. My main purpose with that posting was to shake the bushes in the hope of bringing reports like yours to the attention of the community, and perhaps to give you the opportunity to see something of the current state of our understanding as a community.</p><p>I encourage all who read this to honor what has been given freely by contributing to the ongoing development of our collective understanding, an understanding which is the product of the work, over many years, of many people in conversation with us and each other, seeking to get the heart of the matter.</p><p>Please let us all know what you are doing and what works - just as Jackx has done here - and maybe even avail yourself of the continuing work that is taking place here to further the understanding of what we are doing, and why, and how.</p></div><!-- class="forum-post-template-post" -->
