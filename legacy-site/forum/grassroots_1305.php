<p>Thank you for this Cytex.</p><p>Although I agree completely that you must find your own way of speaking about this, there are a couple of problems with what you are suggesting here.</p><p>The problem we seek to solve is not a matter of conscious understanding, such as the conscious understanding that looking <span class="forum-post-italic">feels</span> like recognition. The looking actually <span class="forum-post-italic">feels</span> like nothing but you. Further development of the experience as <span class="forum-post-italic">recognition</span> is a movement in the psychological apparatus to take ownership of the act and put it in a category that will not threaten the defensive organization of the mind.</p><p>Now, to be clear, if you have accomplished the simple act of looking directly at yourself this misdirection will not stop the regeneration of the mind from unfolding, although it might prolong its duration. But if you suggest this to others as a method to accomplish that act you will lead them astray.</p><p>It's not that <span class="forum-post-italic">recognition</span> <span class="forum-post-italic">per se</span> is wrong or unwelcome, it is only that searching for recognition will find the <span class="forum-post-italic">experience </span>of recognition long before the faint feel of <span class="forum-post-italic">you, </span>that which you would call <span class="forum-post-italic">me</span>.</p><p>I know that recognition is extremely satisfying, and can give rise to well-expressed thought forms masquerading as proof of success, but it is actually not the sweetness of recognition that we seek &mdash; it is the snuffing out of the unseen context in which all ideas of recognition and understanding form and fall ill.</p><p>And it is precisely this unseen belief that what is needed is a correction to a misunderstanding of our 'true nature' that has caused us to fail again and again over the thousands of years we have been trying to solve the problem of being human.</p><p>Please take this as part of an ongoing conversation between us all here to find ways of speaking usefully about this great gift we have been given. I mean no disrespect. You are to be commended for working with people you know and reporting it here. Your report and the conversations it triggers are the key to finding a way to reliably communicate this work to the world.</p><p>Thank you for your participation here.</p><p>John</p></div><!-- class="forum-post-template-post" -->
