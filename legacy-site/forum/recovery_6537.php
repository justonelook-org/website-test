<p>John says that after just one look at "me" the context of psychological fear vanishes from my mind permanently. If that is true, why do I still experience confusion, depression and anxiety? My focus, in finding signs of progress in my recovery, has almost exclusively been aimed at how I feel and how my mind behaves. When seen like that, my recovery has been a really slow and a pretty hard process.</p><p>But recently, after a conversation with a friend, it hit me that a really fundamental change has taken place in my mind since I looked at myself. A fundamental shift, that I have become used to and that I some how tend to forget. I am still confused and I still have days with depression and anxiety. And I often have a hard time to actually pinpoint what, if something at all, has changed for the better as a result of the looking. But now I know for sure. My mind is not perfect, but I always act from a sane place. It is not easy, and this fundamental shift has actually made some of my relationships harder than they where before the looking. But it feels real and true.</p><p>I see now what John means when he says that the fear based psychological context  disappears after just one look. I have seen this for a long time, but not as clear as I see it now. Instead of only looking for signs of progress in how my mind is dealing with life, I can notice the underlying fundamental shift that makes me see and express myself from a sane place.</p><p>Niklas</p>
