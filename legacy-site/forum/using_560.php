<p>Hello JOL Community,</p><p>In recent days, I feel as though I have entered a period of renewed dedication in the evolution of my work in the looking.  The concentrated time with all of you this week has certainly helped!</p><p>There is definitely a "muscle memory" of the ways in which I used to react to the events in my life.  Often, these days, something will happen and I just wait for the old reaction and nothing is there.  It is like, I remember clearly what it was to be crazy in this situation but I can't bring it back! Maybe it's the real meaning of "the snake and the rope" thing.</p><p>The looking itself happens more and more without actually sitting down and trying to make it happen. Sitting in a meeting, standing at the hoststand of our restaurant, are just a few examples of places where it just happens.</p><p>I am allowing myself to more ahead with dedication.  spent 41 years in insanity and that is enough. I have an enthusiasm about this work and my participation in it that I can't ever remember with anything before.</p><p>I thank John and Carla for such a wonderful 5-Day Intensive this week and Worldwide Meeting on Saturday. I was also very inspired and grateful to everyone who spoke at these meetings. It was a great five days and it was a privilege to be with all of you.</p><p>As I spend this Sunday with my partner and my dogs enjoying my sane relationship with this life, I am so grateful to John and Carla and to all of the community. I wish you all the very, very best.</p><p>Love,</p><p>Brian</p></div><!-- class="forum-post-template-post" -->
