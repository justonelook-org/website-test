<p>Hi Brian,</p><p>I wanted to thank you for this wonderful posting and echo your sentiments about the intensive.  It was indeed a wonderful and productive experience!</p><p>I really relate to your comments about "muscle memory."  That's been my experience also, that my patterns of crazy reaction seem kind of pre-programmed and automatic.  I think I'm just beginning to experience what you describe about the crazy reactions not showing up.  More frequently though, the crazy reactions show up but I'm aware of them in a way that's different from before.  Before, I was either so swept up in the reaction itself that I was totally oblivious to its craziness, or if I did somehow manage to see its craziness I would get that sickening feeling of "Oh god there I go again, I'm never going to get this right."  Now it feels like I'm aware of my crazy reactions in a neutral way that doesn't make me want to berate myself, and although I'd still prefer that the craziness not be there there's also a feeling of ok-ness that didn't use to be there, as well as a feeling (which also didn't use to be there) that everything will "be taken care of" as John says.</p><p>For me, the "muscle memory" thing applies to the looking also.  At first it felt really effortful and hard to do, but these days it feels much easier and almost automatic, almost like my attention is a muscle and has internalized what it is I want it to do, and kind of does it on its own.  Neat stuff <img src="smilies/smile.png" alt="smily"></p><p>Love and thanks to you, the community, and John and Carla!</p><p>Youngmee</p>
