<p>Hi Youngmee.</p><p>Thank you for your kind words.  Actually what you describe about your experience matches mine very closely.  I have to be careful that my enthusiasm does not cause me to rush the recovery process!  It would be more accurate to say that "sometimes" I wait and wonder where the old reaction went.  Other times, the reaction comes up and I "choose" not to act.  Other times, the reaction flies right out!  Always now, though, the old giving myself a hard time seems to be over.</p><p>The most profound aspect of this work for me is the skillfulness that we spoke about this week.  Right now, we are in the process of buying a house (again!).  I am noticing that this time, I am being very alert, focussed and sane with the process.  This is new and I can attribute it to nothing but the work that we are doing here.  This also translates into other practical areas.</p><p>Thank you again Youngmee.  See you at the next meeting;)</p><p>Brian</p>
