<p>I've read a lot of reports from people saying that they have ups and downs in recovery, where they start to believe that everything is starting to clear up, only for it all to crash back down.</p><p>This seems to of happened to me a few times now. It's like either I am now going to be fixed or I am still broken...</p><p>'m convinced something has changed in me, something undetectable, (as I really can't remember exactly how I experienced things before I tried it) but the same S*** that has always troubled me still lingers on.</p><p>Anyway, this is what i think happens when we start getting our hopes up; We start having a new belief that the 'looking is now working' and then we reinforce that belief by looking at all the positive things that arise. This could be a way of behaving or a general feeling, a little happier about things. I think this is what cognitive therapy and most self help teaches us to do. For me, I did it by accident because the 'belief' that the looking is working was obviously very real. Self help might try to teach someone that ''they are worthy and loved'' for instance.</p><p>I don't want to go on about this too much as I think it could confuse some people. But anyway, I wonder who can relate to this? I also wonder how focusing attention has anyhting to do with this or could have anything to do with this...</p><p>I felt really optimistic a few weeks ago because I 'believed' something was working. And then guess what, it crashed and I found myself withdrawing and hatign everything again. Regradless of whether I believe it to be working or not, if it's worked, it's worked. I'm not concerned about that anymore. i'm just concerned whetehr we could use our minds during recovery to get a bit of joy out of life.</p><p>This is quite a messy post so apoligese. i had to rush as I have limited internet time. Peace out</p>
