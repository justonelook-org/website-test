<p>The up and down pattern is true for me as well.  Sometimes, more and more of the time, it's smooth sailing and no worries, then we hit a trough and feel that everything is being undone.  The way I see the looking is that it is like a depth charge going off at the core of our being.  Seeing ourselves at the core is subtle, yes, but also the most powerful thing we can do.   The reverberations of this charge move upward and outward through us into our world, as barriers come down and our understanding of who we are expands.   The internal changes are hard at first as the fear is intertwined with our DNA and muscle fiber.  We hold psychological pain throughout our bodies and minds and this has to be undone.  This feels like a huge cathartic process in the midst of which we want to feel good (I know the feeling).  Actually, the fact that we don't feel worse is probably amazing.</p><p>So, yeah.  It's major reconstructive surgery and we come out differently and in some ways we need to get to know this new person and our new world.  Then we have to deal with the fear in others and in our external world, which can be painful as we expand and barriers come down.  The longer this process goes, the more I see the enormity of it and also the simplicity.   We don't have to do anything besides endure the rough patches and be a fair witness to the process.  No prayers or practices.  Right now I am finding it easier to be alone as well Jim, and rather difficult to be with others.  I pick up on their pain and confusion and sometimes mistake it for my own.....I think.  I think the only thing we can do is be easy with ourselves and this process.  I keep a loose journal, which is helpful to note changes as they happen and realize things are moving in a positive flow.</p><p>I'm glad for some of the positive signs you are seeing, littleowl, and hope they continue.  It's good to hear from you again.</p>
