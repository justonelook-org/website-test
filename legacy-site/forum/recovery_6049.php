<p>My social anxiety is gone as well, or rather diminished enough to be inconsequential.  I had a terrible public speaking phobia, now I speak in public once or twice a month and enjoy it.  I believe the change in social situations is that I no longer feel I have to perform, be witty, or stand out. It may be part of that perception that I have to be perfect.  I find I simply connect more with people rather than trying to impress them.  I'm still learning how to do this, but I have less anxiety about it now that I don't see social situations as much as a performance.  I guess I see situations and people for what they are and if I can't connect to someone or a particular situation, it's okay.</p><p>This perceptual change is undoubtedly due to the looking. I'm sorry, but I don't feel that there is anything I did to advance it in any way and the shift was gradual.  It corresponds to an overall reduction in anxiety in general.  I think the vanishing of the drive to be something different is a good thing and, in looking back, something I experienced.  It's a wiping of the slate, perhaps, and allowing something new to emerge.  The beginning of naturalness and authenticity.</p>
