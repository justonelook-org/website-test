<p>Hi Jim.  Yes I believe I had social anxiety as well but hid it under alcohol and the facade of being tough and fun.  I can relate with Jack's comment of being a social performer.  I played up being witty, unique, and people found me interesting.  I felt I needed to wow them with my travels or exploits so I could perpetuate the image of being interesting, edgy, and exciting.  Most of that along with the social anxiety has fallen away.  I sometimes wonder if it's getting older and more comfortable with myself but I think mostly it has to do with the looking.</p><p>I went through a period of time were I felt like I shed layers of old outworn skin and was stripped down to my bare bones.  It was like I had to wait for my new more porous lighter skin to regrow which was an incredibly vulnerable, exposing, and painful time.  It passed.  I wish I could tell you something other than time helped.  Lots of walks, journaling, and alone time was what I needed.  This feeling still comes up usually during a transition but it's not as intense and has become somewhat of an old friend.</p><p>I'm also struggling with the ambition part.  I'm self employed and I need to drum up new business to get clients.  It's hard to hustle and market when I don't feel particularly ambitious. Part of it is dealing with some anxiety around putting myself out there and talking to new people.  But on some level it just feels like normal anxiety not anything debilitating that I can't move through.</p><p>Hang in there. It will pass then maybe come up again and pass again each time with less force than before.  At least that's been my experience.</p>
