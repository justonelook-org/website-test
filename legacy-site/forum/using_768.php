<p>Dear Gerrit,</p><p>1.  It's not that you shouldn't, it's that you needn't. It seems that one dose is enough with the looking, although it is common that upon taking that first look, one finds oneself returning to it again and again whether it'is useful or not. It may be that if more is needed it is taken without the need for our conscious involvement.</p><p>Of course no harm can come from returning to the experience of yourself, and it is actually impossible to homestead there and go back to sleep. The urge to do so, to set up camp and retreat there, or to somehow merge with myself there, is nothing other than the defensive desire to go back to sleep that is the motive force of many of the therapeutic and spiritual solutions offered these days. It is all symptomatic of the fear, and will all fall away soon enough.</p><p>2.  I have no way of knowing whether 'successfully' returning again and again to the simple experience of me would be helpful in the recovery since I began to lose interest pretty quickly once I actually did succeed for the first time, even though I did not know at the time that I had succeeded. That knowing came later - after the recovery was well underway.  I was briefly looking - more like glancing -  often, but not with conscious attention, it seemed just to happen on its own.</p><p>I did begin to notice the faint and ever-present feel of me in the corner of my eye more and more and still now, there is not a moment when I do not have the taste of me in my conscious awareness. So far as I can tell, nothing good or bad comes from this, although it is quite pleasant to taste from time to time.</p><p>I am very grateful for your presence here Gerrit.</p><p>John</p>
