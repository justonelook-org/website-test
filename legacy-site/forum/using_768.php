<p>Dear John et al.,</p><p>You have said many times before that there is no usefulness whatsoever to trying to prolong the act of The Looking. And I think I've also heard you say several times before that there is nothing to gain even from trying to increase the frequency of the moments when the beam of your attention is on You. For reference sake, in the 2011/02/09 Open House Meeting at around 42':30" or so you say: "There is no added value to increasing duration of the time when the beam of your attention is trying to get a glimpse of what it feels like to be You."</p><p>I guess I'm just questioning this for verification. Even with the analogy of taking medicine, it is not necessarily true that there is no advantage to taking a medicine more rather than less; keeping up a certain dosage may be crucial.</p><p>So my questions are: 1) is it really true that I shouldn't make much effort to increase the frequency or duration of The Looking, and 2) how do you know this? How do you know that successfully increasing the time spent successfully Looking at Yourself will have no effect on the course of your recovery?</p><p>I'm just asking for interest & clarity sake and not to be challenging or anything like that. I really appreciate your work and these forums!!!</p><p>Thank you,</p><p>Gerrit</p>
