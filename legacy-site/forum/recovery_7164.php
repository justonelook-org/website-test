<p>Here's what's been happening since my first post (See: Things are moving along quickly...). I continue to do SDA several times every day, and take a "look" when I feel moved to do so.</p><p>Overall, and this is hard to put into words, it seems as if something is "loosening," like barnacles breaking off an old ship hull. There seems to be less strain, and no more need to do what I think I "should""¦I'm just letting things unfold instead of pushing myself. No judgment. If I don't take a shower today, so what? I'll take one tomorrow. If I have a stack of laundry I didn't get to, it's all right"”the world is still turning. I'm less anxious, and when anxiety or fear rear their ugly little heads, I do SDA and they back off.</p><p>In addition to SDA and occasional looking, I also find it very helpful to quietly say to myself from time to time: "I'm not my life, I'm not my body, I'm not my thoughts."</p><p>I've had some insomnia, but it doesn't concern me. I think it's because I'm caught up in the amazement of this powerful secret, and that it will settle down at some point.</p><p>I'm more content with things the way they are, even though I'm still housebound. I've stopped struggling against my chains, you might say, and am generally somewhat more at ease.</p><p>Interestingly, I've had an awakening about what I eat. For years, I tried to follow various diets: macrobiotic, vegetarian, vegan, raw fruits/vegs, blood type, and on and on. I felt I should be a vegan at some point, because it was the "purest" thing to do. I no longer feel any of that and have discovered that I really need to eat more protein (chicken, fish, eggs, etc."¦not sure I want to go back to red meat because it kind of grosses me out). Since I went back to eating this way, I'm starting to feel physically better and stronger and I'm not hungry all the time. Not saying this is for everyone"”we're all different"”but it seems to be good for me, and I'm convinced I saw this as a result of doing this work.</p><p>That's it for now! <img src="smilies/cool.png" alt="smily"></p>
