<p>Hi, Gerrit.  Thanks for your interest. Nobody I know gives a tinker's curse about any of this. How refreshing to be asked about it. In reply to your questions:</p><span class="forum-post-italic">1) What was your state like before you started the Looking? I mean, how were your emotions and your overall experience of life?</span><p>I had been meditating for about 15 years prior to discovering John. I could go in and out of mild bliss states at will, but had realized that these states were impermanent. I'd sat with a lot of teachers from both the Ramana Maharshi and Nisargardatta lineages, so I had a good map of the territory. I was born depressed and miserable, but the ability to go into bliss at will mitigated the depression. I was taking a certain brand of medicine of my own devising, but the chances were not good that I was going to get well. However, I could process whatever tensions that I accumulated during meditation.</p><p>The looking gave me the ability to process tensions during daily activities. It hasn't made them disappear. Yet.</p><span class="forum-post-italic">2) If there were any substantial tendencies toward depression or anxiety or hatred of life/self, have they fallen away?</span><p>Interesting question. They haven't fallen away. However, they have become like a sabra, a Middle Eastern prickly pear--a pain on the outside and sweet on the inside. They come up, and if they're strong enough to get noticed, they subsequently fall. But, I know that they're there.</p><span class="forum-post-italic">3) Would you now say that life is quite joyous and free from fear or angst?</span><p>Daily life is becoming joyous. I'm not free from fear or angst. I've only recently reached the place where fear and angst are being processed naturally in real life. John says that they eventually go away. My experience tells me to believe him. I'm no spiritual genius. I didn't rub a lamp (i.e., hear a few words from a teacher or brush up against a copy of "I Am That") and see a genie appear (i.e., suddenly burst into full understanding of "what is"). I was spiritually un-gifted and had to put in lots of time looking at myself in order to get results.</p><p>That said, I must also add that all of this work was simply not necessary. What I was looking for was right there all along. I could have saved myself decades of work by simply (a) looking at myself and (b) knowing that what I was looking for was my mundane, nonspiritual, ill-defined, vague, unfulfilled sense of me that has always been living in less than glamorous circumstances of daily life.</p><p>Again, thanks so much for your questions. If there's anything I can do, please let me know.</p><p>Sincerely,</p><p>David </p></p>
