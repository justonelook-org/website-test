<p>Upon discovering John, I devoured his podcasts, which both disabused me and apprised me of several notions:</p><p>1)    That feel-good states of being were superior to feel-crappy states,</p><p>2)    I learned that it was not necessary to look at myself "correctly"--That I only had to look at myself, period--to take my medicine. (John uses the analogy of medicine to describe the process of looking at oneself. Like medicine, which is not necessarily pleasant, looking at oneself does eventually affect a cure).</p><p>3)    I was intrigued when he said that looking at oneself <span class="forum-post-italic"><span class="forum-post-underline">is</span></span> enlightenment--an assertion with which I agree.</p><p>For a while, I spent a lot of time looking at myself. Somehow, gradually, (after about a year) life became less troublesome. When bushwhacked by negativity, I found myself looking at myself automatically. This happened in a millisecond. It was as if negative thoughts were hitting a bug-zapper. Without my noticing, the looking had become a reflex reaction to unhappiness. When mental tension developed, I could shun it like a dinnertime telemarketer.</p><p>I found the next stage of this process perplexing. A while ago, I unexpectedly lost the urge to look at myself. In fact, I developed an aversion to the process. In its place came the desire to simply be. When this first happened, I had the urge to call John and find out what was going on. But, I figured, "Spare him my luxury problems".</p><p>The falling away of the need to look at myself had several curious side effects:</p><p>1)    The ability to choose how I would feel: (If a hopeless feeling crept in, I simply thought, "Be happy" and happiness happened. Of course, this happiness is not like celestial ceiling opening up and flooding the universe with frolic. It's a subtle, happy bias: barely enough to register, nothing to blog about).</p><p>2)    An unexplained attraction to normalcy: (Whereas, I once cultivated powerful experiences--both of the pleasant and unpleasant persuasion--I now feel, "the less drama, the better". Mundane, bleak aspects of daily life, once the source of persistent depression and misery, seem, by contrast, richly textured and fascinating.</p><p>(3)    Formal meditation now usurps much less waking time. (Ironically, real-life is becoming a meditation wherein I enjoy people and circumstances that I once found irksome and exasperating).</p><p>What I have not gotten from the looking:</p><p>1)    Unfortunately, I have, failed to cultivate magical abilities, which I once associated with awakening.</p><p>2)    I do not live in a trance, nor do I wish to.</p><p>3)    I cannot materialize riches (Absent a solid business/marketing plan, line of credit, and more effort than I care to expend).</p><p>To sum, everything John told me would happen did happen. Also, some things that he didn't tell me would happen happened. I highly recommend this deal.</p>
