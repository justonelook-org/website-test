<p>I have noticed several changes in perception since starting this work. Actually too numerous to post but one remarkable one is that I don't somehow see people as "other" than me. I think John talked about his experience of other people in one of the retreat videos. I used to be hypervigilant, on the watch as it were about others. You could call it mild paranoia I suppose.But today especially I barely gave it any thought at all. The whole "vigilance" thing was just gone. There was no difference I guess between me and others... hard to express.</p><p>Very striking experience as I tended to be a shy person and being around others made me nervous.</p><p>I am grateful to John for all he is doing. And will continue the work.</p><p>Blessings John,</p><p>Antony</p></div><!-- class="forum-post-template-post" -->
