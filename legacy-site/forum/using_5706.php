<p>I agree that this is an interesting and useful discussion. Here's my take on the usefulness of psycho-therapeutic intervention during the recovery period.</p><p>The problem with psychotherapy is its focus on reforming or eliminating specific, existing mental problems. It concerns itself with bringing attention to bear on the sickened processes in one way or another, for one reason or another. And there is no doubt that such approaches can help the troubled find considerable palliative relief from the misery of specific neurotic psychological processes.</p><p>But the mind suffering from the effects of the generalized fearfulness we call the fear of life is more than a conglomeration of neuroses. It has its being in a context that assumes that life is to be feared. When one set of neuroses departs, new ones arise to take their place.  And the new neurotic defenses are always more efficient  at masquerading as harmless and necessary. Putting attention directly on these mechanisms in a therapeutic context actually energizes and trains them to change form and shift places to evade extermination.</p><p>The primary purpose of the practice of focused attention is not to provide relief from the specific issues to which it declines to attend, although relief is welcome. The purpose of the practice of focused attention is to take authority over the only thing we can actually control, and its effect is the dawn of full self-reliant sanity and satisfaction with the life we have.</p><p>Diverting your attention to seek relief from specific trauma-induced neuroses will not stop the process of renewal that's already underway in your mind. I can attest to that from my own experience of many years of recovery in complete ignorance of the process that was working to clear my mind of the effects of the fear. But if you will put all of your eggs in the basket of gaining self-reliant authority over your attention, I promise you the length and misery of the recovery will be greatly diminished, and you will never regret it.</p>
