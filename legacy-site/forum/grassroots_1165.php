<div class="forum-post-quote"><p class="forum-post-quoted-name">lerajane</p><p style="margin-top:0;">I feel shy in bringing it up. I have some misguided idea that I am saying, "I know what your need." and it will be discounted as another gimmick before they even try it.</p></div><p>Hi all,</p><p>I don't find explaining the looking to be very hard. When you talk to someone, they're there, even if it's over the phone or some kind of instant messaging. The way I explain it is very similar to how John explains it, and I also use the childhood memory exercise a lot which I find very helpful in getting someone's attention to their selves fast and without clutter. The hardest part for me is to get the idea across that one the one hand you have life which can be overwhelmingly complex and contradictory and painful and enjoyable and messy, and on the other hand there's this 'little exercise' that will make it all get better all by itself, as long as you try it. I've been trying to figure out how to bridge that gap ever since I learned of the looking, and sometimes the transition happens very smoothly, at other times there's great resistance in making the leap from worldly and/or emotional mayhem to just look. But if it's smooth, I've been able to make some people look at themselves, the common factor being the right mood which seems to be 'being fed up with life'. You can't force feed this apparently--I've had long discussions about it which led nowhere in the short term (maybe they did dislodge something that might manifest later on?), and I've been shut down immediately as well, where the conversation just flows away from it on its own terms, either because the idea has nothing to latch on to, or because it's defying the importance of the problem someone has. Kind of like trying to pet a stray cat - that's only going to happen if the cat will allow it.</p><p>Hope this makes sense.</p><p>Wouter</p>
