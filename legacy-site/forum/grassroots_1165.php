<p>Hi all,</p><p>I suspect that everyone in this forum has tried to share the act of just one look with at least one other person. When you did, it may have been well received or not; you may have felt it was worthwhile to share this again or you may have been discouraged by the response. (At other times, you may have had no idea how it was received.) Perhaps you saw an opening when <span class="forum-post-italic">look at yourself</span> came to mind but you couldn't manage to speak about it.</p><p>I think it would be helpful to all of us if we shared our attempts (or lack of attempts). We can learn from each other.</p><p>I, personally, gave a speech about the looking to my Toastmaster's group (a captive audience of about 20 people.) It was extremely well received as a "speech." I have no idea if anyone followed my directions during the speech or tried it again after that day or was interested in following up with the website. I also include the process sometimes while leading a meditation. So basically, I find it easier to <span class="forum-post-italic">teach</span> the process to a group (without any preliminary commentary).</p><p>Other than my husband and one of my sons, I have not mentioned it outside these venues, even when I have had the impulse to. I feel shy in bringing it up. I have some misguided idea that I am saying, "I know what your need." and it will be discounted as another gimmick before they even try it. What I feel I need is some openings (like the massage therapist who mentioned her mother looking in the mirror and not feeling as old as she looks; we have all had that experience).</p><p>What about you? What have been your experiences or your fears about sharing Justonelook. It would be very helpful to me.    Lera</p>
