<p>It has been my experience that the development of constant aware attention is of great use in forwarding your intent to share about looking at yourself.  I have found that the intention to be attentive to my life so that I notice thoughts as much as possible, and I notice life as it appears has me consistently attuned to my experience in such a way that opportunities to bring the idea of looking at yourself show up naturally, and that I simply mention the idea, especially when people around me are complaining about their life, which is common, or when people are in the middle of some kind of suffering.  I have found that getting into elaboration about it is not useful, but rather to simply slip them the justonelook.org website on a card or write it down and slip it to them and say something like, you might consider giving this a try, it worked for me.  The less said the better. This kind of leaves people curious and if they are looking for solutions in their life (who isn't) they may follow up.</p><p>One thing is certain that which you constantly attend to is energized and shows up in your field of experience consistently.</p><p>Love</p><p>David</p>
