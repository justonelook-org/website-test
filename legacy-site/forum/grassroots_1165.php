<p>I have been thinking of this, lerajane.</p><p>For some people I meet as a health worker,it works to pose it as a therapeutic measure. For giving relief when things are not so great. it does help they say. At least it gets them looking. I don't promise them that the bad feeling will go away at the moment, but I say instead of focusing on the bad feeling maybe you can look at what never changes, which is you.</p><p>For others who seem to be miserable, but wont accept that they are, a good question to ask is are you completely satisfied with your life, are you truly contented with it? If not, then this looking can help.</p><p>Because it seems, thats what the looking gives you in the end. It doesn'r remove problems or challenges or all stupidities even... it just removes the thing that makes all those unsatisfying.</p><p>If the looking with intent once is truly effective, then ofcourse they will look again. So I don't say they MUST look everyday, I say, whenever it strikes you, look! I don't know if that really works for everyone. A couple of my friends did this but apparently they havent found themselves doing it again. So...</p><p>I also started to teach the 7-10 yr old children in my locality to do the looking. They seem to find it much easier than adults. And they report to me, that they have been looking from time to time. It can't do any harm, so I pose it to them as an experiment that they can do whenever it occurs to them, especially in times they don't feel ok. They like the idea.</p><p>People already engaged in spiritual practice, don't seem to much like this technique. They say it is too simple!</p>
