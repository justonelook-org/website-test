<p>Hi Marlowe!</p><p>Thank you for your question. I will try to clarify what I was trying to say.</p><p>It was not my intention to make a distinction between the fear of life and the fear of being a human. It was also not my intention to say that the fear lays in being a human. What I meant was that the most of us humans seems to do everything possible to try to escape the raw and naked experience of being a human. Distractions and restlessness (which I know a lot about) seems too have its root in something so simple as the difficulty in just being here and experience life as it is, as a human. And it is the context of fear that does that. That stands in the way.</p><p>So I was just trying to confirm, threw my own experiences, that it really is the context of fear that robs us from our natural human experience.</p><p>And yes, human is all we know, but we haven't really tried without fear and distraction.</p><p>I hope that answered your question, if not, just write again.</p><p>Niklas</p>
