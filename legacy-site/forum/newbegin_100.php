<p>Hi</p><p>One result of this process that I have noticed more and more recently is that I now have one life. What I mean by that is that when I hear many people speak, and write about their life and experiences, they seem to refer to two different lives: one inner life and one outer life. And I can remember that I used to do that too. And to think that you have to combine those two lives can be a real struggle<span class="forum-post-bold">. </span><span class="forum-post-italic">"Am I doing what I think is right? Should I do something else? My life situation does not match my deepest thoughts and feelings. I want to find my destined purpose in my life that makes my inner and outer lives become one. And my inner life is very private and complicated and hard to share with others."</span> I am certain that the experience of having two lives that must be combined to give them meaning is the experience of all kinds of people. Not just spiritual seekers. I see this struggle everywhere I look.</p><p>For me. this struggle is over. I have one body, one mind and one life. And I must say that this feels great and I can almost not remember that it ever was otherwise. I honestly don't know what inner life means anymore. I don't experience having anything special inside myself. And when I write about this I can notice that there is something that used to be here and is not here anymore (probably the <span class="forum-post-italic">context of fear</span>). It feels like something really complicated, heavy and bottomless has left me. Now I feel me being here, interacting with my life. This almost sounds stupid but that is what I feel.</p><p>And this new experience just confirms to me what I have seen for some time now and what John has said all along. Namely that the feeling of "me" has healed my mind and that that in turn has resulted "only" in <span class="forum-post-italic">ordinary sanity</span>. What I have wanted all along is plain, <span class="forum-post-italic">ordinary sanity</span> and that is what I have got. My mind is more and more interested in sanity, or maybe more and more uninterested in insanity. When I, for example, talk to my friends about these matters, I almost feel simpleminded. I never follow along into what I see to be not worth attending to. It is not that I only want to speak of the act of inward looking and what I believe that act is capable of. I love to look into almost everything. But if I feel that the "drive" behind what is said is coming from the <span class="forum-post-italic">context of fear,</span> I never give in to it. And that is not a struggle for me, I am just not interested. I will not serve fear anymore.</p><p>One look leads to one sane mind and one sane life.</p><p>Niklas</p></div><!-- class="forum-post-template-post" -->
