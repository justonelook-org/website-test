<div class="forum-post-template-post">
<p>Hi John!</p><p>I read the new version of your "fear of life" essay and I think it's a great summary of the essential points of what you teach.  I'm getting to a point where my urge to share your work with friends is beginning to grow stronger than my fear that they will reject your work and think I'm nuts for suggesting such a thing to them, and I think this essay is a great introduction to people both totally new to your work and unfamiliar with the spiritual arena.  One point you've added to your essay that I think is SO crucial is the point that a lot of things that we think are "inward," like emotions, thoughts, and personality traits, are actually NOT inward but outward.  I think this is such an important thing to state right at the beginning because I, for one, didn't realize this until I read it, and I think especially non-spiritual people (like myself) are used to thinking of thoughts and emotions and self-talk as being the core of inwardness.  If we're not explicitly told that those things are outward, I think we're very easily liable to spend lots of time looking at those emotions and thoughts etc. and thinking that's "me," which while maybe not harmful could lead to a lot of unnecessary confusion and possibly discouragement.</p><p>On a slightly different note--I am so excited to hear you are working on a new book!  I look forward to it greatly and can't wait to see what you have to say.</p><p>On a very different note--you've often said it's useful to think of the fear of life as a disease and the looking as an antibiotic, and I do find it immensely useful to see things in those terms.  To extend the metaphor a little, the more I listen to you answering people's questions, and especially with the new community center, I feel like this community that's forming has a bit of a resemblance to a patients' support group, especially a support group for people trying out an experimental treatment that is known to be very effective but has so far been used only by a few people.  I do have some experience with this because I have a rare chronic illness for which a medicine is just now in the process of being developed, and I see a lot of parallels.  I feel like what we're doing here is bringing concerns to you and each other, like "I've been taking the medicine and I keep getting this or that weird reaction, does anyone else ever get that?  And does it mean I'm taking the medicine wrong?  Is what I'm doing still going to be effective at treating the disease?"  Your answer is almost always, "Yes, lots of people get that reaction, yes, you will still effectively treat the disease."  Or sometimes people will report, "I've been taking the medicine and I can tell you that it is working for me, I am getting better," and then the rest of us are encouraged to continue with the treatment.  Just a thought; don't know how useful it is to anyone but me but I wanted to share.</p><p>As always, thanks and love to you and Carla!</p>
