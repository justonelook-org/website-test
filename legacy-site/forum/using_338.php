<div class="forum-post-template-title">Inward vs. Outward; &quot;disease&quot; and &quot;medicine&quot;</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">teacup &nbsp; &nbsp;</span> 
