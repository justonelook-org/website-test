<p>Hi Bill (and everyone else)</p><p>I'm very encouraged by this new understanding of the human condition as a disease state.  I first heard a statement to that effect when I saw one of the 'guerilla ontologists' of the 1960's generation state at a lecture that 'the ego is a symptom of pathology'.  It struck me then, and still does today.  Here however we have the remedy in our hands... where it has always been.</p><p>The work generates its own steam.  The sense of what this looking is changes with time as the effects of the looking stir up the habitual patterns that have developed around the fear of life.  I would not say that the gap between me and my life shrinks, but it closes in fits and spurts with greater frequency and the sense of physical energy released at each such event adds to my resolve and capacity to look again.  It may be important to note:  this sense of physical energy... I think it is the way the habitual separate self sense interprets and coopts the true bodily sense.  It is thus important for me not to get caught up in the energy... just let it be and look on... look on...</p><p>Keep on keeping on my friend.</p><p>Brezz</p>
