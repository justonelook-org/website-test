><p>John's idea of the power of attention reminded me of that personal conquest in a more general context: just accept you are inside a body/mind that may have broken parts and notice that what is not broken is that you can decide what to put attention to. It is nice that no matter of what we can control that. So there must be things you can put attention to, or not put attention to, that are better for you.</p><p>Also I would say that if you think you'll be better homeless, try it. In my experience, sometimes being far away from some people is useful to get better. Theoretically it is not necessary, but in practice it is easier.</p>
