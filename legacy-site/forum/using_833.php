<p>Hi John,</p><p>First of all I must say I don't do the Looking much anymore. I spoke to you on an open house about that and you pretty much cleared that situation for me. There are occasions still where I do pause and take notice but these are becoming few and few and far between. It feels good but certainly not done with the fervour of a "man with his hair on fire" which is a great relief to me.</p><p>All other practices seemed to have this desperation to them which finally became just an obstacle.</p><p>I have been a practicing poet for some twenty or more years now and for a long time it was tangled up with stories about who I was or wanted to become which I see now actually got in the way of writing well. Then there came a time, some 3 months or so where the desire to write or even any thought of it just vanished. To my surprise I was actually quite relieved. "Whew. I don't have to do that anymore."</p><p>But then upon hearing a song by a friend of mine the words suddenly rushed back into my head. It was like a dam breaking.</p><p>And I have been writing ever since but in a very different tone. I say this partly to encourage one of your members, a sculptress I believe who found she couldn't sculpt anymore. (I believe I have it right.) It might come back or it won't.</p><p>There may come a day when I find my life is sufficient and writing is extraneous. At this point in time however I don't believe that to be the case.</p><p>Writing thrives on the world that is forever in flux and movement. That is what writing is about and the closer you are to it the better it becomes. I am witness to this. You say one becomes more adept once this neurosis is gone and I can from my own experience, assure you that is the case. I have seen it in what I write and the way I write and in my own day to day existence.</p><p>Writers are a curious lot. They create a world to avoid this one in a way. There is a curious paradox here. One needs the very thing one is trying to avoid. Typical of the illness, no?</p><p>So it goes on but I thank you for the great support you offer. As for the looking, I have not much to say about it. I can only encourage people to TRY IT. Everything you say about this wonderful thing will happen. EVERYTHING.</p><p>Bless you my friend,</p><p>Antony</p>
