<p>Hi Everyone,</p><p>When it comes to this whole notion of the "self" being at the root of human suffering, I've noticed there are two schools of thought that people fall into. The first are the people who say there is an instant, point-of-no-return seeing that causes all suffering to drop away; the second are people like John Sherman (actually, just him, as far as I know) who suggest that recovery through inward looking is a much more gradual process. In reading John's story, it almost seems as if John has experienced both: first an instant recovery, then a relapse, then a gradual recovery... (Both somehow related to the notion of the self.)</p><p>I think we can all agree: we would all prefer instant relief if given a choice (provided it has a lasting effect).</p><p>I have noticed SOME benefit from the looking, but honestly, I am REALLY starting to lose my patience with the length of the recovery process. I can't bear the thought of two to five more years, before I settle into sanity. I have so much inner turmoil right now, and I am really disappointed that John (as if it was somehow his sole obligation) cannot offer us something that can break the chains of misery, quickly, easily, and effortlessly. Of course, those who claim to offer something like that have failed me as well. I don't really care who it comes from, I'm just tired of thrashing about for an answer to my simple inability to find five minutes where I don't feel as if I'm never going to resolve the tangle of emotional stuckness inside me that is robbing me of living a normal life.</p>
