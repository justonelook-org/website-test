<p>Hi franco14,</p><p>I've been thinking quite a lot about this specific subject the last few days, how John's method of looking at yourself seems to be the exact opposite of many spiritual/psychological/philosophical solutions. And at the core it is exactly the opposite in my understanding - instead of building a new context which tries to create and nurture all the right conditions to live life naturally, you just put your attention on yourself, see who you really are, and let everything sort itself out the best you can.</p><div class="forum-post-quote"><p class="forum-post-quoted-name">franco14</p><p style="margin-top:0;">I think we can all agree: we would all prefer instant relief if given a choice (provided it has a lasting effect).</p></div><p>Two months ago I would have agreed very much. Now I simply cannot agree. The suffering I once wanted relief from, and the relief from that suffering, were two sides of the same coin, and I was running after my own tail to try and find the peace I felt I naturally deserved. I know now beyond any doubt that I am untouched by any idea whatsoever I may harbor about 'my' life, including this mad search for the end of suffering. And all it took was one look in the right direction, to recognize for the first time who I am, and that I never am, never was and never will be defined by any mental activity. Of course, that didn't mean that the mental activity, including the running after my own tail would stop instantly, nor that I would at all times see all these old habits as unreal. But it does mean that I now have the choice to step out of them, although moving my attention away from them might be very difficult at times. But I firmly believe that this is all I can do. I cannot recover myself, I cannot heal myself. But I can let that healing happen, and let the old and useless quest for not-suffering die a natural death. It's not because it's still alive that it still has purpose, but that is easy to forget at times.</p><p>Anyway, this feeling of losing patience with this never ending quest for not-suffering you mentioned is actually a good sign. To me that means you're ready to let it die, which is good as far as I know. In me this being fed up with these old tools (which never worked, and if they helped it wasn't for long) paved the road for real patience - the trust to abide in the unfolding of the healing, which started spontaneously upon seeing me. Just letting it all sort itself out, without focusing on and breathing life back into the dying ideas that I am bad or wrong or unfinished or need more work.</p><p>Hope this helps.</p><p>Wouter</p>
