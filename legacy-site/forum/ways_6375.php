<p>It struck me this morning, as I was thinking about the recent great forum posts, that we need to get this word out there fast and efficiently.  I thought that speaking to large groups would be more efficient than one on one relating, having the whole group look together.  Perhaps there would even be a synergistic affect when done this way?</p><p>Than I thought about John in prison and how this was the genesis of the whole endeavor.  John's pain in prison caused him to search in a way that he may not have done in another setting.   There also seems to be a theme among lookers that they are more open to this act when they are desperate and have exhausted all other resources.   Anyway, I got this vision of groups of prisoners listening to John and doing the looking with him.  He would have total credibility given his past and the audience, it would seem, would be highly focused on his story and the act of looking.   I also think word would spread within the prison system about this new thing that others are doing to feel better.</p><p>Also, We could change society from the bottom up, the inside out (not that the prison population is necessarily the bottom of society).  It might also be a good setting to do a clinical study.  Choose prisoners who are going to be in prison for at least four more years and are relatively stable within one prison system and do a longitudinal clinical trial over four or five years.</p>
