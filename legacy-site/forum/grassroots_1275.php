<p>Hello Everybody!</p><p>I have just talked with an old friend. For a long time I have planed to tell him about what has happened to me since I looked at my self and the hole process around that. But it has been so hard to do it. I have had a big resistant to talk to him about what I am doing. My feeling has been; how can I ever talk to him (anybody) about what is happening to me and in this work, can he really understand, does he think I am crazy or something and so on and so on.</p><p>It was a little scary and strange to tell him about this project and my own experiences. But It went really good! He is really interested and I got the feeling that he actually tried to look at the feeling of himself while we talked in the phone. We are also planing to meet and talk more about it. This is so big for me because I want this to be natural for me to talk about, and for every person I talk with it feels less strange. And it also strengthen my own ability's to talk about this new possibility.</p><p>And this experience also tells me, once again, that what we are doing here works.</p><p>Good luck to everyone out there who are trying to share your experiences. JUST DO IT!</p><p>Love,</p><p>Niklas</p>
