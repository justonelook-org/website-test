<p>Niklas,</p><p>I too find it easier to talk to people about the looking.  I have talked to many of my friends and I know for sure that a handful have tried the looking. It has been very interesting to watch the results!  As I get clearer and more confident about what is going on with me, I find it easier to talk to others about it. Early on, when I talked about the looking, I threw in a huge caveat, "Hey, I'm not sure this works", you know the changes may have been due to something else", etc.</p><p>I no longer need to add the caveat. I know what changed me and who I am.  This has made my communication more direct and clear and simple.  It's all so plain and simple. I also believe that others pick up on the reality and honesty of the changes in us and this is probably at the intuitive level.  We are different and that comes across somehow.  Especially to people we are close to.  I remember the evangelical Christian adage, Let you life be an example.  Ironically, I have never been able to do this until the looking.</p>
