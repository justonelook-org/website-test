<p>Jackx,</p><p>I recognize this very well. It is getting easier and easier to talk about. And I have noticed that talking about it, is the best way to get better in talking about it. The same process as in every learning. It has helped my process of recovery very much since I threw my self out in conversation about this work. For a long time I sat and waited for things to clear up, and the hole thing of looking at yourself, recovery and this project seamed pretty strange and hard to link to my self and my life. A gut feeling, that this act and this work was offering something really powerful, kept me here. And the real breakthrough in my recovery came when I stared to talk. Talking through postings in this forum. Talking with friends. It made the process more real and about me and my life. It was not just some new interest that I had. This is actually about me and my life. Nothing more nothing less.</p><p>And as you said about that we effect the ones we are closed to. That is also my experience. It seams like we don't speak from the fear based context us much as before. And other people can feel that, as you said. I often find myself using sentences as: "that is nothing to be afraid about, it is not dangerous, right". And if we don't speak from the fear, it is almost as a release for others. Actually I think it is much humor in this. Maybe tragicomic but anyway. I  often get a picture that the neurotic fear is like a little scared child. The scared child, like the fear wants to check everything and has to be guided threw "dangerous" situations. "It is ok, it is not dangerous, you can go and check it out, it wont hurt you". I use to talk to a friend, that also is saying goodbye to the neurotic fear and we love this image of our neurotic fear as a scared child.</p><p>Conversation is priceless. I even feel stronger and more self reliant after every posting. Thank you Jackx for engaging in conversation about this wonderful work we are doing together.</p><p>As you said, Now our life is our example.</p><p>Niklas</p>
