<p>The other day it occurred to me how snuffing out the fear of life was like removing a blindfold that had kept me from seeing my own life clearly, and this recovery process has largely been about getting accustomed to my new eyesight which filters out much less of what is going on. It is not all that different from other changes I've gone through in the past.</p><p>The difference seems merely the scope of it. Normally only a part of or some parts of the environment change, but following the looking the whole world is new. Adjusting to new conditions happens all the time only that most changes are so small that they don't, or barely, catch my attention and they often happen without my involvement. But every now and then something major happens, like a big separation, someone close dies, or a relocation to new town etc., things which can leave me confused and miserable for a while until I get back my footing in the new circumstances. It's natural that more substantial changes are harder and take longer before I feel normal again. Whatever happens though sooner or later I always find myself head up feet down, limbs in place, feeling the same as I always did, but in a slightly new situation and one or two experiences wiser. This thing doesn't seem so different to that after all. What occurred to me is that despite all the colorful images shoved in my face suggesting I do this or that, recovering from the fear feels very much like the same old familiar process of <span class="forum-post-italic">getting used to </span>life after some significant change, and that it really doesn't have to be any more complicated than that.</p><p>So to anyone going through tough times: if you aren't already doing focused attention practice begin immediately. It comes with many benefits but perhaps most relevant is that it increases your brain's capacity to adapt to new circumstances, so you can shorten the time it takes before it's over. I trust it will be the same in the end either way though. You will get comfortable in your new shoes eventually, just like you would start dating again after a painful breakup, even though it might have seemed impossible at first.</p>
