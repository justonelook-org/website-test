<p><span class="forum-post-bold">Some more thoughts</span> <span class="forum-post-bold">on recovery</span></p><p>Another idea recently presented itself that I'd like to scribble down. This is a thought other people have shared here before, and one that I have <span class="forum-post-italic">believed </span>to be the case too, and even argued for because of its logical appeal. This time I want to report it only because it came to me directly, so I might reiterate what it looks like from over here. And that is that sanity strikes us immediately with just one look. The recovery period is not something real, in the way that sanity is real. Recovery is not an event that happens, it is only the name given to a period of time after just one look in which the pervasiveness of sanity is not believed to be experienced because the fog of fear is still thick. That does not mean we are not sane humans already, technically speaking. Up until now I have held other speculations. That was the scribble, cheers.</p>
