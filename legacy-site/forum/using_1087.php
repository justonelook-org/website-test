<p>I have also noticed significant changes in relationships since engaging in the looking. To be honest, the changes have made life easier, in some ways, and more difficult, in other ways. The easier part has to do with just not feeling the need to try so damn hard to please people in general. I am less fearful and vigilant in work relationships and, in some ways, more honest and direct which I think people actually appreciate. I don't feel that I always  have to be the "nice guy" with others which has been a central part of my character  for most of my life.</p><p>The rub comes with feeling more sensitive to and irritated by the high volume of neurotic chatter that seems to be everywhere around me. it is grating.  As a result of this I find I need more time to myself . In fact I am writing this from a beach that I have been sitting at, alone, for the past 3 hours. I used to come here to meditate ...to do some "serious practice". Thank god that has fallen away. Now I just come here to sit....read.....look at the water....and watch this amazing show.</p><p>I am drawn more to relationships  where silence can also be part of the experience. I have also noticed that this okay-ness with silence can create more stress in family relationships. I can be perceived as being distant when, in fact, it is not my intent to be that way. That is difficult, at times,  but I think people are getting used to that.</p><p>It is strange ....I feel more connected to life, people  and everything ....but , at the same time,  strangely cut off at times.............but I am not complaining.</p><p>Paul</p>
