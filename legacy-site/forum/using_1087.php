<p>I have been checking the forums regularly but have not posted on them. My experience, after looking for the first time, was ecstatic. After a while, the ups and downs of recovery began. And they were a pretty bumpy ride. I knew those things would pass and I just had to keep looking. So thats what I did. Meanwhile, doing whatever else I could do to mitigate the difficult periods.</p><p>For me, what I noticed, was a roller coaster effect. At times, something would seem to be resolved and then a period of more confusion would follow, they alternated and I swung between them. I wrote to John a short while ago, that I felt the fever had finally broken. The unfolding of this, whatever it is, continues. But there is a different tone to it.</p><p>Many of the things John has said were seen to be true. Not his truth, but my own experience. Some really huge blocks in my life, seem to have fallen away.  My painful relationship with my father which I had tried to do everything to change but nothing had ever worked, has just changed totally. It just became simple, one day, without notice. I cannot attribute this to anything other than the looking.</p><p>I also seem to have forgotten how intense and difficult those periods of confusion used to be. Or before, how chaotic life seemed to be. I forget that it was not always the case, life was not always sweet. In fact, it seemed harsh and brutal and I did not want to keep living. It is so different now. There is still confusion at times, but it is not a problem.</p><p>Life seems to arise and fall in me, almost dream like, but also very vivid. Whatever the feeling or situation, it is interesting but not in the same way as before. Not as in , 'I am at stake in this'. It's interesting because it's life.</p><p>I could say much more, but I am not very good at articulating this. I have been watching the retreat videos and they have helped me to understand some of the new things emerging in my consciousness.</p><p>I am very grateful to John and Carla.</p><p>Much love</p>
