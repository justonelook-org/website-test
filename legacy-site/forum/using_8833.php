<p>First of all, thank you for joining our forum and trying Just One Look. From what you wrote, I understand that you have read our ebook, The Just One Look Method: Complete Instructions.</p><p>As you saw in the book, the method has two parts. The act of looking at yourself is not a practice. You only need to do it once. One time is enough to start the process of regeneration of your mental mechanisms. But since it is almost impossible to know that you have actually succeeded in doing it, we advise that you look at yourself every now and then, whenever you remember it. Don't think of it as a practice. Just turn your attention inward to get a sense of your me-ness every now and then, when you think of it.</p><p>Once you have looked at yourself, the process of regeneration of your mind will begin and it will go on mostly unnoticed. You can go on with your life and never even think of it anymore. The results will appear over time.  But if you really want to gain control of your attention and be more aware of the changes that are happening in your mind, start doing the Self-Directed Attention Exercise right away. This is a practice and you will get better at it over time.</p><p>The practice is simple. Focus your attention on the sensation of your breath in your nostrils. Count the outbreaths. When you find yourself paying attention to something else, start over from 1. The goal is not to get to 10. The purpose of the exercise is to notice when you are distracted and then bring your attention back to where you want it to be.</p><p>The practice itself will not eliminate negative thoughts. This is not the goal. The only goal of the practice is to gain control of your attention. In time, will notice even more the times when you are distracted and this may feel like you are more distracted than ever. But it is just that, with practice, you become more able to notice more subtle distractions.</p><p>The practice will strengthen your control over your attention over time. What keeps those negative thoughts coming back is the attention you give them, and even naming them "negative thoughts" is giving them attention. In time, you will be able to decide what you want to pay attention to and what is not worth paying attention to.</p><p>Any thought that is not relevant in the moment is not worth attention in the moment.</p><p>I hope this answers your question. If not, please let us know by posting a reply.</p>
