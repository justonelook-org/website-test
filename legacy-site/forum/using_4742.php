<p>When all the agendas of defense, acquisition, denial, and contention that are symptoms of the disease fall away, it is to be expected that there will be some sense of strangeness, and even of loss in the stillness that remains. Many who lose the goad of fear become a little disoriented by its absence.</p><p>But nothing has been lost other than the misery of the constant monitoring of sensation and thoughts about sensation that fill the mind when the symptoms of the disease are alive and kicking.</p><p>When they disappear, what is left in their place is the space to perform useful and satisfying work. It is the nature of a healthy human mind to want to work, and to be gainfully employed in activity that is useful and provides the opportunity to develop practical skill at whatever you do.</p><p>You can expect that over time you will find great satisfaction in working to solve the ordinary problems and requirements of life. I have found it greatly satisfying to learn and improve simple skills like dishwashing, cooking, gardening, even cutting and filing my fingernails.</p><p>And, of course, there is the grand project of bringing the cure for human misery to the world, which brings great satisfaction and fullness of purpose to Carla and me, the like of which we could never have imagined before doing the looking.</p><p>Ir's good to hear from you - good luck to you both.</p><p>Keep in touch.</p><p>John</p>
