<p>This thread surfaced at the right time for me. I'm going through a very awkward period in recovery, my life right now seems riddled with paradoxes and all kinds of unexplained and often weird or unbelievable experiences and arisings. Some days I become very fearful of where I am and where I'm headed, a feeling of despair and lack of control. At times I find it hard to distinguish fantasy from reality. What i see to be the case is that subconsciously stored shame, guilt and self-doubt (aka fear) is coming at me with more force than before, and is taking shape right in front of me, making it very difficult to feel at ease with myself for an extended period of time. The soldiers of fear are sending in reinforcements one after the other, drastically enlightening me as to what I need to change about me in order to be well. It is, as the title says, an intense experience if suffering on these "down" days. It occurs in vivid dreams as well.</p><p>I don't know what has caused this to happen now, but I speculate that 1) I am doing the self-directed attention exercise more intensely than before, stirring a ruckus among the fear troops, and 2) that I am in fact not alone in this process, that my social context has an influence, with many others who are close to me that are also recovering from the fear of life disease. With much fear gone from my mind already, I am now more open to others' say and suggestions that may not always be grounded in sanity. The self-defense mechanisms that were strong and automatic before now feel weak and ineffective. This openness can make me feel gullible around folks who are more prone to violence and violent attitudes, when I just want to chill, and calmly enjoy my life. Is it reasonable to think that my state is related to theirs?</p><p>Or is it just another illusion to keep me away from taking responsibility over my own attention? I'm working to take agency in the way John and Carla are suggesting, because it is the only way I trust will get me out. And the only reason I trust the exercise is because I can see that my attention is the only thing I see to be truly my own, whereas all else is all else.</p><p>The fear that arises in my life now is intense, yet I have a sense of okayness with all of it, even an interest in it, even in the midst of all this wishing and yearning for things to be different. Some days I so long to join in the fun that is my life... that the agonizing and alienating fear would just go away once and for all... wherever the heck it came from.</p><p>Love and peace to all,</p><p>Robert</p>
