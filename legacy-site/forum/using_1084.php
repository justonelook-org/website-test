<div class="forum-post-quote"><p class="forum-post-quoted-name">Jon_K</p><p style="margin-top:0;">..  What does this bring up in your awareness?  How do you notice the possibilities for your life when you notice that you are alive?   Perhaps you don’t notice that you are alive in a meaningful way while looking.  What is that like for you?</p></div><p>When I look, attention turns inward. What it feels like to be me, to me, is an area in body in the vicinity of the head and throat and chest, but it isn't really, it isn't that attention goes to that place specifically. It's not an inner body thing. Yes, it feels alive, but not vibrantly so, not colorfully so, if there were adjectives I could come up with, they would be very mild. I'm usually relaxed when I look, and the looking usually relaxes me even more. The looking lasts about three or four seconds at a time, and by that time attention is usually diverted to a thought or something else. In the beginning, I felt a lot of agitation. That passed. I looked for about three months and then the urge went away. Nowadays I look every once in a while. The question still sometimes come up whether I'm doing it right. People have asked me what the looking is like, so I sometimes I look with the idea of trying to describe what it's like, but I find it's very difficult to do both.</p>
