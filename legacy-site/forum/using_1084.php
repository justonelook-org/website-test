<p>Anyone wish to share an experience about noticing that they are alive when looking.  I don&#8217;t just mean the thrills of life and living.  I am talking about when looking and noticing that you are alive.  What does this bring up in your awareness?  How do you notice the possibilities for your life when you notice that you are alive?   Perhaps you don&#8217;t notice that you are alive in a meaningful way while looking.  What is that like for you?  That is if that is the case for you.  I will share the experience of a bit of what it is like for me to notice that I am alive when looking.</p><p>I sit down and find a place to be in for a while.  Once if occurs to me to look then I look at myself and notice that I see myself.  I shift my attention and see that in this moment I have an awareness of me being alive.  Several questions arise in my mind.  I quickly divert my attention from these questions because I have been down that road before.  I know that questions often manifest as distractions from my experience.  It occurs to me to look at myself again, I do so.  I see that who I am is just fine and then I notice that I relax. I notice that relaxation and life go together quite well.  I begin to feel excited about the possibilities that I intuit about relaxation and life.  I shift my attention back to myself because it occurred to me to do so.  My attention shifts to the thought, that feeling alive is an experience much sought after.  This is just a thought, even though I am interested in entertaining this thought I shift my attention to my body.  I notice that I am breathing.  I notice that my heart beat can be felt.  It occurs to me to look and I do.  I ask perhaps if I continue to pay attention to my ability to look, then my ability to pay attention will develop usefully.  I look at the clock.  I notice my breath one more time and shift my awareness to standing up from my seat.  I think to bring a finish to what I started to do when I sat down.   This is just a thought.  I think that this is what the experience of being alive is like.  This is just a thought.  The more I attend to paying attention to life and my ability to discern between what to pay attention to, the more I am able to pay attention to usefulness.  This is just a thought.  It occurs to me to look.  I relax a little.  I stand up from my seat and pay attention to my surroundings.  I realize that being alive happens every moment.  This is just a thought, but I pay attention to this thought as useful.  Paying attention to being alive in a useful way allows me to be aware that I am alive.  I walk out of my room and I am alive&#8230;</p><p>I am open to hearing any type of input on the topic of being alive and looking.  Write about it in any style you wish to write in.  I just chose first person, you can do whatever.</p>
