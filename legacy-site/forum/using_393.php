<p>Dear John, dear Carla</p><p>You asked for feedbacks what helps people to bring their "beam of attention" towards themselves. So here's what works best for me:</p><p><span class="forum-post-bold">Looking back in time</span></p><p>I was starting from what you often describe as looking for a moment in early childhood. Not having any memories back into early childhood, I started to go back just into my "past". And the interesting thing for me: there is an endless number of moments in the past I can "go back to". And--as you say--it is not a going back to what "happened" there or how the circumstances where and so on, it is directly going back to "how it felt like to be me". And--also as you say--it always feels exacly the same, without any difference, always exactly the same. And this "movement" to "how it always feels like to be me" brings my beam of attention always to "looking at myself".</p><p>Apart from the fact that it "works" it is also very interesting and "funny" to do that. The more I do it, the more "moments in time" pop up, and it always feels exactly the same; and every recalled moment, even memories of dreams I had in the past: the "me-ness of me" feels always exactly the same.</p><p>Another interesting aspect: there are so many activities we very often do. For example sitting in a train or even sitting in a sofa. So when I look back into the past I can easily sit in the train when I was fourteen going to visit my uncle, sit in the train when I was twenty one, travelling through Romania... not recalling events, landscapes, moods or circumstances but recalling how it felt like to be me: and it always feels exactly the same. Amazing!</p><p>Who am I, what am I?</p><p>Here a second approach that "works" very well for me. You don't seem to give "much credit" to this approach, but for me it works very well. It is not an "intellectual" questionning, not even a "thought" questionning. Often when I sit or stand somewhere I am somehow strongly aware of "being here". And my attention kind of goes to the "question": what is here? Who is here? Who sees this? And it is not a "question that wants an answer", it is more a kind of a alert gazing at myself" and it brings me very directly to "looking at myself".</p><p>The common factor of both "approaches" for me: It is not a "mental-verbal-questionning" or enquiry, not a "thinking about" the past or about who or what I am. They are both just helping me to "look in the right direction".</p><p>It must be about two years now since I first heard of you. Every word I heard you say since then proves right in my life. I can't express the gratitude I feel for you and your work and I hope you can plant the "seed" of this looking into as many beings as possible.</p><p>Lucerne, Switzerland, June 10, 2011</p><p>Thomas Kueng</p>
