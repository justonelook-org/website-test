<p>Hello Thomas,</p><p>If I may, I want to respond to what you were describing about how once we look back in the past and (for me) a split second image came to mind.  I was putting on a new dress and I was running my hand down the front of the fabric.  Simple act.  However, I remember or see now that I had seen who I really was then and there and had no idea that it happened at that time.  What struck me was when you wrote that after you were able to go back and see instances where "you" were there, other points of time started coming to your awareness.  What is weird for me is this is happening all the time almost everyday now, and I will be amazed at the memory and then moments later cannot recall the memory.  the moment in time come out of no where and they disappear as quickly and I can barely remember them an hour later.  The wonderful news about this development is I just let them follow away.  That is nothing like my normal personality.  I am an obsessive person, but not about this.</p><p>Thank you Thomas, I really appreciate what you shared because it made me feel connected to this experience in a positive way.</p><p>Have a wonderful day.  Actually, I think it is the middle of the night for you.</p><p>Paige</p><p>Kentucky, USA</p>
