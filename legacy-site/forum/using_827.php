<p>to Karsten and trimpi</p><p>It's certainly very tempting to use the Looking as a way to get rid of bad feelings, depression etc. and I've occasionally tried it with that intention.</p><p>Mostly I was hung up on wanting to be a different person, more likeable, whatever.</p><p>However it doesn't seem to really matter whether I do it just for the sake of Looking or with some objective in mind.The Looking is always</p><p>the same because it's just YOU and it's always YOU. The way you were as a child and the way you are now and since the Looking is just a shifting inward</p><p>of attention trying "harder" (trimpi) doesn't accomplish much if anything at all. This is not said to discourage you from doing whatever you think will bring you to the looking but that "trying" seems to me to be unnecessary. John's words about not being able to get it wrong have always been a source</p><p>of comfort and encouragement to me.</p><p>The other thing is I would suggest you not try to define yourself. It's something I've looked for and never found, a real sense of WHO I AM. I found a lot of definitions but none of them were satisfying.  But the Looking doesn't seem to be about that. In fact this is what drew me to this act is that fact that I could stop trying to figure all that out. It was great relief to me. "Whew. I don't have to do that anymore." It was alright not to know. Of course it took a while for the entire project to dissipate but it's definitely on its way out.</p><p>A lot of things go on during the process of recovery so I respect where you are coming from and just offer this as a something that may be helpful.</p><p>Antony</p>
