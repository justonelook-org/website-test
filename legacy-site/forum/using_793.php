<div class="forum-post-template-title">Four years of looking</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">spencer &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1332155940-->Monday, March 19, 2012 at 04:19</span> 
</div><!-- class="forum-post-template-author-date" -->
<div class="forum-post-template-post">
<p>Someone sent me a link to John's podcasts about four years ago. After listening to a few I began trying to look at myself. Typically the first six months or so involved a lot of wondering if I was doing it right and trying different methods of entry. After about a year it became clear how simple and easy the looking is and since then I've had no doubt that I am doing it right. The first definite, noticeable effect of the looking was the falling away of any interest in spirituality. It still amazes me to consider how quickly the looking changed my perception of spirituality into a benign interest like that for music or the arts.</p><p>However, what really drove me to continue the looking was the promise of an end to the underlying fear of life that has always caused me so much anxiety and neurosis. I suspect I'm not the only one here who feels that much of my life has been spoiled by a definite sense of being at stake, on the defensive and neurotically concerned with avoiding that which is undesirable and scrambling after any means of escape from the unpredictability and emotional danger of life.</p><p>Unfortunately, after four years of looking I have noticed no easing of the sense of being at stake / the underlying fear of life. The symptoms of the fear seem as present and active as ever. Over the last year I've noticed my tendency to look at myself falling away. I'm not sure if this is because the process has run its course or if I have sub-consciously just given up on the idea that it will help me. I recognised early on just how accurate John was in saying that the underlying fear and feeling of being at stake is literally the only thing that spoils life. I can clearly see that to be the case in my life and have become convinced over these four years that no other course of therapy or spirituality is going to fix the core problem.</p><p>So at this point I am fairly confused and a little let down. I would have thought that if the looking was going to snuff out the fear of life at all, there might have been some sense that this was under way after four years of practice. I've enjoyed much relaxation and entertainment listening to the podcast over the years, as well as doing the looking, however I simply can't report any change in the only area that matters, the sense of being at stake, and the ever present underlying fear of life. I would love to hear John's feedback if possible, but I would also really like to hear from anyone else who's been actively involved in the looking for a similar amount of time.</p><p>Thanks for reading.</p>
