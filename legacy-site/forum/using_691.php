<p>The physical reaction to the misery of others results from strong fearful resistance to that misery, but you already know that. And you also know that the fearful resistence to misery will itself soon fall away, I think this is what you mean when you say:<div class="forum-post-quote"><p>"... the sufferings of others did not 'bring on' this illness"</p></div> and that<div class="forum-post-quote"><p>"my 'sensitivity' to suffering will just disappear as I continue to look"</p></div>and that<div class="forum-post-quote"><p>"... I feel that right now, my body is experiencing the fear of life".</p></div> There are some simple meditation techniques such as mindfulness meditation and mental relaxation practices where you try to relax into the resisted experience, that might be helpful and analgesic in the short term. But the looking can be depended upon to do its work one way or another, and sanity and satisfaction with your life will dawn in you sooner than you might think.</p><p>It might be helpful for you to look for the absence of misery where you would expect misery, and to notice that the intensity of the experience of existence is growing stronger as the imagined distance between you and your life is shrinking. It is that falling away of the wall of fear that allows you to feel even the pain of neurotic resistance with greater force than before. There is nothing to fear.</p><p>Thank you,</p><p>John</p>
