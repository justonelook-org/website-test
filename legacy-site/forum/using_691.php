<p>Dear John,</p><p>I have a question for you about the recovery process. I've been Looking since October of this year, and here's my question:</p><p>I am worried that I am not strong enough in my body to hear of the pain and suffering  that others are carrying without becoming sick myself.</p><p>I say this because of two recent events where I became quite ill. At Thanksgiving, we visited with family and then drove to Marblehead, MA where we stayed with one of my husband's closets friends, his wife and little boy.</p><p>During the time we were there, the wife told me about the painful difficulties in her current life, and they resonated with me and I felt very sad for her.</p><p>When I got home I became ill, and now am recovering from pneumonia. This happened to me last year too when my husband and I visited one of his friends in Doha, and the wife there also told me of all her unhappiness, and I became very ill the next day and had to stay in bed during the rest of the vacation.</p><p>So, during this current illness, I've continued Looking &mdash; or paying attention to Me &mdash; as often as possible. I can see that I am fine, and that there is not a problem. This illness is something that is coming and going.</p><p>And I believe you will tell me that hearing the sufferings of others did not <span class="forum-post-italic">bring on</span> this illness and that my <span class="forum-post-italic">sensitivity</span> to suffering will just disappear as I continue to look. Have I got that right?</p><p>John, I very much want to help in bringing Just One Look to the ROW, and I feel that right now, my body is experiencing the fear of life &mdash; and all I must do is continue Looking.</p><p>Thank you for any thoughts you may have on this.</p><p>With great love,</p><p>Dawn</p>
