<p>Hi anonymous,</p><p>Welcome to the forum. If you browse this forum you will find that there are many of us who know a lot about anxiety, depression and suicidal feelings, including me. And many who have found a relief and significant lessening of those after a while. Keep reading here, John's blog and texts, and listening to John's webinars and podcasts, and you'll find answers and gain a picture of what is going on.</p><p>I'd like to point you to consider the difference between fear as a feeling and fear of life as John defines it regarding the work here. Fear of life is the context that the initial scare early in your life pushes your mind into, to put distance between yourself and whatever happens in your mind and outside yourself. This overall context is set at that moment which then shapes your mind into a collection of twisted responses and dysfunctional coping mechanisms.</p><p>The context you can't see, smell, hear of sense in any other way, but you experience all the various fears that resulted from this little mistake. Your anxiety, depression and suicidal thoughts examples of this among many others, and are natural consequences of the erroneous context you grew up with.</p><p>So there's the original fear or scare which is brief and quickly fades, the fearful context after that, and the fears that result from the way you try to cope with this perceived hostility in your life, which are your companions now.</p><p>You can expect the latter to dissolve over time, if not vanish altogether. Excepting the fear as a necessary and intelligent biological reflex which is always in response a real threat.</p><p>There's no need to expect any amazing results after looking. I didn't experience any, though some do. But you can expect to be amazed one day when it dawns on you how much your life has gradually changed over time.</p><p>This is how I understand it, anyway.</p>
