<p>New Hope!</p><p>I recently read John's article on The Difference Between Self-Directed Attention and Mindfulness Meditation. I'm so glad I did!!!</p><p>I've learned so much since then about myself, meditation, forgiveness, patience, Vigilance, and how being consistent is worth the effort! But mostly I'm beginning to see myself differently! That's the forgiveness part.</p><p>I've been working on the practice of Self-Directed Attention for only 2 weeks. Already I see a subtle shift in my relationship with myself. I've heard, and even said "I am not my thoughts". But I never got past that - "it's a good thing to know I'm not my thoughts" - philosophy to having the actual hint of the experience, or KNOWING it! ( Except for the brief experience of unity I had in 2012 which caught me completely off guard because I had no reference to understand what happened to the "Me" I always knew myself to be)</p><p>Anyway...very quickly It is becoming clearer that thoughts don't have to be believed, owned, or even paid attention to.  I can't tell you how many times I've heard that thoughts are like bubbles or clouds coming from and returning to the nothingness they came from.</p><p>But after 2 weeks of practicing Self-Directed Attention  for  just 5 minutes a day, I'm catching myself more quickly and more often coming from the conditioned mind. Or as John would say, acting out the behaviors caused by the psychological mechanisms.</p><p>Of course, It doesn't keep me from acting them out, but recognition is a step towards healing!</p><p>I'm excited! And very motivated to continue with the process and the practice!</p><p>I feel like now I have some HOPE for a sane relationship with Life, myself and those around me. I feel some relief that I don't have to know some secret code to find "enlightenment".</p><p>All I need to do is learn and CHOOSE what is worthy of my attention.</p><p>I'm sure I don't yet know what IS worth attending to yet or probably even fully understand what that means. It's really too early to say too much about any big shift. There really isn't one. Other than the fact that something in me KNOWS that THIS will WORK where all my other efforts have failed!</p><p>Thank you John for such a SIMPLE TEACHING! I can't express my gratitude enough for the gift of a simple doable method that proves there is much more to who & what I am than thoughts feelings and actions. (Effects)</p>
