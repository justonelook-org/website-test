<p>Thanks for sharing, Rick! I've been experiencing exactly the same thing since I started practicing self-directed attention about a month ago. Like you, I have read and heard countless times advice along the lines of "You are not your thoughts, they have no real power to hurt you; let them pass through you like clouds passing across the sky." I agreed so wholeheartedly and thought this made perfect sense. Every once in a while if I manipulated my attitude with enough finesse I might even manage to call up some semblance of feeling like my thoughts were harmless clouds passing through the infinite sky of my being or some such thing, but for the most part it just didn't do much to relieve me of my constant underlying misery. But now that I have been practicing self-directed attention, for the first time I'm experiencing firsthand what it feels like to really know for myself that even my most desperate, self-destructive thoughts are as insubstantial as wisps of smoke that dissipate into nothing if I turn my attention away from them. More recently, I've sometimes even been seeing these thoughts dissipate on their own without my even consciously making an effort to turn my attention away.</p><p>Like you, I also felt (and to a large degree still feel) that I don't fully know what is or isn't worth attending to, and as I think you implied, I decided I wouldn't worry too much about that; in a very short period of time, some of my most painful and debilitating thought patterns have already diminished to a degree that I didn't think was possible. I figure that as my most obviously unhealthy thought patterns fall away, I will naturally become more able to recognize healthy/attention-worthy thoughts when they appear without my having to do a whole bunch of mental hand-wringing about that (which is what I always used to do before starting this practice). Thanks again for sharing your experience!</p>
