<p>Hi Richard, A thought on your posting:</p><p>There is the dream and then there's the response on waking. The dream is just a dream that you have no control over, unless you master some sleep yoga and learn to turn your attention to your breath in your dream state"”not that impossible.</p><p>The attention you give to the dream on awaking is another story. Like any other thoughts that come into your mind while awake, they are what I think John calls "robotic," and need be entertained no more than any other thought.</p><p>Because the dreams come from our unconscious, there may be a tendency to give them more credence than we would thoughts coming from our everyday life, but is this necessary or valid? I think not. My sense is that these are the last dregs of the anxieties that the looking has quelled in your day-to-day life and that, likely quickly, if you don't extend them over-importance through attention when you wake, they will fade from your dream state as well.</p><p>I hope this is helpful. And it's wonderful to hear that fear is gone from your waking state. Feedback always appreciated, best regards, Marlowe</p>
