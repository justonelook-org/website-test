<p>Well it's been a little less than a year since I started with this inward looking. Like a headache that goes away so gradually, it's tough to appreciate; I have seen this looking have an effect and yet I am unable to fully appreciate how far I have come. As my title says, going sane is taking so long I'm going insane. At least, that's the subjective experience. The reality is, this is actually helping, but HOLY S***! I really wish there was some way to speed this process up. I have certain unhealthy fears and avoidance patterns that I was certain would be gone by now, but they are just as strong as ever. I think my grasping for a future state where all my problems are solved is perpetuating the very anxiety I am trying to resolve (oooh... poetic!).</p><p>I'm not sure if I'm looking for advice, sympathy, or strategies. Maybe just a way to speed this up? Is there a way?</p>
