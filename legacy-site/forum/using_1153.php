<p>Take the medicine and be cool.  That's the best advice I could give.  It has been said that when you try to get rid of annoying patterns that they will persist (you get what you resist), and that's probably true, but I wouldn't pay much attention to that since you're going to do as you do regardless.  I bet it won't be long before you start enjoying the show, but don't count on your mind and understanding for that to happen.  By the way, I don't think this is about ridding yourself of life's problems; it's more like eliminating misery and suffering.  Your so-called problems are going to come and go like everything else cropping up in your life, including your impatience and the desire to be done with the undesirable tendencies.  trimpi</p>
