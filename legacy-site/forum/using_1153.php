<p>Franco, Thank you for reporting what appears to be a common experience for many after doing the act of looking.  I have had the same thoughts, and at times these thoughts re-occur, but now when they do I see them in short order for what they are.....just thoughts that bubble up in the soup that is now simmering and will boil down to the pure and direct experience of life as it is, without the desire for it to change.  I have found it to be useful to be attentive to these thoughts and when they are not useful and perpetuate the context of fear that has us talk of wanting it to end, just turn attention to the breathe.  The breathe is not only a neutral location to rest attention, it is a very direct connection to life with its movement in and out, rising and falling, with a balance of in and out, just like the natural experience of life that comes as the looking does it work.  I have found it to be accurate when John says that as the fear of life departs we have direct contact with our experience unmitigated by the distance we had from life when fear had us hold back and away, living in concepts.  This direct experience can appear as an increase of intensity of the anxiety, depression, frustration, and despair that have not yet calmed down and mixed with the fullness of life.</p><p>Thank you for your contribution to this conversation.</p><p>Please consider if you have not yet done so to donate on a monthly basis to secure the future of this work.</p><p>Love.</p><p>David</p>
