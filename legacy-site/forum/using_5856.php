<p>I hear you Jim.  I've felt in a similar manner and still do occasionally.   I was watching one of John's retreat videos and he said that things just fall away, one by one, without any fanfare.  New material then emerges and we find out who we really are, without the fear, piece by piece (I hope I'm recounting that accurately).  He said there is nothing magical or abrupt about this process, it just happens slowly and inexorably over time.  After almost 3 and a quarter years I find this to be true.  Generalized anxiety, which I felt almost always, throughout my life is pretty much gone.  Situational anxiety, related to a specific external source, fluctuates, but is rarely unbearable or intolerable.   More and more I feel I'm gaining mastery over my life, or at least better at accepting the way things are.</p><p>I would look for negative patterns and habits falling away.   Maybe keep a journal to keep better track.  Writing things down has helped me.  Noticing that things go away quietly might help you feel more optimistic about troublesome responses, like your social responses, eventually disappearing.   In my experience they do eventually see themselves out.</p>
