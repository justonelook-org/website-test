<p>Thanks for your post Lera--- I feel I can relate as I had a somewhat similar experience the other day.  I was going for a walk in my favorite park the other day and was spinning in one of my usual loops.  As soon as I realized what i was doing i said 'no'... in that instant my vision went from a narrow pinpoint to something that encompassed the entire park.  I could see the trees and flowers, hear the birds, smell the dirt with a vividness that hadn't existed when I was caught up in the loop.  I feel that I'm able to catch on more quickly and tell myself 'no' more often and I can feel the freedom in that.</p><p>I am also feeling more and more like a grateful spectator....It definitely has not always been this way (sometimes it's been kicking and screaming spectator or completely apathetic spectator).  Coming to terms with the fact that I'm not in control has been an extremely painful process.  I've had some pretty amazing what I like to call 'existential temper tantrums' over the past few years but I'm happy to report that life is a bit more graceful these days.  I still have my moments but the energy and drama behind them are diminishing until one day they may be gone.  Who knows?</p><p>Natalie</p>
