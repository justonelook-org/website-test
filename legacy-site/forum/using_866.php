<p>I've been "taking the medicine" for over 4 years and I love how indications of recovery show up on their own. First, it is clear to me that I am not in control of any of this. I'm merely a grateful spectator. It started this way:</p><p>I was sent an e-mail invitation to a special sub-committee of an organization I belong to. I was curious, so I showed up to see if it was for me. They were very kind, thanking me for my interest, and saying the invitation had been sent in error. I was not surprised. I understood and I left. Later in the day, I noticed an old thought pattern trying to gain traction: "What must they have thought about you?" I caught it mentally and said, calmly and firmly, "No." The pattern tried again from another angle and, like fencing, I parried, "No." and the thought form retreated, perhaps to try again another day. My confidence reminded me of a recent dream. In it, my dream personality is keeping a lioness as a house pet. One day, the lioness starts trying to get out of the house. When challenged, the lioness has a firm, determined look that says, "I will be free."</p><p>This is not "will power" or determination in the usual sense. It is nothing my personality could successfully employ as a strategy. I know because I watched my thoughts and changed them for years. It calmed my overt anxiety but did nothing for the underlying angst of life. This comes from a deeper place, as evidenced in my dream.  I am offering this as encouragement: the looking does the work.   Lera</p>
