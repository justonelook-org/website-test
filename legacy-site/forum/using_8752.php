<p>I had a private convo with John and Carla - it was great. I resonate immensely with their message after 20+ years on the spiritual path starting with imaginary gods as a pre-teen and ending up with Niz and Ramana in my early 20s. During our chat John mentioned something to the effect of "the effort made to dismiss unwanted thoughts is itself a form of SDA". I found that hugely inspiring and *appear* to have incrementally greater success in NOT letting depressive/anxious/fearful thoughts run my head for hours on end. I see them as a challenge when they arrive, and attempt to completely turn all attention away from them onto happier more positive thoughts. I thought to share this because it is not only a way of doing SDA but also serves as a palliative for the negative thoughts (if one can even marginally succeed in turning attention away the moment they surface, before they gain traction and snowball into something massive...). I'm also curious if there is some form of SDA that can be done other than breath watching that can be done anywhere.... Something that can be done in the subway or while walking....</p><p>Secondly, I've seen a lot of people saying they fail utterly at SDA. My POV is that much like the looking it is impossible to fail. Is this correct? I say this because the ATTEMPT to reign in attention in itself works the attention muscle. Like trying really hard to lift a heavy dumbbell which may not budge but your arm will be sore. And eventually, the dumbbell will move. So there is progress being made regardless.</p><p>Thoughts anyone?</p>
