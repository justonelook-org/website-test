<div class="forum-post-template-post">
<p>After some time has passed since doing the Looking you see so many wonderful changes happening. It's the same life but...completely different. The sense that this is not just about you creeps in. There is a strong desire to share this with others. You see more and more, people struggling with the added burdens of self-doubt, fear on top of the everyday problems. This added burden seems now so unnecessary. You see so much suffering that is well.. unnecessary. This is not something only your logical mind sees. Many people know and realize that much suffering is unnecessary. They do what they can to alleviate it in their own small way. My own awareness of the suffering of others has altered radically. It's not just a thought in your head. Others are a part of you somehow. So far I've been content to sit back and enjoy the benefits of the Looking and that's been OK. I'm not getting down on myself because I'm alright with things as they are but the sense that I"m not alone here is getting stronger. I don't know if this is part of the recovery or perhaps the final aim of the recovery. Truth is, I'm not sure how to share this (the Looking) with people without proselytizing or getting on a pulpit. I suspect all this will work itself out in time but if anyone has experienced these feeling, I'd be glad to hear from you. Thanks.</p>
