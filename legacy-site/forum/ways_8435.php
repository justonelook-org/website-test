<p>I really think that the spreading of this teaching is up to all of us, and that we all have our unique strengths in the ways in which we can do that. Although I have spoken about JOL to a fair number of people now, I never have been the one to bring up the topic 'out of the blue', so to speak. It's always been in a context of someone telling me about some suffering they're going through, or just a group of us talking about deeper matters. I really feel a person is going to have to be going through some major suffering of some sort to have the desire to apply a teaching like JOL. Although I also think it's really good for as many people to know about this teaching as possible. Who knows what might happen with a person who is feeling good right now, several weeks, months, or years down the line? I am also able to speak of JOL in my profession as a jazz musician as a way to help improvisation.</p><p>Now that John and Carla have put links to past webinars up, you can direct people to webinars where John and Carla lead people through the looking and the self-directed attention exercise.</p><p>I currently have the idea that, as the 'soldiers of fear' continue to fall away for me, I will gain more clarity and avenues about communicating JOL. I have said several times on this forum that, when I experience periods of less discursive thought I feel much more 'sane' and 'at ease'.</p><p>Also, I tend to think there may be an energetic type component to all of this. Like, when one is walking around with very little fear and just very empowered/self-reliant..however you may want to put it.. I think there are others who tend to 'pick up' on that. They might even not know why they pick up on something. But I think there may be this energetic-type component which exists in all of this.</p>
