<div class="forum-post-template-title">How to speak to others about the just One Look Method</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">antonyirvine &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1528520593-->Friday, June 8, 2018 at 22:03</span> 
