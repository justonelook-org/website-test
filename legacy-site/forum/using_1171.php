<p>I started listening to John Sherman's youtube videos of the retreats (2009, 2010 & 2011) in the middle of November 2012.</p><p>I have been on a search for salvation from my life since the end of 2008 and as you could possibly conclude I have moved from one discipline and teaching to the next.</p><p>What John was proposing hit me like a ton of bricks and it was so clear. Everything else just fell away. It was over. I did not have to struggle. I did not have to learn techniques or do difficult mind exercises. I was so damn tired of all the struggle. I could not keep it up. John said in one of the retreats that "the looking should not be a burden. We have enough burdens in our life." So I realized that there is no escaping or transcending life. Hope has gone and now I can just live my life. Live my life to the full. Not worry about what I am doing, how I am doing it, what I am thinking, What I should be thinking, what I should be feeling, what I should be saying or how I should be acting.</p><p>John & Carla thanks for this!!!!</p><p>All that is left is to live my life (bliss). "I once was lost, but now I'm found.</p><p>That brings me to my next point. I have a desire and I do not know where it will take or lead me.</p><p>My desire is this: "To connect with other 'Lookers' in South Africa."</p><p>Due to technology constraints it is difficult for me to be part of John's meetings (including "Grassroots")or retreats. Also downloading his podcasts and videos from this website (although I will try tonight at home) would be slow and time consuming. Taking into account the time difference between the US and SA is also huge. Therefore many occasions the meetings would be early in the morning (and with very slow internet...impossible).</p><p>So if there are any south africans who are part of these forums...please message me. I stay in Paarl a rural town approximately 30 kilometres from Cape Town.</p><p>My final comment I would like to make is that when I started looking at or feeling myself it has been quite successful....however for the last week to week-and-a-half it is something that I haven't really "efforted" (is that a word?) to do. When it occurs to me to look I look but I do not strain myself to look and then somehow I get the feeling that I have looked or felt myself... It's just me it's not something alien.....so it has become more and more effortless and natural as if this is what I have always been or done.</p><p>But now I wonder am I deluding myself that I am looking or what!!! I don't think so but I would really appreciate your comment.</p><p>Found</p><p>Bisschoff ('Looker')</p>
