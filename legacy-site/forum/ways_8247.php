<div class="forum-post-template-title">Hindrances for studying TJOLM using quantitative methods?</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">roed_ &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1525282109-->Wednesday, May 2, 2018 at 10:28</span> 
</div><!-- class="forum-post-template-author-date" -->
<div class="forum-post-template-post">
<p>Quantitative studies are often used by our modern institutions to test the effectiveness of a treatment. They aim to show with statistical data how and if something is working. It has been done with mindfulness practice, and with significant results. The many 5-star reviews on Great Non-profits are indeed telling for the effectiveness of JOL, but we who have put reviews there are likely to be biased. There needs to be a control group to prove something statistically. But I wonder, what are some obstacles or hindrances for studying TJOLM using quantitative methods?</p><p>The first and so far only that comes to my mind is an ethical matter. Say that a study is designed using questionnaires. Two groups of volunteers get to rank different aspects of their well being, perhaps using similar questions as in previous studies. Then one group is encouraged to work with TJOLM, but the other is not. After a period of time all the respondents take the questionnaire again and the results can then be compared. But what about the control group who are not instructed to look at themselves and subsequently practice SDA? They will be left behind in such a scenario, and that doesn't sound right to me. Maybe it is OK if they are offered the same solution at the end of the study? Still not entirely good to my ears, but that may only be me.</p><p>And, ethics aside, I wonder what other issues could arise. I'm not a psychologist and have no training in how to study mental health. I may have overlooked the obvious... But what the heck, shouldn't it be straight forward to just copy a study from this field, but change the treatment to JOL/SDA?</p><p>I'd like to hear your thoughts about it, this could be a useful discussion. If JOL is statically proven effective in the field of mental health it may generate a whole lot of positive attention. I mean, TJOLM should be like a wet dream for every mental health professional out there, if it really works.</p></div><!-- class="forum-post-template-post" -->
