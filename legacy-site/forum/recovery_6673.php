<p>Turns out that the act of looking needs to be done just once. It is common for people to glorify the looking and assume that the more they look, the quicker they will be finished with the misery created in fear. This is understandable given the fact that nothing useful can be done to cure the mind's madness until the fear of life is cured by the act of looking at yourself-at your me-at least once.</p><p>But once you have done that, the disease is cured, and repetition, although sweet, is absolutely optional. Once you have succeeded in that really simple task, what remains is the recovery, which can be difficult and painful, and can persist for a long time unless you take direct control over the development of your mind as it heals from the disease.</p><p>And that is the purpose of the practice of self-directed attention. As you work with it, you will come to see that:</p><p>1. Your attention is the only phenomenon over which you have any control at all;</p><p>2. Gaining effective control over attention is extremely hard in the beginning, but can be accomplished more quickly than seems possible when you first begin to work with it;</p><p>3. As skill and understanding grow with practice, you will begin to see the true and deeply satisfying nature of  the human mind. The childish fear-driven hunger for a stroke of magic to take away all difficulty and even the need for work on yourself will vanish. In the end, you will come to love your human life precisely because it takes clarity, patience, and skillful work with your attention to bring it to fruition.</p>
