<p>You show a good understanding of the cause of the problem when you write that you have "observed that there's something full-on determined to hold me at arm's length from life."</p><p>That's exactly true, and that something determined to keep you away from your life is your mind, which has been crippled by the fear of life itself, and paralyzed by the energy exuded by that fear. Looking at yourself destroys the underlying context of fear, but although the fear of life is gone its effects will take some time to die off. It is those lingering effects that cry out to you to escape before you forget to be afraid.</p><p>It is not your fault. Your personality, like for most if not all of us, was damaged by having taken birth in the fog of fear and it will take some time to recover and heal. The prime directive of the fear is to forbid direct contact with life. The irritation you feel about the persistence of the symptoms is itself one of those symptoms, and it works by pulling your attention away from life as it is to the irritation and confusion about it.</p><p>You need not wait passively for your mind to clear of these aggravations. If you are willing to take on the practice of self-directed attention as you recover, you will learn to take intelligent and self-reliant control over your attention. It can be very hard in the beginning, and might even seem impossible, but you will begin to notice real progress within a month or so, and as you gain in strength, even the difficult process of recovery from fear will come to be deeply satisfying.</p><p>Here is a link to the ebook with instructions for the self-directed attention practice. Please take this seriously.</p><p><span class="forum-post-bold"><a href="https://www.justonelook.org/download/DownloadHandlerLink.php?file=JOHN_and_CARLA_SHERMAN_The_Just_One_Look_Method.pdf">The Just One Look Method</a></span></p>
