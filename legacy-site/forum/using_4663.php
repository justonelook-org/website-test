<div class="forum-post-template-title">Open House 6/18/14: Tracy</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">Rosen &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1403209641-->Thursday, June 19, 2014 at 13:27</span> 
</div><!-- class="forum-post-template-author-date" -->
<div class="forum-post-template-post">
<p>The question has been whether to follow the therapist and put attention outside or follow John's suggestion to look at oneself. - I believe to understand the fear of "making a fool of myself" which means to continuously worry about myself in relation with others (What do they think of me? How should I behave? etc). I've known this condition for great part of my life. But if I take a close look at what is going on, I have to see that I am always defining, seeing, judging myself in respect to others which means in respect to the outside. If the therapist's suggestion is meant e.g. to just see the tree, hear the rain, feel the sun or the wind on my face, I assume that this is about putting ones attention onto something neutral which is useful. In the same way I may feel inside myself a toe or whichever part of the body. I'm simply aware of it without interference of thoughts and emotions. As I understand John's suggestion "to look at myself" though, something more essential, more basic is meant. In my life I tried therapy, meditation, inquiry etc. Progress was made but the feeling of neediness remained. It felt like a house built on sand. Trust and stability were lacking inspite of all my efforts. Looking at myself (I still don't know whether I did it right) immediately felt being at home or reconnecting to "something" that, without my being aware, had always been there. It's true, the separation from life vanishes, as someone has pointed out. The hope, to finally be helped or saved, needed to be and could be given up. I stopped looking for support from outside, instead it's always me with myself. Inner strength, stability, stamina are growing, inspite of the discomforts of the process. It is uncomfortable but not different or worse from what I've known. I can take it and I want to know. I don't avoid or run or ignore. What I notice in all that is that I begin to see myself with greater clarity, myself as this person, this character with all its shortcomings which I had hated all my life. Already that is liberating. I'm curious what will be coming next. And, although I'm rather sloppy and tend to forget, the training of putting attention on the breath, entering and leaving the body, is helpful. Switching from thinking to the nose is, if I'm attentive and willing to do so, immediate. And it connects inside and outside which seems significant, although I cannot yet say why. - I hope to be of help to you!</p>
