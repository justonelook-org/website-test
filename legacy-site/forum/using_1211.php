<p>I feel that once the spiritual seeker in one is activated that the main focus is this question. Meaning by most spiritual teachers seems to be responded to with a focus on purpose. Telling you who you really are and how to properly relate to life.This usually involves rules and forms of ritual behavior that can possibly ground you and allow your focus to concentrate in specific areas relative to the teacher or teaching's way to supposedly move you towards a personal understanding. I have found this to be an expansive, distracting, and in the end not truly helpful way to gain self knowledge.Learning what a teacher has experienced can lead to insights but the price seems to be a less questioning and more placid, even oppressed person that has not found out the answer and has sacrificed the adventure of finding out for a fake safe haven.</p><p>The looking has completely bypassed this whole drama of allowing you to be in the one down position in order to understand your purpose or the meaning of life from the Superior Source. It's not that you have given up on finding the answer to the question but the act of looking has the consequence of bringing your point of view to a new and less desperate attitude regarding when that needs to be accomplished.</p>
