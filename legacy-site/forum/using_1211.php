<p>Hi everyone! I came across the looking about a year ago now after spending much energy "seeking" for a solution/resolution to some intense anxiety I developed which at the time seemed to come out of the blue but likely came from my brain "catching up" to things in the past that I had not taken the time to deal with. I guess in the end it doesn't really matter where it came from because it was always probably there (the anxiety) lurking in the background, but I had never paid much attention to it or was too distracted to notice it. But now it seems to have gone the other way where I think about the anxiety daily. I will come to intense realizations followed by a period of no anxiety but then it seems I forget the realization and have to start over again. I did the looking and truly feel that I have had the one look at myself. I am also actively doing the mindfulness training and it seems to be helping. Compared to last year, I am much improved but not 100% free from it either, but that's ok.</p><p>Most of my negative thinking patterns have dissolved but the main thing that my mind still seems to go to a lot is struggling with the meaning of life. Do others struggle with this question? It's odd because some days the answer seems perfectly clear, other days it doesn't matter what the meaning of life is because I am content without needing to know, but the other half of the time the question remains. I find it hiding in the background of my day, sometimes as more of a feeling than a thought. Does this make sense? Does anyone have any words of wisdom?</p><p>Thanks in advance and keep up the amazing work John and Carla, I am forever grateful.</p>
