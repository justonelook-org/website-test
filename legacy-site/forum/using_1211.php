<p>Hi Sophie,</p><p>I used to be concerned about the meaning of life, and I can see layers or various meanings to meaning. What I mean is there is the question of a purpose or a task in life for each individual and then there's the feeling of meaning or meaninglessness, as in "what's the point?" of it all. I suffer from the latter. The question comes when I suffer. When one is happy and content it has its own meaning, and the question doesn't seem to arise. Then there's the general philosophical question of the meaning of it all, "the life, the Universe and everything" as Douglas Adams puts it in his Hitchiker's Guide To The Galaxy. Lately, I've been more content with meaninglessness. Life doesn't necessarily need philosophical explanations. I think the need for those arises to some extent from suffering, as a buffer to discomfort of detachment from life. The big question is part of life and will probably remain so but the urgency to base your sense of security on the possible answer will most likely dissolve with the fear, I think. It's then an interesting question among others and can be enjoyed as a challenge as such.</p><p>I congratulate you for the most of your negative patterns dissolving! I'm happy for you.</p><p>Seppo</p>
