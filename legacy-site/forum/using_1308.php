<p>Hello everyone!</p><p>I'm a bit confused about what to look at. In some places John says to try to get a feeling of what it feels like to be me. Sometimes he says to look for what is permanent. Or look for the seer.</p><p>And then I read this in the book "Look at yourself": "The feeling of 'I am' is an experience; it is a definite, energetic, sensational experience. It is not reality, it comes and goes. It comes with the 'I' and it goes with the ?I?, and you are not aware of it at all times." (what does that 'I' refer to?)</p><p>Compared to the instructions on justonelook.org starting page: "What we mean by looking at yourself is to try to get a slight taste of what it actually feels like to be just you -- what you would call me."</p><p>So does that "slight taste of what it actually feels like to be just you" refer to "the feeling of 'I am'" (sensation) or the permanency.</p><p>It doesn't help the confusion that I've read also some other material concerning self-inquiry and not one place has been clear on this point. One source even said that if you don't start with the "I am -feeling" (meaning the sensation) you may run into problems later on.</p><p>Or does it matter so much what I look at? Is the point rather that I look at whatever seems like me and try to see if it truly is me?</p><p>So what am I supposed to look at? "I am"- sensation? That which sees the sensation? Or whatever seems like me (whether it's a sensation or not)?</p>
