<p>Dear Kismie,</p><p>I've been listening to John and participating in conversations here for about five years now. It is good to see and hear John struggle with trying to say something clear and direct about this act, and to have watched him shed old ways--in short, to watch him learn and grow. To give yourself a fresh start is a great idea, I think.</p><p>Looking back, I don't think it mattered what I was looking at. In fact, it might have been helpful to know what I was not looking for, but even that can be confusing. What mattered most is that I kept at it. I kept talking and trying to get a glimpse. Doing this destroyed the quiet desperation that haunted me, which I can see now was an unconscious feeling I had about my own existence. John describes this better than I can. When that feeling left I can't say, but I'm sure now that it is gone. It turned out to be nothing but a wisp really, but now I'm treading on confusing ideas. These ideas make more sense to me now and are more enjoyable to talk about after the fact.</p><p>Today, the fact that I exist is permeated by everything this body comes in contact with. Realizing this with my rational mind has caused me to become overwhelmed at times, with a sense of deep satisfaction like I could have never known. I have gone through many phases with this; for a time I was left speechless. I felt like there was absolutely nothing I could say about it; that everything that was ever said about the wonder of human life was useless. I felt for a time like I had that disease that Benjamin Button had, and that I was at the end of my life. But now I'm inspired to at least try to say something again. I'm starting over. And I'm starting to see that this existence is the same for everyone, but for many, it is clouded by the fear of life.</p><p>Keep at it. It can't hurt you. It definitely made all the difference in the way I experience life now, hardships and challenges abound.</p>
