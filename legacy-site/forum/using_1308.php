<p>I feel that the word looking is just a name. so if you treat it like that, then you could say it points to an act.</p><p>now what is this act then? it is not the selection of a thought or an artefact in your mind that feels ok to you versus those that dont feel ok. such things are fine and are necesary to live life, but do not constitute the looking.</p><p>the looking is a very specific action where you move your attention to the feeling of me and you know that you moved it. of course it is hard to know if one really moved one's attention to me vs something thats not me. the mind throws up such objections normally when this is attempted. then you could tell yourself that as long as your intention was to move the attention towards 'me' and you actually consciously moved your attention and felt it, you are doing great.</p><p>the difficulty in communication is from the fact that the act of looking is simply only about this movement and nothing else. all else is in the realm of thoughts and living life.</p>
