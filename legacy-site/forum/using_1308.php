<p>Thanks for your replies. I thought to resurrect this thread to get a little bit more clarification on the matter. So in the end I got clarity by looking at whatever crossed my mind, and I think I succeeded even before I got that clarity, because I started noticing that things started to change so soon after getting more clarity. But the question still remains in a somewhat different form.</p><p>I came to the conclusion that there is both the sensation of me, and me feeling it. They both seem permanent, especially me, I don't see how it could change, because it isn't even a perception, but that which perceives. Simply me. They seem to be linked together, so when I get in touch with the other, the other comes also to the front.</p><p>But I'm a bit confused that for example in the direct look method -instructions they are spoken of as the same thing, when there seems to be a distinction (between the feel of me and the me-ness):</p><div class="forum-post-quote"><p>Now, in the same way that you moved the focus of your attention to your breath, move the focus of attention inward, looking for the faint sensation of what it feels like to be you. What you would call 'me'.</p><p>What you are looking for here is the simple me-ness of you. Not the thoughts that pass through you, or the emotions that play within you, or the sensations that rise and fall within you. You are that which is always here. Everything else - thought, emotion, sensation - comes and goes in you. "</p></div><p>Am I seeing things right? Are they two different things? If they are, why are they spoken of (at least it seems like) as the same thing? Is there a reason for this?</p><p>And the other question is which is the preferable one to tell others when talking about looking? Do I try to get them to feel the "feel of me" or to "see" the me-ness? Or are both needed? Or are they alternatives? Or is the me-ness the goal and the "sensation of me" just a way to get to that (since I'd say the me-ness is me and the "feel of me" just a sensation of sorts)?</p><p>My own experience with telling this to a few of my friends is that the "feel of me" is easier to find. And after that it seems like it's easier to "see" the me-ness. If I try to get them to see the me-ness from the get go it just doesn't seem to work.</p>
