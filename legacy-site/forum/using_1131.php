<p>Hello Barbara,</p><p>I just joined here, too, after a year of lurking. I've been thinking about the circuits aspects you mentioned. Our responses to life are hardwired in the brain and have been reinforced for ages, perhaps even decades, and so they might take some time to fade. But as I've been trying the attention directing trick I had this thought that our lives are what we attend to. I'm coming to see that what happens and attending to it are two different things. The latter is under our control, to an extent. It's bit like that new agey saying "you create your life", which isn't true when your life is dominated by fear, though. But it might become true when the fear is gone. We can't always, perhaps not even mostly, choose what appears and what we attend to, but it might make a difference when we can. It remains to be seen. I do cleaning in a neuroscience lab in the mornings, and they did some research there into neuroplasticity, the brain's ability to change and create new neural circuits. It seems the brain can change, throughout life and quite dramatically. This is not news to us laymen but I find it helpful to think that the brain follows the changes in your mind, with a delay, and in our case, the change in the main theme in driving our behavior, the Fear of Life. It's just that the brain being an organ takes some time to adapt and develop the new patterns needed. And this seems to happen in a gentle, almost imperceptible way.</p><p>I, too, feel that this is the real deal although I also feel sometimes that this one single look changing the game is just too unbelievable! That the key to end suffering hid in plain sight for so long. But I suppose crazy things happen. If it is true what John says about it and it's effects, we might be witnessing the most unbelievable thing happening for humanity since God knows when. And not just witnessing, but participating in it, and enabling it to happen, if we take part in it. We'll see.</p><p>Seppo</p>
