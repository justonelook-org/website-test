<p>Hello Folks,</p><p>I spoke with John this past week and two weeks previous to that, and he suggested I join the forum.  I've just begun to read the threads, and wanted to write something about my experiences of Looking at the Feeling of Me.  At first, I tried to follow John's directions by remembering what it felt like to be me at an earlier age, but since I have a lousy memory that didn't really work.  Then I tried focusing my attention on my big toe, and moving my attention to different parts of my body, and then on to the Feeling of Me.  That did it!  For 2 weeks, several times a day, whenever I thought of it, I focused my attention on the feeling of me.  A few days ago, when I did that, I had the thought/feeling that "ME" had always been ok, and would continue to always be ok, no matter what.  It was quite an amazing and liberating experience.  I thought I was "home free", and life would be "easier" from here on out.  Ha!  Life is still throwing me curveballs:  my hubby is sick in bed, we've had 2 financial setbacks just in the last half a week.  However, I took John's advice, and instead of letting my mind run away with fear and anxiety, as it would usually do under similar past circumstances, I focused my attention on my breath, and calmed down.  I realize that I have past reactions that are embedded in my psyche, and I need to learn to ignore them and they will pass, and eventually new circuits will be made, or so I'm thinking.'m very excited and honored to be joining you all on this adventure.  I've studied lots of various spiritual & self-help stuff, but this feels like nthe real deal. Love, Barbara (to be continued)</p>
