<div class="forum-post-template-post"><div class="forum-post-quote"><p class="forum-post-quoted-name">tryingtolook</p><p style="margin-top:0;">It’s been several months since I posted here and since I’ve been “serious” about the looking.</p></div><p>Hello Ansley,</p><p>that sentence made me think about what my life was like in the months preceding the first time I looked at myself as per John's explanation. I had become extremely serious about having to understand the very core of all my ignorance and suffering. I started to understand, through repeatedly bombarding myself over several years with the words of Ramana and Nisargadatta, that my true salvation would lie in finding myself. And I tried so hard to push aside all that wasn't me to search for what was supposed to be me. Very serious stuff.</p><p>In retrospect, from that very first time I looked at myself, the seriousness surrounding the understanding of life started to die. If I had to put a number on it, it was about two months later that I actually decided for myself to let go of all effort to better or understand myself. Like John says, once you look at yourself, all else follows naturally. To use your words, the mindset shifted from trying to feel at home in life, to being me inside life - in whatever form that presents itself. But that happened very spontaneously, there was no doing or trying involved.</p><p>To me then, the word success is not really a relevant term in this discussion. From the first time I felt weary of life and started to question the way of the world, to the point I consciously looked at myself, about 15 years had passed. In a spiritual context I don't feel I have achieved anything in all that time besides having looked at me. I have learned an amazing amount of ways to describe the most basic of phenomena : me. But in all that time, life never stopped - the only thing that is changing and growing since the first look is my cooperation with it. Nothing more.</p><p>I've had a hard time finding the right words to get my meaning across so I hope you find something of value in them.</p><p>Wouter</p>
