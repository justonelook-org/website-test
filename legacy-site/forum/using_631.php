ations. I find that mindfulness meditation comes easier now and Im actually eager to do it! I think that is because I can see the great usefulness in it and that is not only because of the looking but also because i spent so many years looking at emotions and bodily sensations that it made the contrast apparent.</p><p>I do not think that you should end your practice, do what you are drawn to do. But I would recommend you to keep mindfulness meditation as a possibility and maybe you will find your self adding it to your practice again!</p><p>Much love</p><p>Rickard</p>
