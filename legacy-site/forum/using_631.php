<p>Dear Sebastian</p><p>I don't want to "play the expert" here, but there are some comments I'd like to make.</p><p>First to your last point, the fear of being disappointed again in the end: I think it's about two years now since I first heard from John and Carla and the "looking" is "with me" since then. I don't exaggerate when I say that it changed my life. Not in its outer form. In a way, everything is the same, but the underlying fear of life is gone. Of course I don't know, whether it will come back at some point in the future, but for the last few month, I was and am closer to life than I ever was. Not "peace and everything is easy", hard things still hard, stupid things still stupid but the underlying threat of being at stake has gone. (I have posted two reports in this forum what and how it worked for me).</p><p>Your first and second point: I can't comment on that. According to John, the way it takes is very individual. I myself give not much importance to the "experiences". They change all the time anyway. The only thing that doesn't change is you ;-))</p><p>Douglas Harding: I read a lot of his writings and did the "experiments" for quite some time. He's a wonderful Englishman with a very authentic and humorous approach. In the last two years I lost interest in his approach (as in all the other "spiritual books"), not because I think it's "wrong", but because the "looking" does all for me I was ever looking for. Douglas Hardings approach to look at the "first person singular present tense" points of course into the right direction - how could it not - but it tends to construct a whole universe of thoughts and theories, I just lost interest a bit, it sounds all so complex and complicated and the "looking" is such a simple act (being not much of a "theoretical mind" this is exactly what I needed).</p><p>Finally: as I said in the beginning: I don't want to "play the knowing one" here, but I can assure you, that "the looking" will "bring you home" and it will give your 10 month old son a sane and loving father.</p><p>All the best into your life</p><p>Thomas Kueng from Switzerland</p>
