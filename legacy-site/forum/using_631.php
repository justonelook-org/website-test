nd be there. I thought God and everyone else abandoned me - and then I realized, I abandoned me too. Now it's time for me to learn how to show up for myself in all ways. And <span class="forum-post-italic"><span class="forum-post-bold">relax</span></span> into that!</p><p>thanks so much Rickard, what a delight you are!</p><p>Jenny</p>
