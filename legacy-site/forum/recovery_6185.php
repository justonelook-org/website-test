<p>Hi Frank. I have experience from NA when I was in the very beginning of my recovery from the fear of life, the psychological fear that we often talk about here at the forum. I have during the recovery process, that is a result of having looked at myself, seen that the root cause of my addiction (or the addictive personality as NA names it) was to be found in the fear of life.</p><p>I remember when I was told in NA that I would have to, to stay out of drugs, follow the program for the rest if my life. I felt deep inside that that was not the case for me. I was of course afraid that that feeling of me trusting my own ability to recover, was one of my old denial's, that so often before had left me in trouble. But not this time. This time it was different. And it would be wrong to say that I healed myself. The looking healed my mind from a psychological fear that controlled my life. Today I only put my trust and faith in my attention.</p><p>But I have the deepest respect for both AA and NA. I think the program itself and the sharing and honesty, is one of the really good treatment there is on addiction. And you should of course attend there as long as it feels right. But I suspect that you will, in time, begin to struggle with the feeling that you want to trust yourself in a way that is new to you. No worry. It is a natural part of having looked at yourself.</p><p>I will always be of help if you want to talk more about it...</p><p>Nice to have you here,</p><p>Niklas</p>
