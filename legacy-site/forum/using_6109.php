<p>When you choose <span class="forum-post-bold">deliberately</span> where you will put your attention, as is always the case in the exercise of focused attention, you are automatically attending from a place of sanity no matter what you choose to look at. It is  the exercise of your <span class="forum-post-italic"><span class="forum-post-underline">authority</span></span> over attention that is sane, and that which will help determine whether the object of your attention is worthwhile.</p><p>In the beginning you will find it hard even to <span class="forum-post-italic"><span class="forum-post-underline">put</span></span> your attention fully on the breath, much less hold it there for any time or determine whether it is a worthwhile object. If you add any requirement to the exercise other than that it be the experience of breath passing across your nostril, it will cause nothing but confusion and hardship over and above the difficulty of holding attention <span class="forum-post-italic">anywhere</span> on purpose.</p><p>If you were lifting weights, it would never occur to you to wonder whether you should have a deep understanding of  the true nature of the variously elongated cells capable of contracting when stimulated before starting, would you? That would be entirely beside the point in the same way that worrying about the nature of what you attend to is beside the point of the exercise.</p><p>Consider this exercise to be more like lifting weights than anything special.</p><p>You may not be completely sane yet, but you are on your way, and this exercise will hasten the healing of your mind more than anything else that I know.</p><p>Have you read the material I asked you to read? If not, please do so before you try again.</p><p>John</p><p>P.S. Most important: don't worry so much. Although you <span class="forum-post-underline">can</span> learn to do this better, you <span class="forum-post-underline">cannot</span> do it wrong. Everything will work out.</p><p>Keep 