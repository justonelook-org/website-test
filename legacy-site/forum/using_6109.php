<p>Bear in mind the act of looking is not magic, and it does not automatically make everything right in an instant. The act of looking merely destroys the underlying, hidden cause of the problem, which is the psychological disease that we call "the fear of life." Here's an explanation of the fear of life:</p><p>The diseased psychological mechanisms that developed from a context of fear and distrust of life are the cause of our experience of misery, hatred of self and others, dissatisfaction with life, yearning for fulfillment, and the entire fog of sickliness that characterizes life for most of us, most of the time.</p><p>Once you have done the act of looking, the mind will gradually clear itself whether you do anything or not, but if you decline to work with your power to  control your wandering attention the road will be much longer, much harder, and much more miserable. It will, in fact, feel much as you have described here.</p><p>You will never have a useful understanding of the value of training yourself to gain authority over your attention without practicing the exercise of Focused Attention that we describe here:</p><p>You can also make good use of your attention by reading the postings in my blog (<a href="../natural/index.html">http://www.justonelook.org/natural/</a>), and attending the webinars we offer on the last Saturday of every month.</p><p>There are many more resources freely available on the JustOneLook website that can be of good use for you. Spend some time exploring them.</p><p>Long story short, it's entirely up to you how long and miserable, or relatively brief and deeply rewarding, your recovery from the fear of life will be.</p><p>And please, never hesitate to ask for help as you have done here.</p><p>John</p>
