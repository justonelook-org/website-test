<p>Yes, I fully agree; I also for quite some time now experiece that state of "nothing special is happening". It is often misinterpreted as feeling bored or lazy but that is not really what it is.</p><p>=====</p><p>When I first did the looking, some memories flared up of moments in the past that felt exactly that way and one can easily mistake it as a feeling of boredom or laziness. For me it were moments like walking home from a party in the quietude of the night or just hanging around in my room on a sleepy sunday. All of these memories that came up had to do with something loud and exciting having come to an end; in my case it was a party or a stressful week, I recall John mentioning a childhood-memory of him coming out of a cinema. These are precisely moments when the sense of "me" is apparent and the noise of the fear is gone.</p><p>This feeling is nowadays very intense especially in the evening, when the noise of the daily activities come to an end and I am just sensing the simplicity of being. But our attention usually goes where the noise is, where drama and excitement is and thats really the shift I and it seems most of us here are experiencing.</p><p>In the beginning I also interpreted this as laziness (my wife still does, haha ;) but actually it is real satisfaction, even love. However, I still quite often fall into the old habit of asking "So what's next?" expecting to experience some excitment and fanciness trying to avoid the backround of peace. This sets in motion the complex, old and corroding machinery of the fear but there is not much fuel left, so it can't run for a long time and it stops again...</p>
