<p>I agree in what Jan is saying Bonnie. Methods for working with attention is very common. You find it in modern Psychology and in Meditation and in many other methods and practices. And that is because there is nothing you can do that don't involve attention. You cant make a cup of coffee without the use of attention. Attention has a key function in human consciousness. Attention is the only thing you have that you can control. But until you have looked at yourself (step 1) you are unlikely to be able to take control over your attention due to an almost universal psychosocial condition called the fear of life. When the fear of life disappears, as a result from the first look, you have a much better chance of taking full control of your attention (step 2). So, working with attention without first having done the first look, is a coping strategy and it is very hard to get results that lasts.</p><p>Good Luck with your practice</p>
