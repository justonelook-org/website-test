<p>Hi Brian,</p><p>Thanks for your reply! I can relate to the spiritual confusion - I have lately come to see, even before starting the looking, that all the babbling voices out there in the world (which say so many conflicting, confusing and confounding things) should just be thrown out the window for the time being. And with the looking, I see that just the seeing of myself is all that's needed - I am right here, so there is no need to "find" myself out there somewhere in what someone else says. So, I am also glad to see all the spiritual jargon and lingo going as well!</p><p>I also appreciate what you said here in another post:</p><p>"My advice to people just beginning the looking would be to be as patient with the process as possible. it is not a quick fix but it is a real fix. Also, it is good to focus only on this for a while. Set the spiritual teachings down for a while... they can always be picked back up again... with a much different perspective. Give yourself the space to experience this work unencumbered by other things."</p><p>That is very good advice - let the looking do all the work, and take a break from all the other voices out there.</p><p>I also relate to what you and Youngmee said about "muscle memory"--the pre-programmed, crazy reactions that continue to happen. This is happening to me as well, but I also am able to see the reactions more quickly, and from a new perspective. As I check for the me-ness of me during or after a reaction, I can see that I am totally unaffected and unhurt by the reaction and unhurt by the emotions involved in the reaction. It is starting to feel ok that the reaction is there, as I'm able to see that I am not only unhurt by it, but I am as fresh and new as I have always been. This is quite an amazing thing to begin seeing. It really takes the edge off the craziness of my reactions!</p><p>And it is wonderful to have this community for support. Thanks so much to John and Carla and everyone else involved for setting this up. I don't really have anyone else in my life who like discussing the looking. It's a marvelous thing to have discovered the looking, and to be discovering myself, and to be discovering that I am everything I always wanted. And it's a wonderful thing to have others to share all this with!</p><p>Blessings and Happy Looking,</p><p>Jenny</p>
