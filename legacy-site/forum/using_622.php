<div class="forum-post-template-post">
<p>I am a long-time spiritual aspirant, with the last two years very intense, reading and following the well-known non-duality teachers. The last year (with the exception of the past 5 weeks since I stumbled upon one of John's videos on youtube), has been quite hellish. The terror invoked by trying to "die before I  die", the continuous wrestling with the "ego", the frustration of the infinite paradoxes, being told I must deeply accept everything as it is, that I must do this and that--all those instructions drove me to the brink of what it felt like to be losing my marbles. I had never had panic attacks before, but I had them this past year. Luckily, it feels like a lot of my "karmic load" had been released during this time--the anger, the hatred of myself and my life--those things had been worn down during this past year. But I was so ready to call off the search for enlightenment, but didn't know how to stop the insanity of it all.</p><p>Then one day 5 weeks ago, I was searching on youtube for what Adyashanti had to say on insanity (because I felt like I was going nuts due to the spiritual search), and one of John's videos came up. I couldn't believe that John also was desperate to stop the search many years ago. What he said made so much sense, that this looking at myself was all I needed to stop holding life at arm's length and to stop feeling at stake and threatened by life. I knew immediately this is what I needed to hear. I have been listening to John's recordings and watching his videos and reading his writings ever since. I can't believe the difference it has made. I feel like I am finally going SANE. The madness of the "search" has stopped. I feel contentment starting to settle in. I still have a little fear or anxiety from time to time, but it feels like it's slowly diminishing. I feel like I am "here" more and more. I can see that things I used to believe would threaten or diminsh me, in actuality have no affect on me at all. It's so comforting to be able to look at myself and feel myself here, and reassure myself that I am still here, unchanged and unhurt by anything that comes along.</p><p>It may be that this looking at oneself is for those who are at their wits end with the spiritual search. Some people I know in my life don't seem interested in this at all, despite my trying to explain it to them. I don't really know what it takes to make looking at oneself interesting to everyone who hears this message. But for me, this was the light at the end of a long, dark tunnel. Thank you so much, John and Carla, for this offering of sanity to the world! I feel like this has been a saving grace for me. I am so grateful.</p><p>Jenny</p></div><!-- class="forum-post-template-post" -->
