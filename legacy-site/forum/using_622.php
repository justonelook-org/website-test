<p>As a follow-up to my post of yesterday, I just want to say what a blessed relief it is not to have to worry about my "stuff" anymore - my stupid mistakes, my thoughts, my feelings, my stumbling about in my humanness. What a blessed relief it is not to have to watch every thought and feeling for resistance, judgement or attachment. How wonderful it is to be able to say "so what" if I'm not perfect (which is every day!). How wonderful to be able to let down my guard against life and myself, <span class="forum-post-italic">and</span> to be able to start seeing the sweetness and beauty of me, just as I am and have always been. Those little glimpses of the purity, freshness and newness of me are the medicine I have been looking for my entire life. What a wondrous thing life is becoming, little by little. Yes, there is still hardship and pain, as John says there will be, but it is more than offset by the reassurance I receive when I look at me, and see that I am untainted and unhurt by any of it. So I encourage whoever is reading this to continue the looking in whatever way you can. Blessings to all,</p><p>Jenny</p>
