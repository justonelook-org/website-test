<p>Hi Nancy:</p><p>I think you question is a good one for sure. One never knows for sure what causes these kinds of things so of course checking with a physician might make sense but it wouldn't surprise me if it is a by product of the looking. My experience has been that I am generally  less vigilant towards life  since I started the looking. At one time I felt that I had to keep everything on my radar less I miss something, make a mistake resulting in, I don't know what.... Reprimand, embarrassment, humiliation. Etc.</p><p>Since the looking.... I am not as concerned about making a mistake, saying the wrong thing, keeping all my ducks in a row and, as a result, I miss things, forget things that  I otherwise, "never would have allowed myself to forget" prior to the looking. Maybe this is nothing more than a  lovely by product of the looking that allows you to give yourself permission to be human and  not have to cross your T's all the time.</p><p>Just a thought.</p><p>Keep well.</p><p>Paul</p>
