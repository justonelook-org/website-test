<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">Nancy Margalit &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1332627126-->Saturday, March 24, 2012 at 15:12</span> 
