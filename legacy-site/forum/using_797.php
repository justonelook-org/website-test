<div class="forum-post-template-post">
<p>I have been doing the looking for three and a half years and now after many ups and downs, I am finally clear that it has brought about some wonderful developments in my life.  I have been so skeptical all along, especially after having hoped against hope time and time again throughout my life, trying this method or that, only to find myself left with the same anxieties and fears I had started out with.  Today I am surprised to find that the resistance and anxiety and anger are slowly dissipating, being replaced with a growing sense of lightness and humor and okayness about the way things are.  My interpersonal relations are smoothing out bit by bit.  This week at work, when somebody projected her pain onto me, instead of it being a messy, upsetting experience, it turned into a joyous experience and an opporutnity for intimacy.</p><p>However, I have been experiencing increasing forgetfulness, to the point that it affects my functioning.  I have been scatterbrained my whole life, but now I feel like things are getting out of control.  I am wondering if this couild be a (temporary) result of the looking.  I also have a very low level of serotonin of which one of the symptoms is a lowered level of cognitive functioning, such as the deteriorization of short term memory. Or am I simply experiencing plain old ordinary dementia?</p><p>I would appreciate your comments or hearing about your experience.</p><p>Thanks,</p><p>Nancy</p>
</div><!-- class="forum-post-template-post" -->
