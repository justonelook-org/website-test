<p>In a recent post, "Forgetting to look in recovery", I commented on the circumstances of a particularly long recovery period.</p><p>I've been retracing my steps recently, and doing some detective work, and now must admit that I've discovered that what I was doing 8 or 9 years ago was not the looking in the sense that we are discussing it now.  In other words, I've discovered that I haven't actually been in "recovery" for 7-8 years, which would be quite a long time indeed.</p><p>When I first encountered John's work back in 2007, I was very much into the traditions of self-inquiry, and, long story short, what I was doing was trying to see "me" without any interference of thought or mind intruding.  I had a very "spiritual" experience in which I followed the instructions of "Who Am I?" quite literally, and encountered a meditative state in which I felt I was seeing my "true self".  In this state I had banished all thoughts in an effort to rid my mind of anything that wasn't "me".  Later I repeatedly tried to reproduce this state, and I thought I was doing the looking, but I wasn't doing it in the way John describes it today.</p><p>The key difference, as I do the looking now, is that I know, whether thoughts are there or not, that I'm seeing what it is to be me, and I'm seeing that I'm always there, regardless of the circumstances, and unscathed by anything.  I've seen that I don't have to banish thoughts, emotions, or sensations in order to do this, and for the last few days I've felt a sense of balance and "always okayness" that just blows my mind.   I'm excited to discover this, and hope the sharing of this discovery might generate discussion and help others who might have approached this work from self-inquiry traditions.</p>
