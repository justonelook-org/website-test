<p>Just to add a footnote to this observation above:  I realized that when I first encountered it in 2007, I used to conflate John's work with that of other teachers whose lives had been transformed through some realization, awakening, or enlightenment experience.  Recently I came across John's "Buddha at the Gas Pump" interview, one in a series that brings the experiences of such teachers into focus on YouTube.  My curious mind caused me to look at some of the other interviews (a couple hundred in all).  One after another, I saw that the experiences of these other teachers contained absolutely nothing that would help me.  Their experiences are absolutely unique and idiosynchratic to them, and trying to change my own life by learning about their lives would be tantamount to watching an interview with Prince or some other unusual celebrity and trying to become like them...absolutely ridiculous and irrelevant!   One thing about John's message that differs entirely from all of these is the account of losing the "enlightenment experience" and then facing life as it is.  Now that's something that I can absolutely relate to.  Ironic that someone who "lost enlightenment" could help me so much more than someone who had "gained it".</p>
