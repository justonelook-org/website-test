<p>Hi, everyone, thought I'd chip in on this, because I turned 70 in Feb 2017, came back to John/Carla June 28 & joined the Forums in early July. And absolutely one thing I've noticed, regardless of ups and downs in the recovery period, is a definite overall improvement in cognitive function. I'd go so far as to say than I'm sharper and more clear than even when I was younger because that lifelong crippling, attention-sucking...fog...of fear isn't there anymore. Not that I never forget anything, but almost daily I just amaze myself when I hear the degree of clarity that now comes out of my mouth, and see the clarity of thought behind it. So while it would have been great not to have wasted a lifetime in fear, I'm glad to be able to say it's way better late than never.</p><p>Those who've read the book or seen the film "To Kill a Mockingbird," may remember the old neighbor lady the kids had to read to while she went cold turkey to recover from drug addiction. Well, some months before finding JOL that became my goal, to die free. Not of drugs per se, because food was my drug of choice, but of the misery my life had been for virtually all of seven decades. I had no idea how that could happen or even if it could happen, but finding John & Carla again via their article "The Fear of Life and the Simple Act of Inward Looking That Snuffs It Out" started me on what turned out to be the way for me. So do the SDA practice as they teach it the best you can, be easy on yourself during recovery, and it will be more than worth it, no matter your age.</p><p>In closing, it was very cool to read that Roed made the connection with neuroplasticity, a subject I'd read about some years ago, in another context, including Norman Doige's book on it. Practicing JOL/SDA definitely brought about the cognitive improvement I've experienced when more than forty years of spiritual seeking made no difference whatsoever and, (correct me if I'm wrong) John's repeated and correct contention that JOL/SDA isn't a spiritual path, though it's been pretty much a miracle to me.</p><p>Thanks, everyone, for participating in the forums so we can all help each other as we're doing.</p>
