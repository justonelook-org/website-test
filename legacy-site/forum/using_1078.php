<p>Hi Nancy, and anyone else who reads this. I've been returning and returning to the Forum, half written postings in my mind, wrote one but don't remember where I parked it <img src="smilies/smile.png" alt="smily">  I have, though, been reading the Forum. When all else is no longer satisfying, and few things are these days that I've previously invested my energy in, I come back here. I read something in a posting the other day that blew on a light of understanding in my mind that was more than worth the price of admission (even though, bless John and Carla, that's free). That's why I keep coming back.</p><p>And then, reading what you had to say about the intense anger you felt when you were with your mother was an exceptional answer to a question i'd been harboring about where my great bombastic rage had arisen from in a challenging relationship. it was like a force unto itself. fortunately, it was expressed, through words, at a distance of 3000 miles, giving both parties the ability to absorb or deflate or deflect the energy without too much harm (one hopes). I hadn't thought about it in terms of 'not holding life at arm's length,' but this makes sense and also gives me a way to not 'blame' myself for these feelings (neither, of course, abdicating me from the responsibility to express them appropriately). it had come to me that all my life i tried to be an 'angel' and not be angry until i finally would explode (regularly, unfortunately, ask my ex and my kids). i guess i'm thinking of what i describe as my 'unraveling' process' includes a more mindful awareness of when these states are building up and learning how to communicate them more skillfully.</p><p>Thank you again, Nancy. </p><p>Warmly, Marlowe</p>
