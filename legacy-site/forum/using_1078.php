<div class="forum-post-template-title">Recovery and the Other Shoe</div>
<div class="forum-post-template-author-date">
<span class="forum-post-template-author-only">Nancy Margalit &nbsp; &nbsp;</span> 
<span class="forum-post-template-date-only"><!--1344781103-->Sunday, August 12, 2012 at 07:18</span> 
</div><!-- class="forum-post-template-author-date" -->
<div class="forum-post-template-post">
<p>I recently posted a letter on the forum in which I shared the fruits of the looking in my life.  At the end of the letter I added, I wonder when the other shoe is going to drop.  The replies that I got were very reassuring, explaining that waiting for the other shoe to drop had become a patterned response to positive events in my life and that I was clearly well on the path of recovery.  I spent some time musing about it until recently when I spent a week and a half with mother.  Although my relationship with her has entailed intense pain and turmoil for me, I have healed a great deal and have found ways to communicate and be with her that are aware and loving.  Well, I was not ready for the level of rage I would feel while with her this time.  I remember having heard once a definition of ego: speed.  Without the defenses I had built so carefully over the years I could no longer contain my feelings as I had been able to previously.  During some of the visit my limbs were shaking, and one day I even had heart palpitations for 6 hours.</p><p>I was horrified by the anger issuing forth from me that refused to be covered up or denied.  The musing about whether the other shoe was going to drop turned into dismay over the fact that here was evidence that the other shoe is never very far away.  But not for long.  Because soon I realized that it was neither my fault nor hers that this was happening to me.  A few days ago, after an imperfect, but authentic time together we parted tenderly and lovingly.</p><p>Perhaps this is what John means by no longer keeping life at arm's length.  Life looks very different when the things that present themselves refuse to be swept under the rug.  Sometimes I sure don't feel very elegant, but I am cognizant of a sense of trust that life is leading me exactly where I need to go.  The visit has produced in me a sense of well-being, something I don't remember having experienced after being with my mother.</p><p>Nancy</p>
</div><!-- class="forum-post-template-post" -->
