<p>Hello fedemart, great to meet you too <img src="smilies/smile.png" alt="smily"></p><p>I think a husband acts as best he can from the circumstances present, and always makes the best possible decisions from his point of view, looking or no looking. In the event he looks and finds he has nothing to fear perhaps his decisions will be more realistic and mutually beneficial for whoever's affected. Or not! A mind free of fear acts out of self-reliance but that doesn't automatically mean the husband will act in any way better  or worse or more or less considerate, only that decisions made will be his own. And that's all that is needed really.</p><p>I hear you about some relationships becoming awkward following the looking. I see a growing dichotomy of thought between myself and other people as well as again and again running in to the fear of others and it stinks. What I've seen though is that nothing necessarily stays the same over time and this looks no different. Relationships are dynamic and work well one day and poorly the next. Over time though I think just by enjoying the deepening of my involvement with my own life, it follows I engage more in others' lives too. By being present with another, instead of our relationship eroding because of dialectics, I find it rather grows and flowers, and not in an abstract "BFF" way but in a human, realistic way, and it's happening because the fear is gone. What is challenging now is to meet people from my past, where they often anticipate me to act fearfully and to handle their expectations but I'm sure it will work out. The need to be liked by others doesn't have the same grip as before, finally making social life manageable and even exciting.</p><p>I don't think the fear can come back in the same way I can never not understand how two added by two is four. It's just too simple.</p>
