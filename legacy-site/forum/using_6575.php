<p>I came back to John's method because I was having anxiety about life decisions, specifically with regards to a potential spouse.</p><p>I was going to describe the specifics, but maybe it is beside the point. My question is: let's say you notice you are loosing your unconscious fear of life, which is great for you because you don't suffer anymore. But how should you go about relationships with people you care about? About decisions that will affect them?</p><p>For example... this is not my case at all, but a simpler story to put as an example. You have a wife who is very depressive, with kids, and you have a lover that you kept in secret for years.That secret made you feel terrible, because you could loose your wife and kids if that affair was discovered. Then you do the inward looking and those feelings are no more, you don't feel worried about the potential outcomes. However, your wife and children could suffer a lot. Do you think about that? If you do, will this empathy recreate fear of life somehow? Like OK, you cannot be touched, but if you care not only about you, but you plus other people (not their untouchable conscience, but their variable selves) then won't you have fear of life again because of this?</p><p>I am wondering wether I should relate at all with significant ones after some time has passed, because I have no idea how I will be if this state is permanent, or even worse... I am afraid my relationships may set me back in my progress. What do you think?</p>
