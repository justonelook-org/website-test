<p>Hey there folks. There's a cat in Brazil that contacted me yesterday. He's been on the forum for several months but hasn't written in because he feels his English isn't good enough. He has what I feel is a great idea. Having people report on their experiences with practicing the SDA exercises and what practical benefits they have obtained. Just give the date you started with the Looking and the SDA exercise and any experiences you have had since then that you feel has come from the Looking and practicing the SDA exercise. I think this is an excellent idea as it will help new people who come to JOL. I have found one of the absolutely most powerful ways to promote a teaching is to have as many testimonials and peoples' experiences with the teaching as possible. So, if this sounds like a good idea to anyone as well, please write in! Ok, well I'll start then:</p><p><p>Lex Samu, 42 male, Queens, NY USA:</p><p>Date I was introduced to JOL - End of April 2017.</p><p>I had found John Sherman's first book on Amazon and the synopsis resonated with me so I bought it. For the first few days I didn't know about the SDA exercise, so I just did the looking. I started the SDA exercise a few days later after going to the JOL website and finding out about it. At first, I did it quite a lot every day. I found it was bringing up a lot of emotional issues too quickly for me, so I backed off to doing it a couple of times a day. I think I stopped for about 6-8 weeks at the end of the summer to early fall as I felt it wasn't doing anything. But, then, I decided to give it another try. I had been doing it usually 1-2 times a day. Most recently, I have upped that to 3-5x a day usually, as I started to feel I was receiving some practical benefits in the last couple of months.</p><p>I have definitely gotten a lot better at SDA since I started at the end of April. I still feel I can go a long way to strengthening my attention though. What I have noticed in the last couple of months is that thoughts that cause me suffering do tend to come up less and less. There are actions that I take, or have attempted to take, in my life that I would like to change, because I feel I am doing them out of the fear of life. The SDA hasn't yet effected these actions, but I am hoping at some point they will. I am encouraged to keep going strongly with this practice now from the results I have already started to see with thoughts causing me suffering 'coming in' less throughout the day. Also, recently, during the SDA, sometimes I get a tremendous sense of well-being that comes over me. Intuitively, I am now strongly feeling that, if I continue with the SDA, I will start seeing more and more benefits in my practical life.</p></div><!-- class="forum-post-template-post" -->
