<p>Hey eversio11. Before I found JOL, I also studied a lot of vedanta techniques and the like and did exercises just like the looking, but surrounded with different belief paradigms. I was also looking for the things you are describing and had a lot of different experiences doing it that way. When I started listening to John talking about the looking, it became a lot more subtle for me. There is also an element of trust involved here in the sense that John recommends that you trust that the 'looking happened' after you do it even if you don't really feel any confirmation that anything really happened. I think the absolute best thing you could possibly do is listen to the clips where John is talking to a group of people and he explains the looking and leads people right along with him in doing the act of looking. I know there are a couple of clips on YT I've heard and I also more recently heard him do it in one of the more recently recorded webinars - say in the past 6 months or so. And he also talks about the 'trust issue' in regards to the looking clearly in his BATGAP interview which can also be heard on YT. When I first found the JOL teaching I had listened to this interview and it definitely made me feel better that something really happened when I did the looking. Hope this helps! All the very best, Lex</p>
