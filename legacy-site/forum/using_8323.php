<p>Hi all, I came across this teaching while studying vedanta techniques, mostly from Ramana Maharshi and Nisargadatta Maharaj. It was this blog post that I first learned about Just One Look: <a href="http://www.beyond-karma.com/awakening/the-sense-of-i-am/">http://www.beyond-karma.com/awakening/the-sense-of-i-am/</a></p><p>What I was struggling the most with was "what is the 'I am' feeling actually feel like?". I understand conceptually that 'I am' is beyond all sensations, words and descriptions - but is there a feeling or sensation that is most closely related to the 'I am', something that will tell me I'm at least looking in the right direction?</p><p>When I practiced the vedanta techniques, I found senses of pressure, vibration, heat, pulsation, etc. when I looked within. As I got deeper into the practice I tried to look past these sensations and just look at my bare self, without any wordly or material feeling, but I grew frustrated.</p><p>I tried the JOL technique recently and it seems to help to practice the awareness on objects / body parts, then shifting that awarness to 'I am'. To me, awareness is sort of like a dense pressure feeling and visually I see 'energy waves' in my mind's eye emanating from this pressure, much like a movie or comic book rendition of energy. As I shift this pressure-awareness-feeling to 'me', the deepest, most inner me, I first encounter the sound of the word 'me' in my mind. This sound then takes the form of a very simplistic human body shape, kind of like an astral body. Again, this is all in my mind / mind's eye.</p><p>With practice of the JOL technique, I sometimes can drop all this mental imagery and get a sense of a void or emptiness. This, from all the teachings I've read, seems to be closer to the real 'I am', so I do hope I'm on the right path.</p><p>So what is everyone else's experience? Did you all go through this stage of mental imagery and feeling sensations? Is there anything concrete you could attribute to your real self, your sense of 'me-ness' or 'I am'? Or is 'void' the closest we can get verbally before ultimately even dropping that and touching the real self?</p>
