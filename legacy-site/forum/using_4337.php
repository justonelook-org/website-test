<p>Hi Jackx</p><p>I was told that I had "major depressive disorder" about 10 years ago, so I took Paxil for a couple of years, before I met John. It made me numb and fat, and my marriage began to disintegrate after I went on it. I got off that and switched to a lexapro/wellbutrin cocktail around the time I first started looking. Soon after I had the desire to get off the stuff. I just felt stuffy and didn't care about anything. I stayed off until I started having trouble doing math in college, so I tried wellbutrin again by itself for a little while, to help me focus. Looking back I can see that it just made me more anxious. Around that time the reality of life-in-my-face started hitting me hard. I stopped taking all medication and started experimenting with food. I stopped eating gluten on a  hunch and found my energy level and ability to concentrate went way up . Since then I've been Juicing vegetables, eating organic, and trying to re-build my broken digestive track after decades of eating food-like products laced with pesticides and god knows how many man-made chemicals. My exercise regime is sporadic, but when I do take long walks with my dog, I notice the difference. It seems that my sensitivity to what I eat has increased, along with the sensitivity I have to people who are steeped in the disease. I've been doing well now for several years without taking any medications. I have a lot of hope, and am happy to see and feel the more natural results that are starting to shine in my life. Depression meds were just another failed attempt at a cure. I do know some people though, who seem to have some really wild chemical imbalances, for which meds seem to help. But I'm glad I don't need them anymore. Hope this helps.</p><p>Best</p><p>Mike</p>
