<p>Hi Jackx, as it is your mind that is actually creating the universe you perceive anything you take will alter the way the universe appears to you. As the "looking" is only the "looking" then I would assume it would make absolutely no difference to that. Some psychotropic drugs do cut out the mind's chatter and so may bring about an increased ease in "Looking". But as with all artificial drug stimulation a price is payed in the body one way or the other... but I guess you know that? In some shamanic rites hallucinogens are used just to introduce the user to the possibility of seeing beyond the mind's limited view and I have even heard that certain addictions can be cured using these substances. There was an interesting case of people suffering from hellish migraines that would occur on a daily basis. Those sufferers able to take LSD once a month stopped getting the headaches... It's sad that so little is know about this class of drugs but I get the feeling they have quite miraculous qualities.</p><p>I  suffered from depression related to a chronic illness that started up when I had a slipped disc, fibromyalgia... It's basically a whole load of pain and a complete drop in energy levels. Not surprising to hear it has one of the highest suicide rates of all illnesses. It completely disrupts your life in all ways imaginable... I had a rough time at the beginning and tried antidepressants but found it made the symptoms worse and made me even more depressed. One of the things with Fibromyalgia is that it makes you more sensitive to any drugs... so early on I realised meds were out for me. So one particular day, a shall I live or die day? I decided to carry on and plunge everything I had into meditation. And it worked. The results were pretty fast in arriving. It was really a great opportunity, in that I really had little choice in the matter and as such had to pull out all the stops. Depressions would lift within a couple of days. These days they hardly touch. Seems that what meditation was achieving was cutting out the pointless inner mental conflict my mind was throwing up. The chronic aches and pains that accompany the illness continue but there is no longer any conflict with this state. This constant conflict served absolutely no purpose anyway, and just helped drain the body of life energy. So now when I run into an attack, I'm really lucky I get long breaks free of it, it's retreat time. Time to slow right down and live an aesthetic life style.</p><p>all the best</p>
