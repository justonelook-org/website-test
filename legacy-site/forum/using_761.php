<p>Dawn,</p><p>I think this is likely to be just the way your recovery is playing out.  Although there are obviously some general effects that we all share, the human mind is inconceivably complex and I imagine the number of possible scripts is infinite.  One of the effects that we seem to share, for example, is the amazing experience of waking up today wondering what yesterday's fretting was all about.  Not that you are fretting here.  Personally, I do not expect to come to any kind of understanding about God as a result of the looking.  Some things are unknowable to the human mind.</p><p>I feel certain that there is something wonderful happening on a grand scale that is far beyond our capacity to understand.  Come to think of it, understandings have for the most part and in the long run not served me well anyway.  At the moment I do not think I could arrive at a fixed idea of a philosophical question if I wanted to!  I think this is a direct result of the looking.</p><p>Here's something I do share with you, I think: A sense of wonder at the changes that are occurring in my life.  If I grab a thought or feeling, as you are doing here, and try to hold it still, it just vanished as if I am trying to hold on to a cloud.  I suspect that these bits are like pieces of a constantly morphing and very intricate jigsaw puzzle.  They might be beautiful or they might be ugly and we can ponder them and create all sorts of "understandings."  But that is an endless task.</p><p>I think our return to the natural state is in many ways a return to the unquestioning wonder of a child.  Yet this perspective does not seem to prevent our functioning in the world as an adult.  Not at all.</p><p>You may read this and think what in the world is he going on about.  I may read it and wonder the same thing!</p><p>Take care,</p><p>Steve</p>
