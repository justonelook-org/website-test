<p>OK... I'm just going to put it out there. I've been looking for about 4 months and I have a question that I'd like to ask you guys. I don't know anyone else who looks at this in the way we are doing...and so I'd like to hear how you feel  about this.</p><p>I know that my language is not as clear as I'd like it to be, so I hope you can read through it. Here goes:</p><p>It seems to me that people look to the idea of God as a solution to their lives. I've heard John say that many humans try to be good, religious, or even appear "godly" in an attempt to appear better to others, to themselves, and mostly to deal with their overwhelming fear of life. (Does that sound like what you understand as well?)</p><p>Lately, when I hear people talk about praying to God, saying that they depend on God, have a relationship and/or dependence on God, or ask and receive a solution from God, (usually calling God by the pronoun "he") I am looking to see -- what is this thing that they are talking about, and that for all of my life I have been calling "God"?</p><p>I asked this question yesterday to my husband and he answered with a long list of synonyms for God, ending in, "you know, it's your higher power."</p><p>When I look inside myself, when I put my attention on what it feels like to be "me," it's absent of any personification. So, when others say "God" or "he" and so forth and so on and I feel judgmental of that, I figure that judgment is a part of my recovery. I am judging them as "not quite there" or "not clear enough" or "lost in rhetoric"... or worse, "wrong." And maybe it's not "worse" or "wrong" but just "what is"... just "what is appearing" (I can't recall John's phrase, but it's something like: "currently appearing.")</p><p>So when I hear the word "God"--I am immediately translating it into this presence I experience for fractions of a second now and then--that I call "me." I know this doesn't make the use of the word "God" wrong -- but it seems so inexact to me. And my thought process/my mind wants to correct it. To look inside at "me" instead of use old metaphors for that presence. And in this recovery, I feel as if I want to correct others too. Yes, I am still wanting to change the world. And I know all I can ever really do is look.</p><p>OK, so that's it.</p><p>With love to all,</p><p>Dawn</p>
