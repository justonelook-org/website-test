<p>I've been trying to put my finger on the changes that have happened more than six years out from the first look at myself. I don't want to be one of those people that goes away and never reports back to this work as I think the community is important. However I do understand the impulse to get pulled, subsumed, into this wondrous life without taking the time for reflection. Self reflection and trying to figure out what went wrong seem almost habits of the fear and not necessary now, a habit that lingers. I also know that I greatly desired word from the other side when I was in my long recovery.</p><p>I now somewhat understand John's loss for words when trying to describe a life without fear. I have a taste of this life. The neurosis and anxiety has calmed down for some time now. When all that is gone, now what? I am going to call it the startle phase. It started with noticing that I am a little more jumping and easily startled by sudden movements or sensations. I've never been jumpy, perhaps a cultivated, cool exterior, perhaps a deadening to the world around me to counteract the craziness inside. Anyway, I became jumpy and easily startled, even though I'm calmer than ever in my life. Hmmmmm. It's taken awhile, but I've realized this startle instinct is a deeper response to the inrush of life. The richness and 'magnificence' of life as John talks about before he trails off at a loss for words. I'm wide open, or wider open, and can feel things like never before, with the paradoxical sense that they don't affect me or hurt me. I'm raw and safe simultaneously. Unmoored, yet anchored. Life seems a balance of continuity and freshness. We maintain our sense of self, our memories and history as the world is reborn in each moment. It seems I am holding onto less and embracing the freshness more.</p><p>As the filters and barriers come down I am less and less in the deadened inner world of the habits of fear. The world around me is enlivened and endlessly fascinating. There is a feeling of rightness, even when there is something wrong externally, and of course much is externally wrong with the world right now, as ever. This feeling of rightness is deep and pervasive from the surface to the bones of me.</p><p>Anyway, that's my best shot at describing the changes brought on by this incredible act. Please hang in there if you are feeling lost and overwhelmed by the recovery. Look for the subtle changes and movements internally and externally. I am one of the many who can say, "if it can happen to me, it can happen to anyone."</p><p>Peace out.</p>
