<p>I just wanted to share a bit about the effects of looking on my life. I was a devotee of Osho for thirty years and all of this experience was incredibly rich and life-affirming.</p><p>The looking for me seemed to be pretty obvious from the beginning, this sense of me just a little obscured by 'spritual ideas' of what I should look like. Anyway I always had a spiritual dialogue going on in my head Osho's words pointers to the truth, etc. going on and on... I must say although I do listen to John a lot he has really succeeded in not turning this into a teaching, I don't hear his voice in my head HA! or if I do it's "just look'. I am the only one who can do it.</p><p>The self-reliance thing just never seemed possible for me until recently. I feel as the looking has really taken over my life, as the fear of getting it wrong lessens I am feeling less and less at stake in anything I express or don't express. It's all just fine and such a relief to feel I tell the truth more.</p><p>Also about money, since I began pledging to this work I am amazed at the abundance in my life. I live on a small pension and recently my brother offered me an all expense paid trip to Florida. My daughter loves to spend time with me since I'm not trying to control her life and she's always offering me gifts and outings. I really feel this trust that all I need will be provided. Isn't money an energy and we need it to keep this alive and kicking?</p> <p>Maureen</p>
