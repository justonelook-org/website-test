<p>I love that feeling or sense of abundance when it comes my way.  Gratitude is always right on its heels.  Sometimes overwhelm is around the corner, but less so these days.  My brother equates money with energy and says that what goes out comes in.  You can read that to mean that the more that goes out, the more that comes in.  I don't know about that, but I do know my relationship with money has softened.  Money triggers, true, but the amperage is mild so that the disturbance is nothing compared to what it used to be.  Years ago I turned the handling of our finances over to my wife, and that kept me at a greater distance.  That would no longer be as helpful today as it was then, thanks in large part to the looking.  And it's not that I'm more financially secure now than then, and it's not that I've gotten clearer about it or more balanced or understanding or anything whatsoever to do with what I think or the ideas and concepts I've developed.  The looking has worked across the board in diminishing the drive to do something about any situation or set of circumstances for the sake of survival or need to satisfy any of my neurotic themes.  Instead, I do because I do, and that's kind of the end to it.  I'm still interested in why I do it, but mostly as a matter of curiousity or habit.</p><p>Maureen, I salute you for your years of work with Osho.  I salute all of us for the journeys through life we have undertaken.  Trimpi</p>
