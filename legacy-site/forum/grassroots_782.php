<div class="forum-post-template-post"><p>What wonderful comments here.  I think it is very useful to consider how the context of fear has dictated our relationship with money.  I have been impressed in my conversations with John and Carla how although they have lived very close to "bust", and continue to be not far from it, they have never appeared "afraid" in the face of this.  Concerned, yes, but not fearful.  For me two places that I find that the transition given by the looking shows up is in the areas of sex and money; two areas of human experience that I think we are particularly crazy.  These day I find that my relationship with money is "respectful", but not fearful, i.e. I am clear that it is an important aspect of functioning in the world, and a necessary element of the creative process, and I am also clear that it exist in "abundance" vs. the perception of it given by fear; that it is "scarce".  There are still neurotic moments in this area of life for me as there are with sex, but they are moments, they used to be not only always there, but appeared to be "true".  It is so useful to notice the difference and notice that it is the absence of the fear of life, an aspect of which is the fear of not having enough, that has diminished.  Fulfillment is present when the fear diminishes.</p><p>Thank you for being here.</p><p>David</p>
