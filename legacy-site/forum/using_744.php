<p>To me, the Looking differs from meditation and similar techniques in that these techniques are for the purpose of finding a relief from the suffering we all experience in varying degrees and circumstances -- and they can help.  The relief (happiness) was the prize and it seemed a few obtained it by accident and a few more by sheer will power, self-discipline or self-denial.  The Looking is just looking.  It is about trying to put this human mind in direct contact with what we are and have always been.  But the Looking is not about straining for a direct "experience" of some sort although to some these occur.  No understanding is required or even can be had.  There's nothing to believe.  Looking inward, away from the cacophony of our human mind, for that which is always there, never changes and has no qualities except it feels like you, will heal the disease that is the root of our distress – of our suffering.</p><p>Now, it is simple but not easy.  Sincere effort is required.  At times it may bring on the very symptoms we so desperately want to escape.  But that will be proof that the healing process is underway and now as we continue Looking, the end is sure.  That's the amazing thing about this practice.  There are not just one or two star students who "get it."  It truly seems that we all will get it.  And here's the kicker: Then instead of fleeing from a life that only brings pain, we will embrace life as it is and for what it is--our most precious, miraculous gift.</p>
