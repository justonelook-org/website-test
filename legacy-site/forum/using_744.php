<p>I haven't been on the forum in a while and now, seeing so much clarity in so many human beings, I am just thrilled to the bottom of my heart. How wonderful it is to see that the looking at myself turns out to bring the same sanity and joyfulness in all people. Wonderful...</p><p>Trimpi, this is my own take on the formal practice aspect. I spend about 1 hour and a half daily in subways. I found this time to be great for just sitting quietly and doing some sort of meditational practice. There have been times when I looked at myself in the same circumstances, but the looking is not something that can be done consciously, continuously, for that long, at least in my experience. The looking is, as John says, just a momentary seeing, repeated whenever possible. And I found, again as John says, that it's absolutely true that I'm always, always looking at myself, I'm just not aware of it. Now as far as mindfullness or other practices go, I consider them quite useful. They can help us relax a bit, release some of the layers of stress, appease some of the crazyness. I use some of them quite frequently, just because I found that they make me function a bit better. Think of them in terms of athletics. If you have a good athlete, if you want to make him better in some area of his sport, you're going to make him sweat and toil, right? That's how our professional and even personal lives are performed - effort to become more skillfull. Now, after you're done with training, you need some diet, some sauna, massage, a good sleep, maybe some psycho-therapy, clean air, supplements, no? That's what mindfulness, releasing, opening practices do. They relax the mind and the body to a deep level. So, they're valuable. But, no matter the strain or relaxation, you are the same. There's an aspect prevailing at that time (like strain or relaxation), but you are here and you are the same nonetheless. That's what makes the looking special and so universal.</p><p>Sitting quietly or counting breaths are optional practices. They are pleasant and, in the end, good tools for a balanced living. You are here anyway, balanced living or not... that's the real jewel. You</p><p>Being judgemental on yourself or others is, to quote John, besides the point. It may go away fast, or not so fast, or not at all. Who knows? Looking at you takes away the feeling that being judgemental or not being judgemental or doing away with judgementalness has any impact on you. And you already know that. So, look at yourself, enjoy yourself. I'm so glad to be with you all in this adventure. Much love and appreciation. Dragos</p>
