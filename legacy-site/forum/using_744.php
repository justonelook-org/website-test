<p>Hi Susan,</p><p>Welcome!  I'm glad you decided to post.  This is a great question and certainly one I struggled with a lot at the beginning.</p><p>I was always an "unsuccessful" meditator too--it took a LOT of effort for me to have any kind of meditation practice, probably because it almost always resulted in extreme self-criticism and feelings of guilt and misery that I wasn't doing it correctly.  So, one of the things that initially attracted me to the looking was it seemed that the "twenty minutes of meditation a day every day" rule didn't necessarily apply.</p><p>Initially, I would just look whenever I thought to do so, as John suggests.  After about six months, I started trying to do the mindfulness meditation he describes for about ten minutes or so and then look for a few minutes after that.  You should be able to find his mindfulness meditation instructions somewhere on the Just One Look site.</p><p>I've continued to do mindfulness meditation because I enjoy it (I think the looking has helped with this) and because I believe John when he says that building one’s powers of concentration (which is what he recommends the mindfulness meditation for) is valuable for the looking and other aspects of life.  I guess it also doesn’t hurt that, for whatever reason, meditation is no longer a struggle.  Although there are PLENTY of days where my thoughts are all over the place, it just doesn't stress me out as much anymore.  So, in short... I don't think meditation is a mandatory pre-requisite, but if you try it again after you've been doing the looking for a little while, you may find that it's a different and valuable experience.</p><p>Hope that’s helpful—I’m still a beginner at this too, but I thought any perspective might be useful.</p> <p>Best,</p><p>Ansley</p>
