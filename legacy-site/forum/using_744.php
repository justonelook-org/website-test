<p>Thanks Trimpi,</p><p>Stopping is a key for me too.  When I began, I downloaded John's book "Look at Yourself" and was immediately drawn to it.  I will add that I was truly desperate at the time.  Eventually I clipped every word pertaining to how the looking should be done, filling a 3 x 5 notepad.  Gradually I reduced the instructions, trying to find a formula that worked for me.  I was used to teachers "revealing" their secrets in riddles, puzzles and mystery and I guess it had not then occurred to me that I had never been smart enough, or something enough, to get what the teachers were trying to impart.  What was there to do but try?</p><p>Still, this was different.  I don't have to change?  I don't have to chain myself to the bed when the full moon approaches?  At that time I was certain that the human mind was defective.  It never occurred to me that it might be damaged or universally diseased.  But I also knew that some few had recovered -- had broken free.  (It is really awkward to try to describe this impression as if I understand the mechanics of it.  I do not.)  So as time passed I came to realize that there is nothing to understand or believe.  I don't have to sell my goods and follow another salesman (I do not mean to disparage intentions) into the wilderness.  I just have to look.  And I get to be me.</p><p>My ‘looking’ has taken many forms. At the moment, my looking usually goes something like this.  I prefer to sit or lie down and totally relax.  Then I think about bringing the attention of this human mind into direct, conscious contact with the “me” that never changes and is always there.  I look but do not “see,” I listen but do not “hear,” I touch but do not “feel.”  Yet I am certain that I am there.  I am the only thing that never changes.  I can see through memories that this is so. I yield to my instinct and intuition to find a way to bring my mind and my “reality” into contact and I try to move my mind inward and away from the source of mental noise.  When I am most successful I am overcome by a smile and then later sometimes by the discomfort of an upset mind as whatever is happening happens.</p><p>But that’s me and that’s now.  I’m just sharing.  You will find your own way.  Like you, I am under re-construction and for me there is much work to be done.  I don’t even know what I’m doing writing this.  But here I am.  Back again.</p><p>One more thing I’ve come to know: This is real.</p><p>Thanks to all,</p><p>Steve</p>
