<p>Your excellent reply raised two thoughts.  First, if anyone has tendencies to be angry, miffed or upset when unappreciated or judgmental toward others (I have great experience in the realms of appreciation and judgment), then the mention of "star students" will trigger a response.  No problem with that, except that if it won't be brought to consciousness it will likely reoccur a lot, thus making the practice more difficult.  This is another pitfall I want to mention.  I spent a lot of time at John's first retreat in judgment of others and missed out on the value of much of what was said.  Eventually it all came home to me, and it wouldn't have been so bad if it was more entertaining, but I layered it over with self-blame and a desire to be "cured."  That I have been a judgmental jerk is no so hard to swallow now.  I know there are a lot of you lying in wait with your judgments, and I would suggest that you be gentle with yourselves and not allow them or your feelings of unworthiness or not being appreciated enough to direct you away from the inquiry.  Another direction, as John would put it, would be sex, drugs and rock and roll.  There's much more inside.  The second thing I'd mention is what you say about "getting it."  I went through est in the mid-80's, and the whole program was a build-up to the last assurance or goal, that we would all get it.  At the end the trainers came around and whispered in our ears that there was nothing to get.  The same can be said of our practice.  There is only stuff to strip away, but nothing (no thing) to get from outside ourselves.    What I think we get out of the inquiry is freedom.  Freedom from suffering, freedom from being so identified with our circumstances that we can be knocked off our pegs, freedom (not the elimination) from the effects of our neurotic impulses.  Embracing presence -- or life as you say -- becomes more automatic and is greased with ease.  Trimpi</p>
