<p>Regarding the persistence of the 'hatred of life', this disgust with human ignorance and insanity will pass sooner and more quietly than you think. It is the child of the fear, depends upon the fear for its nourishment, and will quietly vanish with so little drama that you may not even notice its passing for some time. Fundamentally, it is nothing more than a wrong understanding of an underlying layer of sensation-experience. There is nothing that can be done about it directly, it is an effect only, and it will vanish gradually as the echoes of fear, which are its cause, fade.</p><p>The phenomenon it seeks to understand and clarify is actually empathy, which is identical to compassion, and is natural to human consciousness. The boundaries that keep us apart are actual, but flimsy. When the fear departs, the sphere of personal consciousness reveals itself to be soft and porous to -- aware of and affected by -- the entire content of human consciousness.</p><p>The wrong understanding of the effects of compassion passes quietly, but the discomfort that this compassion causes has no cure other than the freeing of the entire human creature of the fear of life.</p><p>Regarding the question of seeing the conscious activity of looking begin to ease up, this is as it should be. We are really not here to spend our time seeking the barest taste of our nature. Our natural state has always included unending awareness of our nature. The process of conscious, effortful looking really is medicine only, and need not be understood as an end in itself. You will see that you have never actually lost sight of yourself. The looking is medicine. There is nothing ontological about it at all.</p>
