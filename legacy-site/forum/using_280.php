<p>Dear John,</p><p>I have a question for you regarding 'looking at myself'. I have been following your work/podcasts for close on two years and can certainly report the benefits of looking at me. Most particularly I have noticed a great lessening of the fear of life for which I would like to offer you my profound thanks.</p><p>However, the concomitant lessening of the hatred of life' has not resulted to the same extent. I have to admit that I still harbour resentment and disgust with people (humanity as a whole). I am troubled by this... Should I be?</p><p>Could this be as a result of easing up in the process recently? In other words, is it important to continue to keep looking throughout one's life in order to continue receiving the benefits, or could one stop without losing those benefits already accrued?</p><p>C.L.</p><p>(Message received on December 31, 2010)</p>
