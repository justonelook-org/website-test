<p>I haven't experienced any significant changes or increased sensitivity to the suffering of others. I have always been sensitive to this aspect of life, and it's probably why I went into clinical social work. However, I think there may have been a shift in the underlying energy which creates the sensitivity. Before the looking I was in pain myself and could see that pain in others. We shared the pain, or my pain was just spread out over a larger area. Since the looking, my suffering has been reduced dramatically while my sensitivity is about the same. On second thought, it's not the same. I am not overwhelmed by the suffering of others to nearly the same extent and it doesn't trigger my own suffering. I have more space to look at it, be thoughtful about it and to try to see if I can help, or not.</p><p>The sensitivity has always been a struggle in my personal and professional lives. I don't think I feel as much pressure to try to help people as I did in the past. I realize people have to go through their own suffering and I try to help where I can. I'll try to tell people about the looking, especially when they are deeply suffering. I just did this with a work colleague and she seemed responsive to it. I've seen the looking take effect in the lives of several people I've been close to and it is very satisfying to watch the changes in their lives. In this limited way I understand why John and Carla are so keen about their work. It's satisfying.</p>
