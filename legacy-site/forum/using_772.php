<p>Hello,</p><p>Please forgive me for using this space for sharing this but it is very painful and I hope that in speaking it out loud it may help. I also have a question related to this.</p><p>This weekend I took a workshop. At the end of this workshop I shared some very personal details of my past. Afterwards I felt a deep sense of being exposed, silly and terrible shame. The process of looking at myself seems to be working in me, however, in this situation the shame has overwhelmed and short-circuited my ability to look, think, concentrate, anything. I suppose it must be my most extreme vulnerable underbelly of fear.</p><p>Have others found that in the process of looking at themselves, external events appear as part of the process?</p><p>What complicated things was that at one point on the second day (prior to the shame-producing events), I experienced a complete dropping away of myself as the physical, mental and psychological entity as I was walking up some steps. For a moment I couldn't breathe or think and my 'apparatus' became as if it were transparent (not literally, but in the sense of hmmm, words hard to find...merging or redundant). It was very shocking. And even now, the day after, I still feel shocked by both things.</p><p>I know to go back to the looking. Thanks for the space here to share.</p><p>Emma</p>
