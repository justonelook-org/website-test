<p>That's an authentic sharing  The energy of shame can be disabling, as you found out.  But that's all it means:  that you were paralyzed in the face of it.  Some people turn beet red; others stuff it and say they deserve to be shamed.  I like your statement that is is the "underbelly of fear," but in actuality it's probably a manifestation of the "not good enough" tendency most of us have been plagued with.  Shame was always there as the most virulent aspect of that theme.  You can count yourself fortunate for having experienced it so powerfully, and yet you're still standing, unaffected.</p><p>As to your question as to whether external events appear as part of the process, they are going to transpire with or without the looking.  But you may find a greater sensitivity to those events, and because you have replaced control with honesty, you are more likely to find yourself in direct contact with the things you ordinarily would avoid or try to manipulate.  I hope you continue to meet them head on.  Nothing can hurt you.  Trimpi</p>
