<p>Hi Emma,</p><p>Please know that you are not alone in these feelings. I, too, have gone through some really awful feelings, including deep shame and guilt, hatred and terror. And now I can see in 20/20 hindsight that they were old buried feelings from the past that I was finally able to face and release. You can count it as a very good sign that your recovery is underway. Also, it's sort of like the feelings do themselves - the shame feels ashamed, the fear feels afraid, the guilt feels guilty. You experience what the feelings do as they pass by. At some point in your life, the energy of them got "stuck", and now they are being presented once again for acknowledgement. They will release themselves as they are simply acknowledged. There's really nothing else you need do with feelings except acknowledge them. You don't need to dig them up, or try to hurry the process. They will show up as they are ready. The process will take care of itself now that you are self-aware enough to acknowledge them as they show up.</p><p>Don't worry about being temporarily unable to think in these situations. Just acknowledge how it all feels as it comes up. And as John says, the recovery process is idiosyncratic and it'll go how it goes. The medicine of looking will work in its mysterious way, and these difficulties will come and go.</p><p>And yes, external events do appear as part of the process. The recovery process will bring up those circumstances which have previously caused you fear, because in the restructuring process, the old mental processes are being dismantled, and this can wreak all kinds of havoc, mentally and physically. The "dropping away of yourself as the physical, mental and psychological entity" is an interesting state, but it was a passing state, and John would say it was of no consequence. It didn't hurt you or help you, and neither did the feelings of shame or being exposed.</p><p>Lately, I notice just how much fear I've lived with. I can see how even the most minor thing can bring up feelings of being at stake. I'm able to recognize this more and more quickly, so I'm able to acknowledge the feeling more and more quickly. I'm pretty sure this acknowledgement of feeling at stake assists in the healing process.  And I'm sure you can see how shame stems from feeling at stake. Feeling at stake stems from the belief that something "bad" will happen if I don't do, say or think the "right" thing – I will be diminished in some way, or abandoned in some way, or punished in some way. This insidious belief gets stuck deep within our psyche, making life more hellish than happy. Like John says, we come to the conclusion that life as a human just sucks.</p><p>By some miracle, the Looking at what it feels like to be me and you is the next step in our evolution. Thanks so much for sharing, Emma! So glad for your presence here in these forums!</p><p>Jenny</p>
