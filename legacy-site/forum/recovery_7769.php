<p>Sorry you are still struggling, Roger. It can simply take awhile. That has been my experience. One thing that pulled me through was to look for small changes and think about how different my life is pre and post looking. No matter how bad it got it was never worse than before the act of looking and there were many things that improved. I have kept a journal during this time which I find helpful to go back and look at how I used to be and realize that many changes took place without my noticing. Many changes. I still have days where I feel nothing has changed, my latest symptom is to feel flat emotionally, not bad, not great just kind of blah, like I can't get excited about anything. I can get depressed about this or mildly annoyed, but then the next moment I am throwing myself into something I really like and get all involved in a new curiosity or pursuit.  Any more, if I feel nothing has changed, all I have to do is think about the hell I was living in every day before the looking. It takes a split second for me to realize so much has changed even though I may not be where I want to be at any given moment.  The mind is a tricky thing and needs to be held accountable sometimes.</p><p>So, I would suggest writing it out. Think of all the positive changes that have happened. Think of all the things you are grateful for. Write them down. Share them with us, I'd love to hear them.</p>
