<p>Emma, I look on it like a passing phase.  As such its power is benign.  It is, like you say, kicking you instead of you kicking it.  Experience, even thoughts and feelings and emotions, pass through us.  The struggle to get rid of the things we don't like about what we experience signals suffering.  Those control mechanisms you mention aren't the problem and are not part of the solution, and I wouldn't be too concerned about the whys and wherefores of their appearance in your life.  They were here; now they're gone, except for their shadows (trails if you prefer).</p><p>It never hurts to see who you have become, pleasant or not.  I just don't put much stock in the findings, because they too come and go.  Nothing is stable except you.  For once in our lives we can look for that which is stable.  What a relief and a blessing to be able to come back to that which doesn't change!  Even if the looking doesn't produce the miraculous change promised (the end or diminishment of misery and suffering), it's something we can do without feeling like we've failed or can't do it right.  And if those feelings do arise, we'll know they come and go like everything else and look when we're ready again. In the meanwhile, we'll do the best we can with our addictions.  Soon the decisions as to how to deal with them will become easier, whether or not the mechanisms you mention collapse.  We'll hold on to the ones that seem to work best for us and dump the ones that don't.  The difference after practicing the looking is not that the choices we make will be right but that they will be more self-evident and easier to make.   trimpi</p>
