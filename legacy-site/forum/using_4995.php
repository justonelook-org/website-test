<p>Hi Matt,</p><p>In response to your post: "Recently I've had a lot of stress and this has insidiously plagued my mind with old bad thinking habits and thus caused anxiety to resurface. I must of dropped my guard."</p><p>You may be aware that your guard referred to here is just another version of a response to the fear of life: "I must watch my thoughts and apply the right fix or something bad will happen." I did that for many many years until finally I just decided my thoughts are not me and most of the time I simply don't take them personally or seriously. (My inner guidance once put it this simply: Don't believe any thoughts. Not even this one!) Kind of like if I am listening to a radio station that has an agenda to stimulate fear, I don't make a judgment in the sense that I think they are wrong and shouldn't be on the air. (That brings me back into fear of life.) I just don't stay and listen, I change the station. I change the station to noticing, experiencing.</p><p>Noticing moment to moment experience does not require thought. But it does require attention and STAYING with life in the raw, so to speak, rather than jumping away from it with some distraction or mental story line. For example, I noticed that I have an old, emotionally charged story line of abandonment, of love that didn't stay. Recently, with more practice in attention and experience with staying, I became aware that I abandoned my natural self and conveniently projected this abandonment onto others, becoming distrustful. THEY didn't stay. A big change occurred when I realized that actually I am the one that jumps away--from myself and others--because of my great fear of life. I am the one whose love, my love, doesn't stay. I don't trust MYSELF to stay. Even now when I notice I am uncomfortable with something in my life, anything from fear, anger, hurt or physical pain, my first impulse is to get away from it. Since my choice is to stay with life rather than jumping away to thoughts, I will ask myself, "Can I be with this?" Often the answer is, Yes, and I stay until the energy changes. Sometimes the answer is, "No," and I let that be ok for this time.</p><p>I hope this was helpful. Lera</p>
