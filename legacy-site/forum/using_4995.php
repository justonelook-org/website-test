<p>Hi Matt,</p><p>I stumbled onto John on the internet about 6 years ago. Since then I have had experiences like the ones you mention. At first I was exuberant. I latched onto the idea that "nothing works" to remove the quiet desperation (fear of life). That became my mantra: "Screw everything. Nothing works. It's all a waste of time." I was happy to believe in the ultimate non-belief. My new philosophy carried me for a time, as did the unique conversations with John and others who had tried this. I was hopeful and tried as best I could to look inward and carry on, in spite of the feelings of anxiety and depression that would come up. I also had moments of absolute blissful feelings, thinking that this was what it was going to be like all the time, once everything turned out well in the end, as I was told it would. It was about three year ago that I got a first glimpse of the underlying feel (it's not really a feeling) of safety, that permeates everything in my experience. I was looking up at the stars after an astronomy class one night, and a new experience blinked in, subtle and faint, like the North Star. All I can say is that it was the truest, non-event of my life to that point. Anxiety, depression, chronic self-deprecation continued. But there was a new idea now too. I have watched many new ideas come about since then. One of which is that some things actually do work now, and that a lot of the stuff I did years ago can be helpful: like the mindfulness training I did 20 years ago. I notice that it is easy for me to move my attention, and that there is much relief to be had in doing it. John told me a few months back that things "might get worse." I was a little miffed to hear that from him, after 6 years. But I'm glad he said it, because I decided to step up on the mindfulness practice and keep in touch with these forums. So yeah I can identify for sure. I still get depressed and have anxiety. But sometimes a new idea pops up in the midst of it that says: "so 