<p>Hi Matt,</p><p>In answer to your questions. No. Yes. Yes. Yes. Yes and Yes. I feel and understand the movement of old hamster-wheel, habitual pathways that produce anxiety and depression. No one can stand exactly in another's mind/body but I can certainly identify with you. So many of my patterns have <span class="forum-post-italic">beat the dust</span> that I feel confident to respond to you. I first started listening to John in 2007 so you can see it hasn't been fast and easy for me, but I am now definitely more intimate with life. I still have thought-emotion patterns appearing but most have a short life, like snowflakes falling on a warmer earth. Recently, one of the old thinking-feeling patterns came up in a dream. On awakening, I didn't feel anything needed to be done about the pattern in the dream as it is hardly noticed in my daily life. However, my mind attached to the fact that it was still in my dreamworld at all. I went, "Oh. No. It's still here after all this time. Will this never end?" I was disappointed and/or depressed for a good hour or so as in, "Just when I think it is all gone, it comes back again." This disappointment seemed so familiar, so me, that it took me an hour or more to realize this disappointment was just another pattern. You see, I could remove my attention from the particular pattern in the dream, know it as <span class="forum-post-bold">not me</span>; but disappointment (perhaps closer to the root) was still being believed as just the way life is, still being accepted at face value as something I needed to do something about or just throw in the towel over. When I realized, with a smile, that the disappointment, in and of itself, was just another of those old belief/feeling/behavior patterns, it was gone. Being close to the root (if it is), I know disappointment will pop up again but I am not as likely to believe it next time. And so it goes.</p><p>You said, "but the inner force is so compelling it is not easy to dismiss and stay focused on the present state of 'me-ness'." That was the case for me in the example above--for an hour. That is the big point. It's about seeing a pattern for what it is. With practice in moving attention, separating thought-feeling patterns from attention becomes much easier and faster each time.</p><p>I offer John's suggestion to practice training your attention on a regular basis. Start with the commitment to practice just 10 minutes a day and add a few minutes when you feel able to. After a few weeks or months of regular practice you will notice a difference. You may find that you will think to do it several times a time after you start practicing. This practice was so effective for me that I recommend it for everyone daily not just when something is up. It speeds up the process dramatically.</p><p>Lera</p>
