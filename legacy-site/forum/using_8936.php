<p>A heartfelt welcome, Lindala1. The point about the first look is not so much the actual experience of the me-ness. The sense of "me here" doesn't have much of a sensation to it and is therefore difficult to experience at all. It is the momentary focus of your attention on it that counts. That fleeting moment sets off a chain of events that will set you free eventually, even if you don't feel anything. I'd strongly suggest to have Carla lead you through it, just to make sure you do Step One without making it too complicated. The fear of life is a trickster. It's going to do anything to keep you from touching yourself with the beam of your attention. Unfortunately, generating spiritual concepts that might appear to be helpful but are not is one of the tactics that it comes up with whenever it reaches for its toolbox.</p><p>I have a spiritual background too, and the only way to actually do what Carla and John ask us to do was to have them do it live with me. There were so many concepts about this and that and the other thing that I just couldn't follow any simple instruction at all without having an inside debate about its true meaning.</p><p>You can request a private session here: <a href="../events.php">https://www.justonelook.org/events.php</a></p><p>I hope I'm not making to much of a racket or preaching to the choir here, but when it comes to Step One I'd rather be safe than sorry.</p><p>Felix</p>
