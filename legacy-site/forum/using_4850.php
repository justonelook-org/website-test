<p>Hello Matt,</p><p>What I referred to as 'mindfulness' exercises are better thought of as exercises in focused attention to distinguish them from the more spiritual meditations known as mindfulness in the Buddhist community.</p><p>You should be aware of the fact that there is nothing spiritual about what we do here now.</p><p>In a nutshell, we have come to see that the underlying cause of all human neurotic misery consists in the creation very early in life - perhaps at birth - of a psychological context that sees life itself to be untrustworthy, deeply dangerous, and requiring constant defensive monitoring lest we be swallowed up in its treachery. From this silent context arise all the psychological mechanisms that constitute the mind/personality of the humans afflicted with it. And judging by the state of the human world, it seems that almost all of us are.</p><p>We have also come to see that a very simple act of inward looking in which the focus of attention is deliberately brought into direct contact with the faint sensation that is recognizably and undeniably the feel of me, the person, will instantly snuff out that context and bring about a fresh start in the mind.</p><p>The existing, fear-driven psychology does not, of course, magically disappear, nor does a new, clean psychology magically come into being in its place. What does happen is that the old psychological mechanisms begin to fall away concurrent with the arising of a new and healthy psychology. In the beginning of this process, considerable confusion and mental misery often appears, but over time the mind fully heals and the personality goes sane.</p><p>It is during that period of recovery that the development of skillfulness in the use of the power we have to control our attention is most needed, and working with it from the start will serve the individual personality very well for the rest of life.</p><p>The more seriously you take the work of getting a clear understanding of the uniqueness of this approach, the better off you will be and the more fully you will realize its worth.</p><p>Along with the piece on focused attention, I would recommend that you begin by reading a paper written by a psychiatrist and two mental health professionals, which you can download here:</p><p><a href="../articles.php">https://www.justonelook.org/articles.php</a></p><p>Welcome to sanity.</p><p>John</p>
