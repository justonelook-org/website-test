<p>Yes, Marlowe, you are misunderstanding me, but it is probably my fault.</p><p>I have grown tired of explicating, over and over, every aspect of my view on matters such as this, and my response to this question was meant to focus as sharply as possible what I understood to be the specific question, which I took to be how I decide where to put my attention in my day-to-day life.</p><p>My reply was particularly in response to the writer's apparent confusion as to the usefulness of attending to states such as empowerment, loneliness, and so forth. From my point of view, attention to these matters is completely useless, since the states themselves carry with them no particular self-definition. The definition of these states as loneliness or empowerment is merely a result of judgments based on the present context. That's what I meant when I suggested that he put "attention on what you are doing instead of on the thoughts about what you are doing".</p><p>In day-to-day life, I attend to what I am actually doing, which includes practical thoughts pertaining to what I am doing. It does not include thoughts about what I am doing.</p><p>I have no interest whatsoever in curtailing the mind, a project which I see to be a symptom of the fear of life disease. The human mind is to me one of the more glorious results of evolution and the defining characteristic of human life.</p><p>I see the simple mindfulness exercises, as well as the deliberate effort to attend to what needs attention, to be, as I said in my reply, mere means to the end of 'effortless sanity and self-reliance', in which the intelligent use of attention is spontaneous and the human mind is fully engaged in the unfolding of life.</p><p>Let me know if this makes sense to you.</p><p>John</p>
