<p>Bruno,</p><p>In your recounting of the story about the terrible fate that befell the friend of your friend, you report thusly: "And I refused to go into the excitement of the horror as my colleague was expecting."</p><p>Did you think through all the ramifications of your decision to respond as you did, or was your response just a matter of fact, expressed spontaneously?</p><p>Yes, doing requires decisions, but in a mind that is free of the fear disease those decisions often arise free of thought, with attention naturally resting on what you are doing, and just as naturally acting with self-reliant intelligence.</p><p>Most decisions are like that, obvious and spontaneous in the moment, requiring little recourse to discursive thought. And those that do require thoughtful consideration wait for insight to arise with no more effort than those that do not.</p><p>Working with the mindfulness exercises strengthens and makes more available the fluency of a self-reliant relationship with the sometimes wild unfolding of life.</p><p>So, yes, do not fear wrong decisions. They may happen, but you will also find no interest in defending them and holding on to them. When they are revealed to be wrong, what's needed to correct their effect will also naturally arise.</p><p>Let me know if this helps.</p><p>John</p>
