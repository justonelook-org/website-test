<p>It's an interesting question. I'm not sure self-worth is the reason for radicalisation. I believe it's fear. Fear drives oppression and fundamentalism, and all the rest of it. Fear robs you your self-worth, too. I see it as part of the identity project and a buffer against threats. It seems that fear is also behind the dangerous combination of lack of self-worth and a sense of supreme superiority in some things. The whole issue is mechanical and automated like most of our reactions to the world.</p><p>In my youth I found consolation in faith, a sense of security. It's based on simplifying complicated things into rules that are seen as divine in origin and hence beyond questioning.  I never was a fundamentalist but I can understand how it gives you a strong sense of security, especially when your group is big and/or has a long history, like in Christianity and Islam, for example. For a scared person, it can be the most valued commodity.</p><p>I believe most people suffer from lack of self-worth and are scared. If looking clears away the fear the issue of self-worth should become marginal.</p>
</div><!-- class="forum-post-template-post" -->
