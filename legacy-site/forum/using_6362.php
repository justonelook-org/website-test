<p>The tragic events in Paris brought me back to this thread, so let me try to get into this topic again...</p><p>Well, first when we think of self-worth the term self-esteem comes to mind as there is a tendency to confuse the two. But I see a fundamental difference between them.</p><p>Self-esteem is drived from my assumption of how other people evaluate me which might aswell be inaccurate because you never know what people really think of you. So Self-esteem is bound to my identity as a member of a family, group, society etc. and thus tied to the collective values of the people I'm surrounded by and most importantly the value I give to those external judgments.</p><p>So we always have to struggle for your self-esteem.</p><p>And value could be seen as the quantity of attention I pay to a given perception. Now Self-worth refers to an INHERENT value within me that is NOT derived from external judgments but is what it is and remains unchangeable.</p><p>So when we cease to pay attention to the natural sense of self, we fall into dependency of peoples attention. I'm still unsure how this really happens, it's really a strange phenomenon.</p><p>And this is where all this madness begins...everyone trying to pull attention to him/herself to compensate for the lack of a natural self-worth which is the inherent dignity in man (in German the word dignity is directly linked to the word value or worth).</p><p>So what i feel we do here is to regain our dignity as human beings, rediscover our true value by the simple act of feeling our natural sense of Self.</p><p>One Love...</p><p>***************</p>
