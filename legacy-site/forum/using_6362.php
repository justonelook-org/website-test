<p>If a person lacks the sense of self-worth he feels powerless, helpless which makes him fearful. He thirsts for life, for power but is afraid of it at the same time. Now if suddenly the opportunity arises for him to taste this power in himself, this raw force of life in its primal form he becomes drunk by it, overwhelmed by this sense of power. He becomes fearless; he is no more afraid of anything, even death cannot scare him and he begins to rape, torture, slaughter and even walk into his grave in an act of suicide-bombing. No he has no fear - he feels powerful, he feels he is now somebody.</p><p>There seems to be an underlying sense of being threatened or in danger but fear - I think that's not the appropriate term.</p><p>It appears to me that there are two primal symptoms of the sense of being in danger, two sides of the coin: fear and anger. Fear can make you intellectually smart, a strategist with low sense of self worth while anger gives you force, power and an over-inflated sense of self worth.</p><p>This is how I see it at the moment.</p>
