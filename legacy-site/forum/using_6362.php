<p>Recently I watched a discussion on TV about the phenomenon of migration and the danger of religious fundamentalism. It was an interesting talk with a high-level politicain, a social worker, a comedian with migration background etc. It basically dealt with the dynamism of individuals and groups being excluded from society and as a result of that becoming a threat to that society. They all, without exception, came to the understanding that the cause of it all is the lack of a "sense of self-worth" within the individual, who then turns to groups or rather ideologies that promise to fill that gap; these groups being often spiritual/religious and sometimes radical in nature due to the pent up anger and fear of the oppressed.</p><p>I personally found that talk very interesting due to the fact I experienced that in my own life (and still struggle with it) and also because we are witnessing at the moment a mass migration here in Europe that presses this topic to the forefront.</p><p>So I would like to discuss that "sense of self-worth", what is it and how is it related to the act of looking ?</p>
