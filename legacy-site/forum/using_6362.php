<p>Seppo, by natural self-worth I actually mean the sense of "me" and the recognition of it's unchanging nature and worth. And where does it come from...it's like saying where do I come from...honestly, I don't know... but I know I have "devalued" myself as a reaction to experiences of humiliation, especially in childhood.</p><p>So, in regards to the acts of terrorism I see humiliation as one of the main causes. That basically was the reasoning of the discussion that I mentioned before, which was focused on the situation within a country but it equally applies to the world as whole, I think.</p><p>I mean, the western powers (mainly USA and France) in the last years were engaged in forcefully destabilizing the entire middle eastern region and thus have been humiliating the people by confronting them with "superpowers" they couldn't cope with and that stirred up in them a sense of helplessness. Not that they were free from the root cause of human misery prior to that, of course not but what makes things escalate now are in my eyes acts of humiliation and the reactions to them. Naturally, they are trying to get out of that state of helplessness and shame by acting their anger out. But doing so however, they obviously increase the misery as they humiliate more and more people from the "other side" (at the end of the day there is no "other side"!). Now planes are setting out that do the same thing and the terrorists will certainly not stop their attacks. So here we go into the cycle of destruction and these acts of revenge just multiply and perpetuate that spiral of self-erosion.</p><p>And for me going through the recovery actually has been confronting me with personal experiences of that same nature, so to me it's an essential part of my recovery to recover my dignity without taking it from others.</p><p>Peace</p>
