<p>Hello everyone,</p><p>I've lurked on these forums for quite a while--enjoying the thoughts and writing of those who have posted here tremendously.  The support, encouragement and focus of these boards continues to amaze me.  So, I'm going to de-lurk and give my report.</p><p>I've been doing the looking now for almost a year and a half, although I don't think I was particularly serious with it for the first six months or so.  I have never had a breakthrough "aha!" moment or a sense that I have "seen" what it is I'm looking for and in fact, I think the fear of life is still as present as it ever was, if not more so.</p><p>And yet.  There are rather cataclysmic changes happening in my life right now that I never would have dreamed that I would be able to bear without bone-crushing anxiety, and instead I find myself feeling (most of the time anyway) that things will work out.  I never would have thought that this would be possible a year ago.  As I said, the fear of life is still present and often seems more intense than it ever has been  When I hear John say as he sometimes does, that the fear of life goes quickly, I usually shake my head and wonder what his definition of "quickly" is!  Or maybe I'm just a tough case.  That's definitely possible!</p><p>I do the looking often and try to do the mindfulness meditation for 10 minutes or so every day and the looking after that.  I find when I think of doing the looking during the course of the day, I will try to do it (usually I'm in the middle of some activity like doing the dishes) and it's at these times that I get particularly frustrated that I don't see "it", that I'm doing it wrong, that I still don't know what I'm doing or what I'm even trying to look for.  It seems that I go through periods where the looking seems to come naturally and it's "easier" to do (and this usually happens when I've been listening to the podcasts or watching the videos more frequently), and then there are days where it's like hitting a brick wall every time I try to look.</p><p>On some days, the energy of seeking, the overwhelming feeling that I need to do SOMETHING to fix myself and my life, is still present and revs up, like an enormous engine straining, but the car is in park and there is nothing to do but notice the feeling.  It is a tremendous relief not to be trying to find the next spiritual fix-- the next teacher, the next book, the next class.  I'm broken from believing that there is any answer (external or internal) besides the looking.  It's not even a choice to stay focused there-- I just know that there's no hope for me other than this.</p><p>So that's where I am.  I would love to know if anyone can relate to this or remembers this stage in their own process. It is such a comfort to know you are all here.</p><p>Much love,</p><p>Ansley</p>
