<p>George, I love to think, especially when I'm at work.  It doesn't interfere with stopping and looking when I intend to do so, although I must admit it does have a certain momentum that carries forward even when I want to leave it alone.  Your real question has to do with whether thinking is counter-productive to the awakening process.  My answer may seem oblique, but here it is.  The drive to do something about that which you feel goes against the grain of self-improvement diminishes with the looking.  Then your resistance to thinking lessens, and you actually think less, or at least you think when you decide to engage your thinking mind.  So it's not that thoughts are bad or are to be avoided; rather, the fixation or compulsion to seek a more improved version of yourself eases off, and you experience life with ease.  Meanwhile, maybe you can enjoy the ride that wild horse of a mind takes you on.  trimpi</p>
