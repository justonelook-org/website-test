<p>Hi George,</p><p>The way I see it, it is not thoughts (or mind, ego, emotions, or mental activity) which is problem. It is the context of the mind affected by fear and its effects, and the recovery from this, which can seem to make thoughts sometimes troublesome. And I agree with you that the understanding that thinking is not inherently a problem is quite freeing.</p><p>In my experience, it's fine to use whatever tools we want to during the recovery process. John often suggests moving attention to the breath. I don't use this technique very much. I use a "letting go" technique where I simply observe what's going on, allow it, and in doing so often can let go of it. So I would say that whereas it is true that thinking is not inherently a problem, it's also fine to use the Buddhist technique you speak of if it helps during the recovery.</p>
