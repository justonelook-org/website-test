<p>Context</p><p>I'm a beginner as well, and I love thinking, too, but when it's gets down on a very depressive and self hating path, I don't love it at all. And it does. Often. And I don't feel in charge of it. I do practice diverting my attention away from it with a deep breath, or several. But I get to do that 5 times a minute and it seems like a losing battle. I don't seem to be very worried about it anymore, though, which seems something like a result of looking might be. It seems to be getting easier, too.</p><p>I had this thought that if I can't really control it, and it's going away soon, why not just let it indulge itself knowing it won't be there for ever. It seems like a more relaxed attitude. Even enjoying it while it's there because soon(er or later) it won't be there anymore. Like Trimpi's wild horse. Not that it feels very enjoyable when it happens.</p><p>The no-thought position makes sense if you identify thinking as the source of all evil, but if it's the fear of life, and it's done away with, then thinking or no-thinking is an effect and not something to be too concerned about. I come from the background of Krishnamurti's teachings (and a bit of Tolle) and I bump into this when my Facebook friends quote Krishnamurti, and I've noticed how my view on it has changed and how I get a bit annoyed at it, however beautiful the insights and teachings they offer. I don't know if they are counted in as non-dualists, but both K and Tolle regard thinking as a problem (not to mention ego!). It makes sense if you confuse a cause and an effect. They don't follow this causal chain to the root, which is the context of fear, in our view here.</p><p>Seppo</p>
