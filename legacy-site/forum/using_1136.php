<p>thoughts</p><p>One of the most interesting and useful perceptions I have had over the course of the looking has been the recognition that thoughts are just thoughts, not "Me thinking", just thoughts occurring.  They are not accurate or useful most of the time and often they are disturbing and distracting.  When I notice that I am suffering, I notice the thoughts occurring and see that they are just thoughts and not useful, I then move my attention to the breath or to anything in the now that is useful.  Over time this appears to take the energy away from useless, negative thoughts, and they gradually depart.  Unfed of attention, they die off.  I like the line in the wizard of Oz  "Pay no attention to the man behind the curtain", when toto pulls the curtain back and exposes that the great wizard is just a simple man who is as lost as Dorothy and her friends.  This reveals that what we pay attention to matters.</p><p>Love.</p><p>David</p>
