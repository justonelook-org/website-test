<p>As a therapist, I'm very interested in whether the act of looking ameliorates more serious psychiatric diseases.   My own depression and anxiety were serious enough and the looking was nearly 100% effective with anxiety and about 80% effective with depression (so far).  This is far higher than the 30-40% effectiveness rates of drugs.  Posts like the above suggest that there is hope that this is true.  I work with kids and have had seriously depressed kids look at themselves, as well as high functioning autistic children.....many of the kids I work with have a syndrome of learning, developmental, and emotional disorders.   So far I haven't seen the long term effects of the looking in their lives, and there's the problem.....the looking can take quite awhile to show the effects and this population is not the most self aware, reflective group.</p><p>Good stuff, though and we must continue to spread the word.  I believe in it enough to tell others about it, including many of the kids I work with.</p>
