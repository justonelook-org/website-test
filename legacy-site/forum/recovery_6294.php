<p>I've heard John talk about at least one bipolar sufferer finding peace after looking. As someone with vacillating brain chemicals, at times I wish that I could hear from someone who has gone through this. If we assume that the moving mood states are caused by genetics and not a response to fear then one might wonder how the looking has helped people when these same people received no positive results from even EST. OK, perhaps this is not entirely true (the 100% genetic) and the removal of the fear pattern is directly helpful. Maybe the condition is a response to fear somewhat and therefore becomes a lot more manageable.</p><p>What I'm noticing: Without the fear I can actually see and notice the mood fluctuations. This has got to be a move in the right direction. Mood swings now don't trigger intense mind reactions that in-turn set up new rabbit holes to find one's self climbing out of. The temporary nature of each experience is either an immediate understanding or ends up being within a short period of time. No multi-day head trips anymore. Prior to 'the looking', mood swings (especially highs) would usually end up with me saying or doing something that triggered shame, loathing, anxiety, etc, mentally magnifying other's responses and inducing and living in my own artificial world for a period of time. Since I was at stake each new episode added shrapnel to the theater of my life that I had to defend and/or perform damage control on.</p><p>The light is shining on all of this and it's a good thing. But now I see things I never saw before.....perhaps that's a big part of the recovery. The light does not discrimiate in what it shines upon.</p><p>So, I can see that looking does something about this. It also stirs it up because you can start to see how you've been kidding yourself and others. But if this is a chemical thing....Like I said, I'd love to hear a few words from some people that have gone through this. My mind could use some external dialog to go along with what's happening to me.</p>
