<p>Thanks for your comments antonyirvine and feraldream.</p><p>It took a while for me to respond because I had to wait and see if I had anything more to say related to this post. And basically I don't, but as this post was made some time ago, some clarifications can be in place though. I can see that saying that one doesn't have an inner life can seem quite strange and confusing. It is not nesseary, in this process, to at some point come to that conclusion and it doesn't say anything about where in the recovery process you are. The most important thing is to get to know and take control of ones attention. But for me it has been a useful experience, to see that there is nothing inward to be found than "me". If I look at myself right now, I'm experiencing having no mind and attention. Mind and attention seems to rise from "me". I am the source of my mind and attention so to speak. Therefore, looking inward after "the looking, seems like a strange thing to say and do.</p><p>And I think that this experience also inspired me to challenge the tendency to be too introspective. There is nothing to introspect. How our attention is relating to life is what is relevant to learn. And when one sees that there is nothing more to handle, process or purify "inside", one gets naturally more engaged in life. And of course, you also then want to give that possibility to people that is still experiencing that there is a deep and complicated inner life that needs to be taken care of before one can start to live.</p><p>Individual freedom becomes impossible. You can never really enjoy solitary freedom when others are stuck in such a crazy state, especially when you know how little it takes too do something about it. And again, to experince that " i have no inner life " is not a mystical experience. My guess is that it is a natural shift in perception as a result of the looking and I think most people can see what I mean by saying that through a little experimenting with ones attention.</p><p>I am glad that you guys found my post useful...</p><p>Niklas</p>
