<p>I read your blog post with interest and understand most of what you're saying. Five or six years after doing the act of inward looking I would not, however, say that I have no inner life. Also, after doing the act of looking there was a great sense of freedom, freedom from persistent, nagging thoughts about life which were entirely a waste of energy, though as time passed this initial sense of release was less pronounced. Certainly, the neurotic preoccupation with oneself slowly disappears; thoughts and feelings are part of the subjective self which perceives the world and we act in the world according to them which is why the freedom from fear is so vital. It is true also that one feels less separated from the world; the distinction between inner and outer experience is much less defined by fear so I think one acts in the world more effectively. One feels less of a sense of isolation. It's certainly the case that one becomes progressively less attached to disruptive thoughts and feelings and this is the result of doing the focused attention exercise. Perhaps this is what you mean by having no inner life. The concern with the ups and downs of the inner life that characterize neurotic thought is eventually completely dissipated. However, It's actually quite impossible to live entirely outside oneself and not very healthy either. We are able to respond to outer circumstances in a more compassionate way precisely because our inner life is free from neurotic fear. We can see things more clearly once free of neurotic self-preoccupation and can be open to the world in a more intelligent way but a healthy inner life is essential to be able to do this. Thanks for sharing as this discussion helps clarify a few things for me.</p>
