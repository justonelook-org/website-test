<p>Hi all! In todays newsletter Just One Look News Feb 10, 2015 I saw something that I want to share. John wrote:</p><p><span class="forum-post-italic">"From the very beginning, my work has been to understand the self-inquiry of Ramana Maharshi. I knew all along that the gift of the love of life that I had found was entirely a result of a desperate wrestling match I engaged in with Ramana's simple advice while in solitary confinement in prison. But having won the prize with no real practical understanding of how it had come to pass, I was not willing to inflict on people a half-baked message and offer no practical instruction on what to do. Now, twenty years later, I do have a clear, practical understanding of the essence of self-inquiry, thanks in large part to the patient willingness of many hundreds of people who have endured my failed attempts to say what I really wanted to say."</span></p><p>I think that the words "practical understanding" really defines what we are working with here. With that practical understanding a new terminology has emerged. Words like <span class="forum-post-italic">open awareness</span> and <span class="forum-post-italic">I am</span> belongs to a more abstract understanding. And abstract understanding isn't very helpful to people at large. I think it only creates a longing in us that almost never seems to have any real effect, at least not in our every day life. And I think that this insight gives great meaning. The old spiritual truths are not necessarily false and this work is not the only true view. The difference is that this work only has adapted to the context we are in now, as ordinary human beings trying to find a useful practical understanding and instructions regarding the relationship towards our everyday life.</p><p>This work has bridged the gap between old knowledge and practical usefulness here and now. The feeling of <span class="forum-post-italic">I am</span> and the feeling of <span class="forum-post-italic">me</span> are the same thing. But to say <span class="forum-post-italic">you</span> or <span class="forum-post-italic">me</span> is much more useful, and automatically includes everyone.</p><p>All the best,</p><p>Niklas</p>
