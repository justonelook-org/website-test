<div class="forum-post-template-post"><p>Dear Agnosus,</p><p>The question you pose is an interesting to me, too. Whether we actually control anything or not, whether control is in actual fact, after an careful analysis, an illusion, is secondary and quite academic. It seems to me that we act under the impression we can affect things and make choices. That's our experience of it, perhaps for a reason. It doesn't matter much whether this is true or not.</p><p>Legally, we are responsible for the persons we supposedly are, and I see the sense in that, but we are not in control of our thoughts and feelings. Just like I might be legally responsible for my pet dog. The person is outside of you, contrary to conventional wisdom. We can affect them in an indirect way by directing our attention, and might get better and better at it as we learn, but it's like gardening. The growing happens on it's own. It's one of those arisings in the universe, the feeling of control, whether partial or not. Perhaps it remains a mystery. I feel a sense of relief in thinking that I'm not responsible for my feelings and thoughts, that it's an illusion, and I also take pleasure in controlling and directing as much as I can. Go figure.</p><p>Seppo</p>
