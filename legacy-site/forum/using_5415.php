<p>When you and your life are the same and do not have separate goals then what you want from your life will no longer hold the same urgency. When I find a helpful tool my motivation wants me to get to the end of the process as quickly and efficiently as possible. Is it sane or helpful to be in such a state of keen inner searching or urgency when this new state (no fear) has only just begun to be available to us? John used a tool of focus to obtain a for-gone conclusion so that he would no longer search for the answer to his misery. He wanted to prove to himself that human unhappiness is the normal condition and be done with the search. Something found John. When our goals are no longer focused on knowing for the sake of security or the relief of fear then your life will find you. I have been looking for a deep connection that I feel I have lost or somehow thrown away. Is it not the fear of life that somehow creates the illusion of the lost connection? The feeling or belief in life as something that I can get something from and then be truly satisfied or complete seems to be based on a static or unchanging paradigm goal. Life always changes - that's why it called LIFE.</p>
