<p>My focussed attention in the first instance tends to go straight to what I think I want or don't want. It can be very powerful and seductive and invariably throws up problems which I then feel the need to work on solving. This often leads to dissatisfaction and suffering. What the looking has given me (when I put my attention on it) is a way of abandoning the need to think about these problems and they can become merely situations. I am only about 9 weeks into this and I am feeling very positive. I don't know if I am about to fall off a honeymoon cloud, but I'm not scared. My life situation was improving when I started this and has continued to do so, so I can't be 100% certain this wellness is all down to it. I had a very difficult 3 years until recently.</p>
