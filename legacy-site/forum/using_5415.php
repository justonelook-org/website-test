<p>Bruno,</p><p>What you call "an inherent will to realize my potential, to plan and perform, to set course," I see as the inherent force of life that is in a perpetual movement of action and effect, creation and destruction. I see no contradiction between that ever-moving, naturally flowing force of life and the power I have to determine for myself where to put my attention.</p><p>My attention seems to be the only source of the energy needed to have an effect on my action in the world, and my choice of where to put that energy entirely determines the development and unfolding of that effect.</p><p>Here's an example of what I'm trying to say:</p><p>When you see the thought, "To succeed, you need dedication, planning, and persistence," and then the following thought, "So, how does this go together with letting go all expectations," in that moment you have a choice:  you can accept the second thought as equally valid as the first thought, or you could simply ignore it. It is in that choice as to which thought forms are useful and which are not that the skillful involvement in your life develops and grows strong.</p><p>I could be wrong, but it seems to me that you have accepted both thoughts as equally valid. And in so doing, you have turned a positive insight about the power you have to determine what you attend to into a justification for the idea that one needs to wait for some level of development to appear before being able to live life actively and sanely, as if that were the logical implication of the idea of learning to use your attention in order to find satisfaction in your relationship with life. And somehow you managed to create a war between the two ideas.</p><p>Maybe I'm misreading your post, considering its truly brilliant conclusion: "When fear or doubts arise, I will look at them, find out what they want to tell me and learn". But the only thing I would add to that is that you can look at <span class="forum-post-italic">all</span> thoughts that call your attention with the same openness to learn from them and decide that you grant the feelings of fear and doubt.</p><p>And, by the way, I see nothing wrong with expectations. They are, after all, mere thoughts which you can choose to ignore or not.</p><p>Thank you for being active in this adventure with us.</p><p>John</p>
