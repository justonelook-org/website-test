<p>Hi everyone,</p><p>It's been a little while since I checked in here, and I wanted to provide a brief report.</p><p>What I hope is the recovery continues, some days more challenging than others, but I still continue to struggle with different manifestations of what feels like the fear of life.  That makes me question whether I'm making any progress at all, but I guess this kind of thing is one step forward, two steps back.</p><p>Specifically, what I've become REALLY aware of over the past few days is something that has always been with me, but seems more intense now&mdash;this constant underlying feeling of needing to DO something, to fix something about myself, all in an effort (and I'm guessing on this but it feels right) to stay safe.  Not that it ever works, but it seems that hope springs eternal.</p><p>It manifests itself in constantly feeling on edge, worrying if I've done the looking <span class="forum-post-italic">enough</span>, constantly trying to do the mindfulness meditation, feeling anxious when I'm sitting in traffic, driving home, feeling that I <span class="forum-post-italic">should</span> be doing something to improve myself-- basically, it's crazy thinking that's making me crazy.  It's embarrassing to even describe it, it sounds so insane.  And yet.  It feels very real, and I guess this is what John means by psychological mechanisms because it feels like a drive that has a life of its own.</p><p>I know that I don't have control over anything except where I put my attention, but even trying to focus on my breath right now has a crazy-making quality about it.  I worry whether I'm doing it <span class="forum-post-italic">right</span>, whether I'm doing it enough, and it feels like that effort is feeding this beast of constantly micro-managing my mind, my time, my attention, etc.  And as I mentioned before, this all has the effect of making me question whether the fear of life has diminished in the slightest&mdash;it feels very much front and center in driving my life.</p><p>Any thoughts or suggestions that anyone has would be super-welcome.  Even a reminder that this will pass would also be appreciated! â˜º</p><p>Thanks,</p><p>Ansley</p>
