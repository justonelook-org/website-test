Perfection!<p>Dear Ansley,</p><p>What a gift it is to read your words. You have nailed my experience completely. Completely. This is absolutely my daily experience, except for one additional thing: it’s that in the middle of all that you are expressing here, I look at me. Sometimes it is with some effort, sometimes it’s spontaneous, but it just keeps happening. And in that moment, my thinking mind just can’t “name” it – and so my thoughts are still and it is as if I am listening to the rain falling on the roof, sputtering in the gutters – just silence, like the sound of a fan blowing. I am without a thought at all, an experience with no name.</p><p>I wonder if this is happening to you as well, and just because our minds can’t name it, it doesn’t seem to be happening.</p><p>In my recovery, I am noticing many physical issues arise, but the most imposing one I’ve noticed is an overriding, heavy, <span class="forum-post-italic">huge</span>, life-long tendency to feel a deep hopeless darkness about my life. I’ve just begun to notice it, and see how I have wrapped many of my daily life events in its cloak of doom.</p><p>This tendency has taken many forms, among them, tiredness, exhaustion, an inability to choose to act on my own behalf, a feeling of illness, emptiness, remorse, sadness. I have begun to notice that I am thriving on, living in, this sense of exhausted doom.</p><p>Lately, with the looking, I am experiencing this doom as simply energy -- I am able to melt into it whenever it arises, instead of holding it, as I have in the past, as a correction, as a truth -- and I am able to feel deeply in touch with my own suffering and hold it with love and tenderness. I used to see me as a hopeless human, me, as doomed. This thought has now begun to deconstruct itself. It has shape-shifted from awareness of itself into awareness of itself as energy, energy that has no particular power over me. Just energy. I wonder if that makes any sense?</p><p>It is so interesting to me, that as I have been studying about each of my current life issues, they are each  pointing me back to Mindfulness Meditation! It really makes me smile as I see how my body is acting as a tough, uncompromising coach, pointing me back to this stillness, this me, who and what I really am! I agree, it sometimes feels to me like one step forward and two steps back, but when I open into the presence of me, even that judgment fades, and the presence of life shines like a silent star in the clear night sky.</p><p>I hope this is some consolation and support to you Ansley. I know you are doing this all wonderfully, and I have so much gratitude for your post today and for you.</p><p>Thank you my dear sister.</p><p>With love,</p><p>Dawn</p>
