<p>I find it helpful to see that others are having experiences similar to mine.  My recovery has been painful at times.  Periods of satisfaction, awe, gratitude and love followed by periods of fear, impending doom and of doubt.  Doubt is the most difficult because I have gone all in.  I have abandoned the pages of the past and destroyed them.  I am fully committed. This makes sense to me and I have seen it work.  If this fails, I'll live out my little fear-filled life and whatever follows my death just follows.</p><p>I have looked for, I guess, a year and a half--maybe two.  Although for many years I earnestly followed self-help gurus and struggled with the many things they suggested had to be changed in my life before I could experience happiness. I tried religion, and so on. I am not very receptive to mysticism or spiritualism.  So when I began Looking I didn't feel a need to understand or believe anything.  In fact I had long reached a conclusion that answers to such things as "What am I, really?" and the endless series of question that follow are far, far beyond the capabilities of this human being to comprehend.  This helped me keep the exercise very simple: there's nothing to believe, there's nothing to understand--just look at the you that never changes.</p><p>Aside from fleeting smiles that form on my lips while looking, as if something nice occurred just out of reach, I cannot prove that I have succeeded in coming into direct contact with myself--except that since the first attempt, I have returned to look hundreds of times.  I have witnessed recovery from a very serious bout of clinical depression and acute anxiety.  I have seen my ability to reason and function rise to levels I may never have experienced.  Some things that used to drive me crazy--well it just seems silly now.</p><p>But, to return to the first paragraph, my case has been difficult.  Although it seems to me most likely that John's speculation that the pain and difficulty is caused by the breakdown of mental patterns that developed to defend me from life itself, it seems sometimes that my mind is fighting this intrusion in almost a personal way, as if something alive does not want to cease to exist. For example, if I ask the question "What did I look/feel like before this human creature existed and what will I look/feel like after this human creature ceases to exist?", I can almost hear my mind howling in fear and I sense strong, unpleasant resistance.</p><p>I turn again and again to John's podcasts and eagerly await new ones.  Although I have looked at the Forums a few times, I have hesitated to participate, perhaps thinking that I needed to come further along first. But on the outside chance I can be of some service, here I am.</p><p>Much gratitude and love to John, Carla and all of you.</p><p>Steve</p>
