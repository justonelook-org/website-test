<p>I was just emailing jazzrascal and thought I would paste this from what I was writing as I'm sure there are some people here who have practiced buddhist breath counting meditation and the like. All the very best, Lex</p><p>"Yes, of course this self directed attention is quite a challenge! Some of it is kind of what I expected.. Certain times I do the SDA exercise I am able to count up higher than others. I haven't yet reached the point where I can go a full ten minutes continually counting up to ten with no thoughts. I think it's even valuable just focusing the attention for the count of 1. I had studied anapanasati before with a buddhist meditation teacher. When I was doing it a lot, the counting fell away and I could go for a long time without thoughts coming in. But, because I didn't have the understanding that John is speaking of, I gave up the practice as I wasn't seeing any concrete benefits. That's why I have gotten so much out of reading and listening to John speaking of the difference between buddhist mindfulness meditation and SDA. It makes <span class="forum-post-italic">so </span>much more sense to me to have this outlook and, now, I feel I can clearly see the benefits of developing this."</p><p>I've only been doing this practice for a little over a week and I'm sure most of the people reading this have heard all John's podcast/youtubes.. but I thought I'd mention that I found 'conversation 9' on youtube to really give me excellent insights into what I always felt was missing from meditation practices I've studied. And I like the fact that, technically, John doesn't refer to this SDA practice as meditation.. It's just simply an exercise to focus the attention, and I think this is very accurate. It's a way to understand how this differs from the way most people understand meditation and it's usual goals and ideas. What John is speaking about is what I've found that those few people who have been able to effect what would be considered 'miraculous' healings throughout history have also emphasized as a major key to healing itself. But I've never seen them present what I experienced as an effective tool for doing so. I do believe it's like John is talking about, that a lot of times these insights certain people have had about all of this have been confined by spiritual/religious belief paradigms of the time which clouded the clarity of practices of gaining insight into one's true nature.</p>
