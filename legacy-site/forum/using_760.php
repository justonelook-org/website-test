<p>Hi Don, LeraJane, Trimpi et al,</p><p>I love this conversation and I just wanted to add my thoughts</p><p>I've been doing the looking for 4 months now, and recently I've been noticing so many fascinating traits that I've associated with myself over the years, and that I am just now becoming able to observe--it's like watching a movie of myself.</p><p>I see things like how much being "chosen" and getting the love of someone else has mattered to me. And no matter who chose me or gave me that "approval," how it was never good enough to staunch a deeper wound inside me--a wound I have always attributed to some injury in my childhood. And while this may in some form be true, now I am experiencing an awareness--like a door cracking open where I can see the pervasive and overwhelming fear of life which was always present.</p><p>I'm seeing my fear of life arise as the fear of losing my body's appearance of youthfulness, the arising of its aches and pains, diseases and limitations. And I am seeing that this body will not "win out" over life--and how all of this is an awareness of the fear of life--of how much fear I've had of being in this body.</p><p>I also am seeing how I have spent many years trying to feel better by attempting to appear to others and to myself, as "evolved," "transformed," "above it all," "better than," "perfect," and "filled with awareness"--and all that time  wondering what the HECK I was trying to create, trying to be, never knowing what any of that was.</p><p>Thank God for John and Carla's Just One Look. This looking is all I do now, and while this odd assortment of ideas, beliefs, stories about me, etc. plays out in my recovery--instead of holding onto them as "me," I am able to just observe them as if I am watching a movie. All quite interesting and strange and sometimes very funny--but not really "me" at all. Just an actor on a stage playing out a role--(it's true, Shakespeare!)--but a really great portrayal, that now (even with all the mess of it!)--I am somehow enjoying.</p><p>Thank you to you ALL for being here. It's a wonderful place to be.</p><p>with Love,</p><p>Dawn</p>
