<p>Hi Everybody,</p><p>I was just considering what aspect of these teachings made then so appealing that I returned to them again and again, and finally adopted them to the exclusion of any other method of "self-realization", for lack of a better term.</p><p>Throughout the history of science, almost any discovery required laborious and complex calculations to verify its accuracy and, even then, the result was an approximation of the theory.  When the calculus was discovered, the process of calculation and interpretation was so vastly improved and simplified that scientific progress was advanced dramatically.</p><p>I envision the looking as analogous to the discovery of calculus, in both its elegance and simplicity.  I'm greatly relieved at not having a "program" to  follow or a list of spiritual suggestions to which I must adhere.  Another metaphor that I think would apply is that of a "surgical strike", using a single (and very specific) method to achieve the precise goal of perceiving your true nature.  The unburdening I feel from the unending analyzing and self-evaluation of traditional teachings is almost physical, and, as the looking has a greater and greater impact, I feel a deep sense of gratitude.  Just some thoughts...</p><p>Don</p>
