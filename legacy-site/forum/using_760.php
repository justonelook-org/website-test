<p>How's It Working?</p><p>Well, Don, now that you ask... I think this is as good a place as any to keep rolling with some reports as to how the looking has benefited us (if it has).  From my perspective, it might be helpful to give a brief history of my "evolvement."</p><p>In taking stock of myself 10 years ago, I felt there was a pervading undercurrent of fear and anxiety, even though I was successful in the ways of the world according to western convention.  I was not drawn to the concept of gurus or spiritual nourishment and was resigned to endure the suffering and emotional responses like anger and resentment that probably arises from that undercurrent.  I remember telling a friend that I wanted bo be rid of those qualities, but most of all fear.  Then came The Power of Now, which led to a couple of Leonard Jacobson's retreats on living in presence before the Fall 2006 retreat with J.S., followed by the same retreat in 2007.</p><p>I have been following Leonard and J.S. since and have listened to their tapes, often numerous times.  The space for being fearful gradually opened.  I knew the value of looking inward the first day of the 2006 retreat.  I had already come to the conclusion that the solution to eliminating fear and anxiety lay within me.  J.S. hooked me when he said that we could actually rid ourselves of misery and suffering by looking inward.  I hadn't thought it was suffering before then that I had been experiencing, but it was.  So I took up the challenge and reinforced my commitment by listening to tape after tape after tape, until I had the understanding that fitted me.</p><p>The need to understand dropped away after two or three years, and almost all other needs except perhaps the need to be appreciated dropped away.  Even that one is more of a ghost trait than anything else, because I am not afraid of what may happen if I am not appreciated or even hated by everyone else.</p><p>On a scale of 1 to 10, I'd say the effectiveness of the looking was a 9.2.  Another way to say that is that I am 92% cooked.  I am absolutely sure the looking has worked, although I can't say for sure it worked without the other influences arising in my experience.  I can theorize, however, that it could and would work all by itself and hope that there will be people out there who will try it and stick to it with no other practice aimed at eliminating fear and anxiety other than looking within.  Trimpi</p>
