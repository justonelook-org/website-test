<p>Dear notconfident,</p><p>What you describe is very similar to my experience and it seems to be related to hormones. You may be going through premenopause. I have been going through premenopause for six years now. I started having the kind of symptoms you describe - and many more - when I was 47. It took me a while to figure out what was happening. I can tell you sometimes it has been a hellish experience. Besides a number of horrible physical symptoms, a dark cloud of depressive thoughts takes over and I feel overwhelmingly pessimistic. It feels like I am never going to get better. No matter that John reminds me, as I asked him to, that this happened before and I came out of it, and just a few days before I was feeling great. That may help for a few seconds, but the rest of the time it's a struggle. I have often had to stay in bed for six days in a row because I was so weak I could not even get up or even eat anything.</p><p>When this happens, I do the focused attention exercise as often as I am able to. But that too is very hard to do in the midst of excruciating pain. I know from experience that this is a passing storm created by the hormonal imbalance and it will pass but, of course. when the storm rages, my mind feels like cotton and I cant think straight. So during those periods I just feel rotten. And when I'm so weak that I can't work, I just have to slow down. And it's really hard, because there is so much work to do... If I push myself, I just get worse and worse. It took me a long time to learn, but I finally managed to not push myself so hard. If I do that, the crisis seems not to last as long.</p><p>What I'm trying to say here is that this is a physical event that all women go through and it affects your body and your mind. You can see that those negative thoughts have nothing to do with your present reality, right? It's just the hormones playing a stupid game in your brain. You can use these extreme moments to do the focused attention exercise. Focusing on your breath may help divert your attention from those negative thoughts for a moment. And as you persist, this exercise will strengthen your capacity to control your attention, but it won't make the physical and psychological distress go away.</p><p>You gotta try and find a way to help your body deal with the imbalance. Maybe you could try speaking to your doctor? Over these years I tried several doctors but most of them didn't have a clue what I was talking about.</p><p>Hang on in there. When you're feeling good, take some time to try to find ways to help your body. Be patient with yourself. Although this transition may last a long time, it won't last forever.</p><p>So you have an older woman you can talk to? She might be able to give you some tips.</p><p>I hope this helps a little.</p>
