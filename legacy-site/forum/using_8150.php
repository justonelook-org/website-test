<p>I wanted to add to what Lex said. He emailed me today to say he was going to watch the webinar. I had already seen it, but was in such a state of distraction that day that I didn't really grasp what John and Carla were talking about. So I watched it again today, and was blown away! (Actually watched it twice). I think the first time I heard John talk about how we don't think our thoughts, that I didn't really believe him. But this time I got it. All of a sudden it made perfect sense. In fact, I realized that for the past few days I had been noticing some really weird scenarios being played out in my mind, and when I became aware of them, I just laughed... Somehow I had the sense that I wasn't thinking them.</p><p>I love how John says that we're the "customers" of thought forms. This is so accurate! They offer us their "wares" and it's up to us to decide whether we want to "buy" them or not! Perfect!</p><p>I also appreciated the part where a listener said that having so many thoughts seemed "unnatural" to her. This hit a chord with me. I've always been an over-thinker, but now, after doing SDA for many months, all this excessive mind yakking really doesn't seem normal to me anymore.</p><p>And finally, I loved what Carla said about emotions, and how there is really only one emotion that thought gives different names to. This is a very powerful observation, because we can use self-directed attention to dismiss emotions and the thoughts they feed if they are not helpful to us. If this were widely known and accepted, it would do away with the need for psychotherapy, analysis, and so on. If any self-reliant individual can handle his/her emotions in this way, then they have the key to freedom without depending on another, not to mention without spending hundreds of dollars and hours.</p><p>This was an excellent talk and well worth watching more than once. Thanks, John and Carla!</p>
