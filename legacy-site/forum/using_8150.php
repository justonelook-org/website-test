thoughts. It's just a concept to keep in mind and be open to. J+C are saying that continuing to practice the SDA, dropping the 'soliders of fear', will lead you to experientially see this truth. Then, your own experience will be unique to you after that. Ok, hope this helps someone who reads it! All the very best, Lex</p><p>p.s. - Carla, really liked your explanation of emotions on the webinar!</p>
