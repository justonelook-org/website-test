<p>Hi Jim.  I used to drink quite a bit of alcohol and smoke cigarettes.  One day I just quit smoking.  I had tried to quit in the past with lots of angst surrounding the process, but it never seemed to stick.  But post looking it just happened with very little drama or thought.  I also drank heavily for years.  Same reason as yours-- it helped numb social anxiety and boosted my confidence.  Now too much alcohol makes me feel crappy and dull like Jack said.  I like a nice buzz but not full on inebriation.  I also dislike hangovers more than the 'fun' of the night before.  I heard a quote where someone said drinking too much the night before is 'stealing tomorrow's fun' ;)  I like feeling clear and grounded which often brings sharp edges because I'm not numbing but i like those too.  I also don't experience intense social anxiety anymore.</p><p>Last night I enjoyed a glass of sake and smoked a rolled cigarette (a very occasional thing).  I realize I enjoy substances much more when I'm using them for occasional enjoyment and not abusing them.  I know my cut off limit and I tend to respect it, but sometimes I still go a little overboard.   Even then since I don't experience much guilt anymore it's not a big deal.</p><p>I'm developing healthier habits like training for a half marathon and practicing guitar and taiji which is infinitely more nourishing and interesting than drinking and smoking.</p><p>So what works?  It's a good questions because I often see patients as an acupuncturist who want help quitting smoking or overeating.  I have a certain acupuncture protocol I use, but recently I have been giving them the directions to look at themselves and attention exercises.  I think the acupuncture can calm stress and help break habits, but unless the groundwork of fear is removed by the looking I don't think it will stick.  So maybe it's just time.  As the fear evaporates and sanity takes over things just leave on their own.  As my attention grows stronger I can make a choice about drinking or not drinking or drinking and not beating myself up about it.</p>
