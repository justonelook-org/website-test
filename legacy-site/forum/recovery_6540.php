<p>Hey Jim, surely a good topic! My relationship to drugs has never been this difficult. After looking it has become easier to recognize when I'm doing something out of fear rather than the reason my mind believes about it. Turns out my drug use often ends up in that category. I get caught up in many, often contradictory, ideas simultaneously so I have a hard time making any sense out anything really. It is very frustrating and I feel helpless and fearful when trying to decide how to go on. On one hand they can obviously help out in the short term, but in the long run they take their toll one way or another.</p><p>For example: Smoking cannabis usually makes me feel more comfortable about the current state of things, however, abstaining makes me feel more alive, alert and I have a much easier time (and interest in) controlling attention. Regular use adversely effects my productive output quite a bit as well, which can carry its own emotional consequences at times, but at other times the mental hell is just more than I want to deal with. Alcohol works great for social anxiety at the right amount, but that's normally difficult to achieve. And I have noticed that if i <span class="forum-post-italic">know</span> that I'm drinking to get more confident, I usually don't get that effect at all. I also see that this drunk-confident me, even if I'm "doing great" socially, is actually prick way more often than sober me. On the other hand everybody drinks and it's what's considered the normal way of hanging out and it really gets me out of the house to meet people.</p><p>John says no matter what you do during recovery it will all come to and end just the same. So should I take drugs to instantly make me more content with the current state of things, or should I abstain to be in more control over my attention and actions (maybe even hastening recovery, who knows), or should I <span class="forum-post-italic">just not care anymore at all</span>, stop worrying about it altogether and just roll with the punches and see what happens. No question mark here, just thinking aloud - maybe someone can relate to these thought forms. So this is kind of what I mean with contradictory ideas, doesn't seem so serious when typed out like this, but this is one of the things I still believe that I need to fix before I can be at home in my life.</p>
