<p>In spite of all my spiritual/religious training before JOL, I was never able to get a grasp on forgiveness. Like everyone else on the planet, I've had people hurt me, and I've hurt people myself. I have tried, over and over, to forgive them and myself, and just as I thought I had a handle on it, the old feelings of blame and self-hatred would pop up again.</p><p>But something changed after I did the looking last year. I didn't see it clearly until quite recently, but I suddenly realized that no one is to blame, because we are all caught up in the fear of life. When this occurred, I recalled listening to John's podcast "Who's to Blame?" Even though I had forgotten about it, I believe it was the catalyst for this change in me. Here's the link:</p><p><a href="https://www.stitcher.com/podcast/john-sherman/being-human-the-john-sherman-podcast/e/45385711">https://www.stitcher.com/podcast/john-sherman/being-human-the-john-sherman-podcast/e/45385711</a></p><p>This is tremendous and has important implications for world peace. If I was able, virtually from one moment to the next, to forgive not only some people who had done things to me that many would consider unforgivable, but also to forgive myself for some terrible things I had done to others, imagine what this could mean if everyone were able to have a genuine feeling of forgiveness towards others, no matter what they had done. If you are enmeshed in the fear of life, you aren't sane, and so you're not responsible for what you do. The general opinion among humans is that we are to blame and must take responsibility for our actions. This is impossible until the fear of life is destroyed. Just One Look...what an amazing, simple solution.</p>
