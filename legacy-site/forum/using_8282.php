<p>Only a couple of days ago my brother came to me and asked "why am I so different from everyone else?". He told me that everyone around him seemed so worried when he was not, but he didn't know why. I was able to answer him, because it was obvious he had lost the fear of life. He was concerned with the way other people suffer from their "own minds" as he put it, and he was very excited to hear an explanation that made sense to him. Just as he felt relieved to understand that no one is to blame for the madness, I was pleased to be having a sane conversation with him about human neurotic fear and how it is responsible for the mess we are in.</p><p>He went through recovery without any understanding of what had happened to him after I suggested a look inwards some years ago. But now as I explained that the looking removes the fear of life, and how attention is related to how our minds are being shaped, it all fell in place for him intuitively. He took particular interest in the topic of who's to blame for all the badness, so I felt helpful to him by having this podcast to kind of demonstrate what we are doing here, what the bigger picture is. "‹"‹"‹To him this whole theory seemed plausible and sane, he said.</p><p>"‹</p><p>I wanted to share this anectode because of the immense optimism it generated in me. Reasoning these things with people still engaged with the fear of life, which I'm sort of used to, takes a lot of energy. But for my brother, who looked at himself at age 18, it was a completely natural explanation, and his follow-up question was simply "So what is the best way to tell my friends about this?" I could not be a more proud big brother! Thank you for this incredible gift John and Carla. I will gladly take anyone's advice how to best answer his question so I can pass it on.</p><p>I still happen to point my finger when I'm frustrated, at people or their ideas. I can just never hold a grudge about it like I used to, because I know that no one is to blame. It is "as plain as the nose on my face", as John would say.</p>
