<p>Hi Aabha,</p><p>My health had also worsened after about 4 months of Looking. There are steps I have been taking to help the situation, and I've really done my best to improve my health. That said, all this is beside the point, as John would say. The recovery process waxes and wanes, from calm times to rough times, and back again. Just notice that YOU haven't changed one iota. You know you are you--nothing has helped or hurt you. Nothing! It doesn't matter if it's easy to look or not, or whether it seems like you are able to or not. The medicine is working on its own. And the frustration and hard times will clear up on their own. The waves of life keep coming and going, and that's how it is. The frustration can't harm you--you see it, and at the same time, you see that it's not you. None of it can touch you--you know you are you. All is well, despite appearances.</p><p>Blessings,</p><p>Jenny</p>
