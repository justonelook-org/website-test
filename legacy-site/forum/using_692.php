<p>Dear John and Carla,</p><p>I am so happy to report that after years of searching for something that made sense to me, I feel like I have found it in the simple act of looking. I have been very earnestly doing this for the last couple of months or more and I can see that some changes have happened. the first time I looked, was very radical for me. I saw the me that has always been there , the same me that was there when I was a child. this was very immediate and obvious and followed by a sort of high. ofcourse that passed.</p><p>I feel that now, I am in recovery .  A lot of buried issues and unease are surfacing. However I find that my response to these is a lot different from before. there is a sense that this too will pass, and when I look, I am always always here. Very often, this brings tears to my eyes, because it is so beautiful to finally feel like I am home. this is home.</p><p>I have waited for my soul mate all my life, and now it feels like the beloved has always been right here. That sounds mystical but it feels very right. I find that my need for a life partner is just not there any more, my need for completion from another source is dissolving.  My beliefs about that have dissolved but I didnt actually work on them during this time. The looking seems to have done that. I  know because I have worked on these difficult beliefs for many years to no avail.</p><p>I hope I am not being impetuous or hasty about declaring these things. But I am so grateful to you both for this gift. Even if these things come back to visit, I know they cant actually harm me in any way.</p><p>A big issue in my life has been that of envy. How is envy related to fear of life? Is it that we fear that someone else has a better life or that something else should be coming my way instead of this which is happening? I am not so clear on that and would be very very grateful for your insights.</p><p>I deeply resonate with the looking and hope I can tell more people about this if they ca