<p>I found after 5 years of looking that my states didn't change all that much but sometimes seemed exacerbated, whether to the good or to the bad.  That was in the first couple of years.  They were much clearer, however, and it seemed I could "own" them where before I was more at their effect.  Their upwellings were part of the process.  I began noticing two things: first, states of confusion, anxiety, resentment and anger didn't last very long.  I used to go into my cave for sometimes as much as a couple of days.  If I was angry for several hours before, I might be angry for 15 minutes now.  Second, it became evident (subtly so) that it was easier and easier to transition.  You know what I mean by transitioning.  I began paying more attention to that as a matter of curiosity at first and then as a tool to keep up my interest and commitment to doing the work.  Now when things upset me or circumstances seem overwhelming or swirling out of control, it's so easy to acknowledge what's happening, know it's going to pass, maybe document in writing what I'm experiencing, take a breather to look to feel what I am, share with others, etc.  John has said it many times, but you can take it to the bank that the confusion and obscuration replacing clarity will again be replaced by clarity, and soon you will have even greater clarity as you see how the process unfolds.  In other words, you will get it that clarity and confusion are aspects of the same coin and are within the whole.  Each crops up from time to time, that's all.  You remain, and that's worth the attention you may want to give it instead of always wanting to fix or improve the apparatus.</p>
