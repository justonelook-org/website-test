<p>Hi Delmogazi,</p><p>I just wanted to say thanks for your honesty. I don't know that I have anything especially helpful to say, all I have to share is my own experience. I had a VERY long and difficult recovery period after I first did the looking. For years and years I didn't feel like I was getting any better at all; I first did the looking before John started talking about self-directed attention, and for me this has been the missing link. As soon as I started doing the 10 minute structured self-directed attention exercise on a daily basis (this was some time in July), I started to experience some of the changes that John and others talk about. Since then, I have experienced a lot of frustration and doubt along the lines of "If I'm really getting better, how come I still have so much anxiety about my job? If this exercise is really working, then how come I still get so wrapped up in XYZ?" Even so, at the same time I've also had the sense that even if a certain pattern of anxiety is still present, other negative patterns are definitely falling away. I have days, even weeks, when I feel like I'm in a full-blown mental/emotional relapse and just as unwell as I ever was before practicing self-directed attention, but if I really look at what's actually going on I can see that even when I get swept up in some emotional drama it passes a lot more quickly than it would have before. Hang in there.</p>
