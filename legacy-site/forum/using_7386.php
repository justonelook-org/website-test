<p>Carla and I have come to an understanding that the non-dual spiritual teachings that seem to be everywhere in one form or another in these times are worthless at best.</p><p>There are millions of us who have believed in them and trusted them to ease the misery of worry and fear that is the lot of the vast majority of us. We do so because we are told that the wisdom to be revealed upon our acceptance and surrender will bring an end to our war on our own life, which is caused by ignorance.</p><p>But those of us who have believed in them as the literal word of god seem to be no less needy for the truth than we were when we first fell in love with those ideas.</p><p>I mean, if you have been truly freed, why would you continue to attend meetings, read spiritual literature, and seek solace in meditation?</p><p>I mean, wouldn't you think that after having seen directly the deep singularity of all things, however small or big, filled with mystery or triviality, the need to return to the tit of spiritual conversation would vanish in the emptiness of found truth?</p><p>Stay tuned. More to come.</p><p>Look at yourself.</p>
