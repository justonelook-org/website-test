<p>It is very clear to me that it is a fundamental difference to understandings, actions and motivations in a mind before and after the looking.</p><p>After the looking new and fresh connections are being made in the mind towards understandings, action and motivations. It is an automatic adjustment in the mind that, strangely enough, seems to change everything and nothing at the same time.</p><p>A way to notice this adjustment is to see how one's thought forms change over time. Thought forms come automatically with a slightly new explanation to a certain "situation" every time. They get saner, clearer and more correct in relating to life.</p><p>A new thought form that has developed in my mind that is related to spiritual understandings is for example: "Why should I meditate anymore? Even if I would meditate for hours a day I would still be here as me. The only thing meditation can change and has any effect on is my state and why would I want to change my state?" My understanding and motivation towards meditation as an action that I had to do to be well changed as a result of this adjustment. It no longer made any sense to me to meditate.</p><p>My motivation towards reading spiritual literature also changed over time. The more I came to experience and understand that the clarity and insights I always had been looking for in complicated spiritual and religious ideas were actually found in my new relationship towards my own personal life. I didn't get what I thought I wanted but instead I got what I always needed. A more self-reliant, clear and intimate relationship with the circumstances of my life. The act of inward looking did that.</p><p>Today, my spiritual literature has found its way to the bottom shelf of my book case and stands as a an old memory of a much more complicated and confusing time. Not that they necessarily are false, but they are far too complicated and useless in my life today. In a way it feels like I don't understand them anymore.</p><p>This post was originally posted in the Just One Look Facebook group.</p>
