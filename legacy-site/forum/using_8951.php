<p>Welcome, deeplooking.</p><p>I'm happy to hear that looking at yourself with your mind's eye gave you an experience of joy. That rarely happens. It's not required, though. It would have worked for you even if you hadn't noticed any internal response whatsoever, as is the case with most people trying this.</p><p>I too agree that continuing with AWA meditations will not harm you. Anything that helps you deal with the things that life or your mind throws at you will be alright. In my experience, it might even be required to stick with meditation for a while, to stabilize your psychology. You just pulled the rug from under it, which can cause it to make quite a racket. Therefore, please do not be disappointed if you develop differently. I've seen people posting after 4 years or so, that the first look doesn't do anything. It can be difficult to accept that most changes happen sort of under the radar. This is what makes this method so unique. Rather than giving you great experiences worth remembering, it actually turns you over to your own natural state, and that completely differs from what we've been taught to expect.</p><p>Should you feel like it some time down the road, learning Self-Directed Attention is recommended as an ongoing practice during the aftermath of the first look.</p><p>Felix</p>
