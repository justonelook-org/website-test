<p>Hello, deeplooking,</p><p>Nice to make your acquaintance and I am glad you started the Just One Look method.</p><p>I came across John and Carla's work about 9 years ago, at the end of a period of intense spiritual search and can attest to the fact that the Just One Look method put a definitive end to that excruciating effort. I hope my experience proves useful to you.</p><p>I personally did a lot of analysis, comparison and synthesis of different methods and teachers, ranging from buddhist to Advaita Vedanta, aztec, Cristian and many others in between. I am very happy today to say...I really don't know!</p><p>I don't know if the Just One Look and the Awareness Watching Awareness point to the same reality. If they do, they sure go about doing it in different ways. There are so many directions, so many teachings, so many instructions to follow, and sometimes we get lost in the search, not knowing what is best, what is safe, what is flawless, what is right and effective.</p><p>And this is where John Sherman's discovery shines in its simplicity and its brilliance. All that's needed is the honest desire and impulse to take a brief look at the most intimate, the most intense and the most solid element in my existence, the ME of me, the I of me. All that's needed is to turn the attention around and look at that. And the rest takes care of itself. Whether that's awareness, attention, the cosmic mind, the local mind, the all-encompassing love, I don't know. And it's not necessary to understand it either, only to do it once. Isn't that nice? The rest takes care of itself.</p><p>Now, don't believe I'm any better, brighter or wiser than anybody else. I came upon John and Carla's instructions and they were right under my nose for 2 years while I perused meditation methods and release techniques.</p><p>And once I was all out of options I actually tried to look at myself. I didn't become a sage, a monk or an initiate. One day I just woke up, got dressed and, on my way to work, for the first time in my life, I felt: wouldn't you know it, there really is no need to understand all of that...it's enough to live one's life without anxiety.</p><p>If you like the AWA method, by all means, keep at it, it surely has value. As far as the Just One Look method goes, if you've tried it once, it will do its work on the fear of life and you'll be allright.</p><p>Take care and all the best,</p><p>DragosCristian</p>
