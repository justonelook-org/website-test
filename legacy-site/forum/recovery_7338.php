<p>Say what you will about moving my attention to breath, and sooner or later all my angst will be gone... What is still here and very real and difficult for me, is this social anxiety that's plagued me all my life. I got rid of it last year, so it seemed. I took meds, got drunk and stoned and numbed the emotions. I think what happened is that I just put the whole thing on pause. Now, It's nothing to do with my minds stories about it, so the breathing techniques are irrelevant I think. It's the instant, sensual, interactions with others in the world which is hard. Why isn't it effortless yet? When I first joined the forum someone said they felt like they were learning to talk from scratch, all over again and that it was effortless and easy, after four years they said.</p><p>I am over four and a half years post looking. I can see that my perception has changed in some extraordinary way, but that alone hasn't freed me of all this inferior stuff. I can do the focused attention or zen mindfulness all I want, the tension and feelings of not being good enough and anxious when I talk to people is still here. I need to wait? I must just do something else I suppose. I guess my intention to get this out is to correct my last reports of everything being fine and experiencing no problems. Just not true for me now. I feel stuck again.</p><p>I spoke with Richard Lang, who teaches headless method, and he said he experienced some really difficult self conscious times twenty years after doing the looking. Kind of diminished the idea of a gradual recovery into a sane and happy life for me. Shit doesn't want to go away that easily. Maybe he repressed it, maybe it's a good thing it's still here with me?</p>
