<p>I can relate to this social anxiety problem. One of the reasons I began drinking in social situations was because the intense fear I felt around people I didn't know and even people I did. I don't know that there is a quick solution to this. I've been doing the looking for some 3 years now and though I am much more at ease with people (put another way, when the anxiety pops up I'm not so easily upset by it) I still in some way prefer a measure of solitude. If that's the way I am, I'm OK with it. I don't feel as isolated as I used to and when I want or need to reach others I can do so. I felt when growing up a lot of pressure to be "sociable" to be a "nice guy". All of that has vanished. The key point is really the fear which the Looking addresses directly and will eventually do away with. Another is the reaction against the anxiety itself. The self-directed attention exercise enabled me to be at ease when that anxiety arose and somehow not feeding it with your attention seems to lessen its effects. It was like, "OK, I'm anxious so that's there." Tensing against the fear only makes it worse. Some therapies speak of this but only after doing the Looking and having some ability to shift attention away from certain thoughts did I see any real change.</p><p>Cheers</p>
