<p>Here's my prediction, Jim. (If our recoveries are similar). You will begin to have this odd sense that you don't care as much about being anxious despite being very anxious. Your anxiety ABOUT your anxiety will begin to diminish. This may go on for awhile, and it may be happening now, but after awhile, without the meta-anxiety kicking in the after burners, the social anxiety will diminish.</p><p>I still get anxious socially sometimes, but I don't really care about it, it doesn't bother me. It then fades away and I don't even know when it goes. All those built up judgments and recriminations about how you should and shouldn't feel is the meta-anxiety stuff and when this goes, and I think it will, you're left with plain old autonomic arousal which is kinda boring without all the mind drama behind it. These patterns run deep, but even these old ruts become smoother with the healing of the mind.</p><p>Look for that odd sense when you don't care in the midst of an anxiety attack.</p><p>Best, Jack</p>
