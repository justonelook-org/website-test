<p>Dear c butterfield,</p><p>Please just try to get a feel, a taste of what it feels like to be you, before the thoughts, the feelings, the sensations, etc. If you try to do it, you can't fail. You may not see anything, or recognize what you feel as yourself. But if your firm intention is to touch the feel of you, you will succeed. There's a reason for that, but I go into it now.</p><p>Once you have really tried looking at yourself in this way, you've done the most important thing and you are on your way to recovery.</p><p>Now, learning to control your attention is hard, but only because we are not used to it. Most of the time, we don't even know that we can do it.</p><p>So, just keep trying. Think of it as exercise. If you want to run a marathon, you have to start slow, running short distances, to build strength and stamina.</p><p>The most important thing to do once you have looked at yourself is to practice self-directed attention -- knowing that it's hard for everybody. But you can do it.</p><p>Try to take 10 minutes a day, when you wake up or before you go to sleep. Set a timer so you know when the time is over.</p><p>Sit down, close your eyes, and just focus on the sensation of the air as it goes out of your nose. Spend some time just trying to feel that. You may want to do just that the first time.</p><p>Next time, try counting the out-breaths. Every time the air goes out of your nose, count it silently in your mind. With the first out-breath, say to yourself: 1; with the second out-breath: 2. Until you get to 10.</p><p>Now, you will probably not be able to get beyond 1 or 2 without getting distracted by your thoughts. For instance, you may find yourself thinking that this exercise is stupid and you'll never get it.</p><p>When you notice that you are thinking and not doing the exercise, stop. Don't criticize yourself, don't tell yourself you can't do it, etc. Stop right there and immediately move your attention to your breath and start counting again from 1. No judgment.</p><p>This is just like lifting weights. The point is not to get to 10. The point is noticing the movement away from the focus point (the breath) and then choosing to move it back to the breath.</p><p>You may find yourself starting over and over for a while. Please be patient. It won't take long before you start to see the real benefit of this exercise in your life.</p><p>Don't give up. You're on the right track.</p><p>Please keep in touch. Let us know how this unfolds for you.</p><p>If you haven't yet, please read the updated instructions in our booklet. <a href="https://www.justonelook.org/download/DownloadHandlerLink.php?file=JOHN_and_CARLA_SHERMAN_The_Just_One_Look_Method.pdf">You can download it by clicking here</a>.</p>
