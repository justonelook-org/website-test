<p>Last night, I had some "insight" that I want to share. It is currently helpul for me, I think it could be for others as well. But in any case I am curious to learn about your comments if you feel like dropping one. As John explained - most clearly in his blog entry: "the mind" - the mind is where the experience of the world and it's evaluation happens. I realized my mind constantly tried to find or build an unknown and non-existant reference, or standard - a great blueprint of one own's, and life in general. This is a big stress, source for anxiety, and distortion of reality, and I believe it is a common human belief that such a standard DOES exist, although nobody knows it. At best, one can could get some fleeting glimpse at it. With the looking one realises (in the sense of "knowing", not "deducing from evidence") that such a reference does not exist, it is crazy to assume there is some unknown truth that threatens or validies every thought and experience in the mind. It is useless to compare and evaluate each and every experience to build the great masterpiece in the mind, to unlock meaning of one's own, or life in general. A greater meaning, a treasure that only a few heroes can find, does not exist. The experience of life in the mind is not lacking a "big" interpretation. So whatever happens in the human mind, no need to scan it, to constantly evaluate whether it conforms to a diffuse standard nobody knows.</p>
