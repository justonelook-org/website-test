<p>Hi Zak,</p><p>Basically I would say that you can practice the Self-Directed Attention Exercise with your eyes open, even though I think is it is much harder due to visual input. And remember that the "free-association mode" you experience when you close your eyes, might be just an increased awareness of the state we are used to live with also with our eyes open in our dat to day life. My point is that you, when closing your eyes, get a more direct experience of how our mind generally work. And that there really is no difference in how our minds work with closed or open eyes. And I also think it is important to see that we cannot really make general assumptions in how our minds work when we consider the fear of life. A human mind functions quite differently depending on high or low levels of fear based thought processes. As psychological fear lessens (after the Looking and the practice of SDA) our minds gets less reactive and associative.</p><p>And Self-Directed Attention is really hard to do, I know! But I would say that the best and most effective way of doing it is to follow the instructions just as they are.</p><p>Great to hear that you are doing the work, keep at it, it will certainly pay off!</p><p>Niklas</p>
