<p>Autonomy of action is something I've thought about quite a bit.  Certainly, we all agree that whilst in fear much of our thinking and actions are simply conditioned, in fact there are neurological experiments to show our nerves and muscles move a nanosecond before we have the conscious thought to move.  A crass example is that our hand and arm may reach for a box of donuts a split second before we know it is happening.  We then, it is theorized, create a back story to match the action of our muscles and nerves, like: I could really use a donut right now, or I've been eating healthy, I deserve a donut.</p><p>If we look at a typical modern human there is not much going on behaviorally.  We sit or lie down most of the time, usually in front of a computer device, we stand occasionally, eat, drink, go to the bathroom, lie down, talk to others sometimes, etc.  Not much is going on, right?  Much of our lives are lived internally, with our thoughts, emotions, and imagination.  The dream world of the internet is where our conditioning happens in my opinion.  We are in a highly susceptible, trancelike state that opens us to the conditioning coming in so seductively through the blue light of the ether.</p><p>I believe recovery is undoing much of this conditioning, but the question remains, how much of our actions are conditioned and how much do we have agency over?  I personally believe that 99% of my actions are conditioning, its just that I have been able to change the patterns.  My hands reach out to grab some food before I make a conscious choice, only this time its an apple as I don't buy donuts any longer.  I feel that I am in the midst of unraveling much of my conditioning from earlier in life and sometimes it takes conscious work, and sometimes it doesn't.....much of this has to do with the religious beliefs I was conditioned with at an early age and the attending emotional control mechanisms (guilt and shame) used to keep my behavior in line.  I have awoken to the fact that much of what I was told and am being told now, are lies.  We are being lied to constantly by religious/spirititual leaders, politicians, psychology, big food, big pharma, educators, and those around us.</p><p>So I believe that the looking allows us to reprogram ourselves so that our automatic actions are pre-programmed from an open, rational viewpoint based closer to truth, rather than a fear based one.  Just my take on a very moving target.</p>
