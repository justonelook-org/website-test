<p>I write today about my experiences, as so many have written here before. I'd like to comment on the fact that this 'you-ness' enlightens us onto the proper mode of being, however its not perfectly obvious <span class="forum-post-italic">how we ourselves should act</span> under the presupposition of 'me'. Obvious as the question is, the answer not totally available among these forums. Self Directed Attention is perfectly logical and agreeable, in practice it provides us with the attending of life in front of us so that we may create the proper mode of being, and then to continue to embody the proper mode of being. I hope these forums are a combined effort into the description of the proper mode of being. With articulation we can produce in ourselves a greater life. I myself would like to aid in this effort.</p><p>First, 'Honesty,' honesty is something spoken about these forums and I would like to define Truth in the way I understand it. "The arrow flies true," in the sense of its going in the right direction, is my understanding of truth. This is so important in definition because being honest is whats going to help solve our problems in life. Maybe not all our problems, but it'll aid in the en devour of the right mode of being. And in the articulation of phenomenon within our lives and these forums.</p><p>Second, 'Sacrifice,' although not spoken among these forums directly, is among the cornerstone philosophies for the proper mode of being. Self directed attention solidifies what sacrifice means, because making a decision to direct attention to the breath implies sacrifice of all other stimuli. Therefore the decision to sacrifice all other sensations reinforces the ability to attend to whats meaningful.</p><p>Thirdly, 'Direction,' the addition of a honest direction can better solidify what needs to be sacrificed. Although this is less obvious, lets say SDA and the strengthening of the attention is the most helpful direction to aid in the meta-goal of a proper mode of being. Directions can inform us to what needs to be sacrificed.</p><p>Critique is welcome,</p><p>Thank you,</p><p>Larry Reutov</p><p>P.S. With a proper mode of being, we confront the chaos and order. The known and the unknown to which we attend and traverse, is facilitated by the proper mode of being.</p>
