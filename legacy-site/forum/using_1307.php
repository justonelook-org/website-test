<p>Hi George,  Im no expert but this is how I like to see it - Maybe enlightenment isnt about ridding ourselves of negative thoughts and feelings and instead feeling good all the time. Maybe its more about knowing deep within that feelings and thoughts negative or positive are not who you are they are just impermanent shifting phenomenons. Allowing them to just be there and observing them from a distance takes the power out of them.   Like when you have that thought that youre not good enough, recognising it for what it is - Just old conditioning rising.   Dont struggle with it. Just allow it to be there and carry on with whatever you are doing. Even have a laugh at it and say "there it is again"  We have a choice to identify with it and allow it to drag us into unconsciousness or we can instead rise above it in a sense.     It sounds easy but I realise its not.  Meditating everyday helps to keep me aware so that I dont become bogged down by these things.   I just go about my life knowing that I am on the right road and whatever happens is ok.  I dont believe that there is a panacea in this life to take the pain away so that we will be forever happy. Instead I believe that pain is a natural part of life we cannot avoid. We can however avoid the suffering that we create around the pain. Having said that, I have yet to practice this in a moment of crisis.</p>
