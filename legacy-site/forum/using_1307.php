<p>Dear John and community,</p><p>Lately I have considered different ways other than the looking to try and feel better and become more comfortable in my own life (i.e. trying to find out what and who i really am).</p><p>It has been about 14 months since I started in the spring last year and although their has probably been less confusion than I felt this time a year ago, I have no big report to tell and still have the exact same anxiety and feelings of misery from time to time, that have always been present for me.</p><p>There have been occasions when I have believed that my perception was really starting to take a turn and things were getting a lot better but I think our beliefs are very powerful; whatever we believe is true becomes our experience. However I know that beliefs are vulnerable and as false and true as any other belief.</p><p>Anyway (i'm rambling a bit), in the last couple of months I have considered taking up other spiritual practices, one of which is the new age type of spirituality (which is all about fixing our beliefs to make them more positive and have a better sense of self whilst also being connected to 'the self', 'consciousness' etc). However I have an underlying feeling that I really do not want to change myself. A bit like a stubbornness. I say this is a paradox because at the same time I still feel I would like uncomfortable feelings to no longer be here and just like anyone else, thus feel better. There is a way I imagine I would like myself to be which I think is the case for everyone but as I say, there is a feeling that I don't want to change myself for people and like it really doesn't matter! I hope some will understand what I am saying here. I understand it could sound confusing.</p><p>What I am wondering is, do you think this is result of the looking? No longer wanting to change myself or others to feel good. What is confusing though is that the feelings of alienation and not being good enough etc. are as prevelant as ever.</p><p>Up until I began the looking, between the age of 16 and 24, all I ever tried to do was find ways to change myself and feel good. Now the drive doesn't seem to be their any longer.</p><p>It could also just be a laziness because I can't be bothered to try and change myself and things and am just dependent on the looking doing its work.</p><p>Would be good to hear if anyone else has/had similar thoughts about this.</p><p>Thanks for reading.</p><p>George</p>
