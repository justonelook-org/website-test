<p>Actually, my sense of it is that your body is you, along with everything else in your consciousness, which is much different from saying that you are your body.</p><p>But that question is not even very interesting. What is more interesting is to see how it is that when the fog of fearfulness lifts everything is seen with greater clarity and intelligence.</p><p>There is a rational basis for fear of bees - bees do, after all, sting - and there is an intelligent response to that rational fear. Better understanding of bee behavior, backed up by effective protective gear goes a long way toward prevention of attack, and even if the preparation fails, understanding of effective emergency treatment of bee sting should bring (or return) confidence to your relationship with them. And, of course, you know all this and more. (I love bees, but know next to nothing about beekeeping.)</p><p>The twisting together of your rational cautiousness around bees with your deep empathy with these magical creatures <span class="forum-post-italic">is </span>the work of the fear of life, and the feeling of the tightness of that twisting is perceived as a problem. That will fall away in time, and when it goes, you may not even notice its absence, but you will surely notice the deepening of your satisfaction with life in general and bees in particular.</p><p>I would also advise you to look into the idea that the problem presented by the ever vigilant defensive and reactive thought-forms that serve the fear are matters of wiring in your brain. They are just thought conditioned by the fear of life. Nothing more, nothing less.</p><p>Please let me know if this is of any help to you.</p><p>I feel sure that you are on the right track, and everything will be perfectly clear to you soon enough.</p><p>Thank you for your good participation here Emma.</p><p>love,</p><p>John</p>
