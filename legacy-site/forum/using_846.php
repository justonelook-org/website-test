<p>Dear John and everyone,</p><p>Yes! I mean, yes, what you say is absolutely clear to me. I can see, just as you say, that the fear of life twists itself around the logical fear of bee stings and combines with my empathy into a neurotic soup of fears. That 'soup' appears overwhelming and unfixable until I see it for what it is - the work of the fear of life. So I have some decisions to make about the siting of the hives in my garden in order to be able to continue to enjoy the connection and pleasure I feel around them.</p><p>I have a report to make that relates to this. Since the first looking last year, I have experienced a 'falling away'.  It is quite disorienting. It feels as if I have no skin on. Life feels very close and very vivid. It is gorgeous and amazing but also 'raw'. People seem noisy and life seems, well, people, seem somewhat 'violent'. I am not a soap-box moralist or tub-thumper nor much given to causes, but what we do to animals and our world has become more vivid too, to the point where it feels painful. This is not how I was a year ago. When I look for the feel of me now and me as a child, there is a 'nothing' there (I really can't quite find the words for this). It is not emptiness or any such thing...hmmm...struggling to put this...All I can describe it as, is a kind of alive background. What is left is just the point of contact with life. Not sure this makes any sense. OK, it is like there was a thick mattress between me and everything now there is just nothing, which is everything, just me. Yeah...lol...that description did not quite get it. Think I better give up there!</p><p>My partner has commented that I seem 'absent'. But I feel that the ongoing panorama of natural life is utterly absorbing and it hardly leaves room to speak or get involved in opinions. My memory is different (functionally fine but it just doesn't seem important). My day to day life still runs and the kids get fed. I don't know which bit of the recovery this is, but it is quite a ride! Raw, vivid, gorgeous, violent, and easy to mix some of that with the fear of life and have it turn into a neurotic thought pattern. But when pointed out, easier to see it for what it is.</p><p>Thank you for the opportunity to be incoherent here!</p><p>Best wishes to all,</p><p>Emma ~</p>
