<p>Hello,</p><p>I am hoping this question will make it through as it is genuinely concerning me, but is both practical and philosophical in nature.</p><p>So, here is the situation. This weekend I found one of my beehives has a deep layer of dead bees in the bottom box. For various reasons, this box on the type of hive I use is not easily accessible, so the situation can't be dealt with immediately. The smell of dead bees is bad and it is due to get hotter here this week. Despite being a beekeeper, I have a fair bit of anxiety about bees: being stung and an intense connection to their welfare (for example, I couldn't sleep last night thinking about them). Since coming across John's work, I see that this is fear of life in one of its forms.</p><p>I lay in bed this morning and asked, 'For whom is this anxiety around the bees a problem?'. The answer was clearly that it was only a problem for the wiring in the brain - I think John called it 'algorithms'. That is, the problem is conjured from thoughts, memories, beliefs running through the synapses etc, just a kind of bodily function. My existence or 'here-ness' is untouched by this. Is this untouched thing 'me' then? And, this is the question that is also buzzing in head worse than the 1,000 dead bees, does that mean I<span class="forum-post-underline"> am </span>my body or I <span class="forum-post-underline">have</span> a body? I know, I know, a philosophical sideroad, but I have to say it is important to me and I would really appreciate some feedback...</p><p>With thanks,</p><p>Emma ~</p>
